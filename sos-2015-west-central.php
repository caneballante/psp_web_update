		<div class="col-sm-7 padding-20-top content-column"> <!-- InstanceBeginEditable name="6col_content" -->
		<p>The West Central LIO has been hard at work implementing 24 NTAs in the 2014 Action Agenda. Of the 24 NTAs, several have seen significant progress since the adoption of the Action Agenda, which are outlined below:</p>
        <h3>NTA WC 4—LID Training</h3>
          <p>This NTA provided training for 80 percent of Low Impact Development (LID) professionals (review staff, designers, installers, inspection, and maintenance staff) in Kitsap County. </p>
        <p>LID is a new, de-centralized method for stormwater management for new and redevelopment construction, bringing with it uncertainty for how to design, install, and maintain these types of facilities. </p>
        <p>Clean Water Kitsap, the stormwater utility within Kitsap County, held both field and classroom trainings to complete the NTA. They hosted national and regional webinars and which spurred follow-up discussion about local lessons learned, challenges, and successes, which enhanced the webinar content. They also hosted field tour of LID sites. Field tour speakers included the design engineers, maintenance staff, and installers. A particularly effective component was performing an infiltration rate test of newly installed permeable pavement. </p>
        <h3>NTA WC 11—Steelhead Recovery Chapter</h3>
          <p>The intent of this NTA is to develop a local chapter of the Steelhead Recovery Plan, which the National Marine Fisheries Service has begun drafting. Partners within the West Sound Watersheds Council received a grant from the Salmon Recovery Funding Board to do a steelhead habitat assessment. </p>
        <h3>NTA WC 12—Priority Watersheds for Protection</h3>
          <p>The intent of this NTA is to develop a protection and restoration plan for the upper Chico, Curley, and Blackjack Creek watersheds. The Suquamish Tribe has published the Chico Creek Restoration Plan that completes a component of this NTA. The Tribe has secured approximately half of the remaining funding needed ($145,000), which will fund an assessment of the Curley Creek watershed by September 2016. The Tribe also applied for funding from the Department of Ecology for the Blackjack Creek plan, though at the time of publication the outcome of this grant application is unknown. </p>
        <h3>NTA WC 14—Kitsap Forest &amp; Bay Divide Property Acquisition</h3>
          <p>The intent of this NTA is to acquire the 700-acre parcel known as the Divide Property, part of the larger Kitsap Forest &amp; Bay conservation effort in north Kitsap County. Through grant funding, a capital campaign, and a bequest, Great Peninsula Conservancy (GPC) has secured over $1.9 million in private contributions, and public and private grants to purchase more than 200 acres of the Eastern Divide property. With Kitsap County, GPC submitted a request to the Department of Natural Resources (DNR) to acquire the remaining 500 acres of the Western Divide forest through DNR&rsquo;s Community Forest Trust Program. This project has been selected as the sole recipient of this DNR program in the current funding cycle. The outcome of the Community Forest Program funding is contingent on approval of the final Washington State 2017-2019 biennial budget.</p>
        <h3>NTA WC 16—Duwe&rsquo;iq Stormwater Treatment Wetland and Stream Restoration</h3>
          <p>This NTA is intended to construct a stormwater treatment facility protecting Clear Creek and Dyes Inlet.</p>
          <p>The proposed Duwe&rsquo;iq Stormwater Treatment Wetland was the highest-priority project identified in the Silverdale Retrofit Plan (2012). The deeper forebay and subsequent wetland will treat 18 acres (12 acres of parking lots and 6 acres of roofs) of commercial impervious surfaces prior to discharge into Clear Creek. The project will significantly upgrade treatment of polluted runoff with 70 to 90 percent removal of metals, road grit, vehicle oils, and bacteria. The project also serves to restore the creek buffer, benefiting habitat to both creek side and upland areas, and a more natural environment for trail walkers. The project is 100 percent designed, permits are in progress, and a draft offer grant from the Ecology Stormwater Financial Assistance Program assures this project for completion in 2016 or 2017. </p>
        <h3>NTA WC 27—Marine Drive/Kitsap Way/Oyster Bay Avenue Storm System Filtration Retrofit</h3>
          <p>The intent of this NTA is to improve water quality impacting shellfish harvest in Oyster and Ostrich bays by installing a passive stormwater filtration system prior to the outfall into Oyster Bay and along several miles in the basin. The City of Bremerton, the owner for this NTA, reached 100 percent design for the retrofit but due to funding constraints within the City&rsquo;s budget, will be re-prioritized for next year. </p>
