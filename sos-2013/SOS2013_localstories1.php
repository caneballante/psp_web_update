						<div class="SOS_localheader">Public-Private Partnership Success Story:<br />
Powel Shoreline Restoration on Bainbridge Island</div>
						
					
						<p><img src="images/SOS2012_powell2.jpg" width="380" height="252" /></p>
						<p>A longtime Bainbridge Island family is working with a local non-profit conservation organization to restore more than 1,500 feet of altered shoreline. This unique project showcases options for restoring habitats on private residential shoreline property. </p>
						<p>For more than 30 years, coastal development in Washington State has placed increasing pressure on shoreline habitats and the aquatic resources that depend on them. Nowhere has that been more evident than on <a href="#bainbridge">Bainbridge Island</a>.</p>
						<p>Nearshore and estuarine habitats of Bainbridge Island have been impacted by shoreline modifications. About 80% of Bainbridge Island’s shoreline has been developed for single-family residential use. Major modifications include <a href="#shorelinearmoring">shoreline armor </a>(i.e.,bulkheads), fill, removal of riparian vegetation, increased impervious surfaces, overwater structures, and marina development. The removal of riparian vegetation and proximity of shoreline development has had a substantial impact on nearshore ecosystems. </p>
						<h3>Powel Family Property</h3>
						<p>For nearly 60 years, the Powel family has lived at their Port Madison property on Bainbridge Island. In 1992, John and Ann Powel donated a perpetual conservation easement to the <a href="#brainbridgeTrust">Bainbridge Island Land Trust (BILT)</a> in order protect the property’s nearshore and upland environment from further development. </p>
						<p>Like many other residential properties throughout the Puget Sound region, their shoreline had been hardened with bulkheads made of rock, concrete, creosote-treated timbers, and other materials. A 2004 inventory of shoreline armor, conducted by the City of Bainbridge Island, found that approximately 1800 lineal feet of the Powel property shoreline was armored, and 70 percent of that was in failed or poor condition. </p>
						<p>In 2008, landowner Babe Kehres (daughter of Ann Powel) contacted the Bainbridge Island Land Trust for advice on what to do about failing bulkheads and asking for conservation-oriented options for protecting existing infrastructure on their property. Bainbridge Island Land Trust saw an opportunity to regain important nearshore habitat in Puget Sound and suggested the family consider a restoration alternative to rebuilding their bulkheads. They agreed to consider restoration options and requested additional information.</p>
						<p>Bainbridge Island Land Trust began working with the landowners and representatives of the city, county, and state and the Suquamish Tribe to conduct a preliminary assessment for the potential restoration of the Powel family shoreline. Protecting existing residences and infrastructure, as well as meeting restoration goals, were important. Nearshore experts, representatives of the West Sound Watersheds Council, and others evaluated the site and helped develop a restoration concept, which Bainbridge Island Land Trust presented to the landowners. The Powel family approved the restoration concept, which included more than 1,500 lineal feet of nearshore habitat. On their 7.5 acre property, which is surrounded by marine shoreline on the east, south and west boundaries of the property.</p>
						<p>
							<p><img src="images/SOS2012_powell1.jpg" width="400" height="267" /></p>
						<h3>Design Phase</h3>
						</p>
						<p>Having secured the landowner’s approval to move forward, Bainbridge Island Land Trust successfully applied for and received a PSAR Salmon Recovery Funding Board grant for nearly $130,000 from the Recreation and Conservation Office to fund a highly collaborative design plan. The City of Bainbridge Island provided nearshore assessment data and permitting assistance. Washington Sea Grant provided science and technical expertise and monitoring assistance. Suquamish Tribe provided monitoring, fisheries, technical and cultural resource assistance. Westsound Watersheds Council, Puget Sound Partnership, Coastal Geologic Services provided engineering services, and Washington Recreation and Conservation Office provided technical review, project consultation, and funding.  The project sponsor, family members, the design team, and other stakeholders met 8 times over a 12 month period to discuss and develop a mutually agreeable restoration design.  Once the design was complete, all parties agreed to move forwarding with applying for an implementation grant, with BILT being the project sponsor and project manager.</p>
						<p>
						<h3>Implementation Phase</h3>
						</p>
						<p>After nearly four years from the initial discussions between the Powel family and BILT,restoration planning and design work, the Powel family and the Bainbridge Island Land Trust broke ground on the Powel Shoreline Restoration Project at Port Madison in August 2012.   Armor and invasive plants removal in the project area was accomplished by the end of September and all native plantings were accomplished by early December. The project is expected to be completed in 2015, including two years of implementation monitoring and maintenance, at a cost of approximately $340,000 (while providing long term ecological values), which is estimated to be about $175,000 less than the cost of replacing the bulkheads (not including debris removal). Funding for the project has been provided by the Powel family, Bainbridge Island Land Trust, and the State of Washington’s Puget Sound Acquisition and Restoration fund, which is administered by the Salmon Recovery Funding Board and appropriated by the Legislature.</p>
						<p>Restoration of the Powel shoreline will provide a variety of ecological benefits by restoring nearly a quarter mile of shoreline through the removal of failed or poor-condition shoreline armor and the planting of native riparian vegetation. This will result in increases in various habitats, including an overall increase of 163% in shallow intertidal important to juvenile salmonids for migration, feeding, and refuge, particularly endangered Puget Sound Chinook salmon. </p>
						<p>Over time, the project will almost triple the amount of salt marsh habitat and enhance more than 32,000 square feet of marine riparian habitat that will restore and or enhance natural ecological structure and result in a more resilient shoreline. The project will also demonstrate how such actions may be taken on privately owned shorelands in a way that balances restoration with residential use.</p>
						<p>Next Steps:<br />
								Long term effectiveness monitoring of post armor removal, to measure the changes in the nearshore as a result of the restoration efforts, while not funded by the implementation grant, is being pursued by a number of project partners.  Due to the goal of project partners to use this demonstration project as an educational opportunity, some aspects of the monitoring will likely to be performed by citizens, following a protocol that can hopefully be a template for use by other restoration projects in the region. </p>
						<p>
						<h3><a name="bainbridge">About Bainbridge Island</a></h3>
						</p>
						<p>Bainbridge Island is a special place. Its million-dollar views and prime location—an easy 35-minute ferry ride from downtown Seattle—prompted CNN/Money and Money magazine to name it the second-best place to live in the United States in 2005</p>
						<p>One of the largest Islands in Puget Sound, it has approximately 53 miles of  bluff-backed beaches, with numerous bays and inlets and a diversity of shoreline habitats, including bluffs, rocky outcrops, lagoons, tide flats, and stream and tidal deltas. Virtually all coastal and estuarine habitat types in Washington State are found on or adjacent to Bainbridge Island. These habitat types include tidal freshwater marshes, tidal lagoons, sand/gravel beaches, stream and coastal riparian vegetation, salt marshes, flats, channels, eelgrass meadows, rocky shores, and kelp forests. </p>
						<p>
						<h3><a name="bainbridgeTrust">About Bainbridge Island Trust</a></h3>
						</p>
						<p>The Bainbridge Island Land Trust (BILT) was started in 1989 by a group of Bainbridge Island residents concerned about growth on Bainbridge Island and established the Land Trust to provide a mechanism for landowners to protect and preserve their property. Its mission is to preserve and steward the diverse natural environment of Bainbridge Island for the benefit of all. To achieve this goal, the Bainbridge Island Land Trust acquires interests in land having significant or potentially significant conservation values such as scenic vistas, wetlands, open spaces, tidelands, forest, unique plant and animal habitats and stream and wildlife corridors. </p>
						<p>Twenty four years after its founding, the Land Trust has helped to protect over 1,250 acres of vulnerable forestlands, wetlands, meadows, shorelines, agricultural lands, riparian corridors and scenic vistas, of which more than 950 acres are open to the public. The Land Trust holds 47 conservation easements (permanent land protection agreements on 42 private and 5 publicly owned properties) encompassing 708 acres, with an additional 83 acres owned by the Land Trust outright.  Finally, 465 acres in which the Land Trust currently holds no real property interest have been preserved as public parks and natural areas through Land Trust acquisition efforts or with Land Trust assistance.</p>
