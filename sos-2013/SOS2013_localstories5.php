				<p>
				<h3>Salish Sea Hydrophone Network and Orca Network</h3>
				<p>The Salish Sea Hydrophone Network and Orca Network are two citizen science projects dedicated to furthering our understanding of abundance, distribution, behavior, and habitat use by the endangered population of Southern Resident Killer Whales, also called orcas. The Hydrophone Network lets the public listen for orcas through their computers and phones, while the Orca Network gathers and disseminates sightings of orcas as they move between Puget Sound, the Fraser River, and the Pacific Ocean.</p>
<h3>Listening in on Orcas</h3>

		<p>The Salish Sea Hydrophone Network started in 2007 and now includes five hydrophones (underwater micro- phones): two on San Juan Island, and one each at Port Townsend Marine Science Center, the Seattle Aquarium, and Neah Bay. By monitoring the sounds streaming live on orcasound.net, scientists, educators, and the public can help detect loud calls and clicks made by orcas as they communicate and hunt. Listeners can also help detect noise pollution caused by Naval sonar and vessel traffic.</p>
		<p class="floatright"><img src="images/SOS_2013_orca1.jpg" width="430" height="573" /></p>
				<p>For orcas and other whales, the underwater sound environment is critical to their sensory experience and behavior. Orcas communicate with each other over short and long distances with a variety of clicks, chirps, squeaks, and whistles. They also use echolocation to locate prey and to navigate.</p>
				
				<p>Hydrophone Network volunteers log their observations on a collaborative Google spreadsheet online or report detections via email. Volunteer observations help to direct field research, including prey sampling studies that revealed the orcas strong preference for Chinook salmon and fecal sampling studies that show orcas may be prey-limited. In addition, the hydrophone network enabled early detection of a new orca calf in 2009.</p>
				
				<p>The Network allows friendly competition and collaboration between volunteer listeners and computers. In detecting when orcas passed by a proposed tidal turbine site near Port Townsend, human listeners heard the orcas 10 of the 22 times they passed by (45%) while auto-detection software detected them 14 times (64%). When both approaches were combined orcas were detected 17 times (77%).</p>
				<p>The number of orcasound.net visitors per day rises from a mid-winter low of about ten to a summertime average of approximately 100, with occasional spikes to 200-350. Listeners are predominantly from the U.S. (75%) and Canada (13%), so observers from distant time zones are sought to boost nighttime detection rates.</p>
				<h3>Watching for Orcas</h3>
			
				<p>Given the wide-ranging travels of the Southern Resident Killer Whales and other whales in the Salish Sea, it is impossible for the few whale researchers to track all the individuals on a regular basis.</p>
				
		
				<p>Orca Network’s Whale Sighting Network was started in 2001 to provide more information on Southern Resident Killer Whale travels in inland and coastal waters. In addition, the network also raises aware- ness, educates the public, and provides a networking and communication system for researchers, educators, and the public. There are currently more than 7,000 participants on the Sighting Network email list, and more than 14,000 subscribers to the Facebook page.</p>
				
					<p class="floatleft"><img src="images/SOS_2013_orca2.jpg" width="500" height="569" /></p><p>With more than 15,000 sightings reported to date by the hundreds of participants in the Sighting Network, Orca Network harnesses broad public interest in whales to provide researchers with critical information for tracking these endangered whales.</p>
				
				<p>Through the Sighting Network, volunteers report sightings of whales, which provide valuable information on habitat use, social and foraging patterns, and behaviors for researchers managing the recovery of Southern Resident Killer Whales. Reports are compiled and sent to researchers, natural resource managers, and educators and are available on the Orca Network website, Facebook page, and Twitter feed.</p>
				
				<p>The Sighting Network also provides an important communication and tracking tool during emergency situations such as oil spills and entangled whales. It also helps identify orcas out of their usual habitat, such as Springer, the Northern Resident orca calf who was reported through the Sighting Network in Swinomish Channel, then off Edmonds, before showing up off Vashon Island. She was relocated to her home in Canadian waters in 2002.<br />
