						<div class="SOS_localheader">CLEAN SEWERS, CLEAN THEA FOSS WATERWAY</div>
						
					
				<p><img src="images/SOS2012_foss1.jpg" width="430" height="279" /></p>
						<p>Located in the heart of downtown Tacoma, the <u><a href="#Thea_foss_waterway">Thea Foss Waterway</a></u> was once characterized by dilapidated buildings, coal tar deposits, and oil sheens. For more than 100 years, contaminants had been accumulating in <u><a href="#marine_sediments">marine sediments</a></u>, a legacy of Tacoma&rsquo;s industrial past. Back then, people believed ocean tides would carry the waste away, so municipal sewers emptied right into the bay, and industries dumped waste into waterways. </p>
				<p>We now know that pollutants bond with sediments and settle into the waterways, so we treat wastewater and monitor stormwater, but our past behavior created a buildup of more than one million cubic yards of contaminated sediments.</p>
				<p>In 1983, the Environmental Protection Agency (EPA) identified the Thea Foss and Wheeler-Osgood waterways as part of the larger 12-acre Commencement Bay Superfund site. The City and the EPA found the following chemicals polluting the sediment in the waterways: </p>
				<ul>
						<li><strong>Metals</strong>:  Arsenic, copper, lead, mercury and zinc. Although trace elements can occur naturally, they are toxic at high levels. </li>
				
						<li><strong>Pesticides</strong>: Includes DDT (dichlorodiphenyltrichloroethane), DDD (dichlorodiphenyldichloroethane), and DDE (dichlorodiphenyldichloroethylene).				</li>
				
						<li><strong>PAHs (</strong>Polycyclic Aromatic Hydrocarbons), a group of organic chemicals that includes several petroleum products and byproducts formed during the incomplete burning of coal, oil, gas and other organic substances.</li>
				
						<li><strong>PCBs (Polychlorinated biphenyls)</strong>: Non-flammable, man-made chemicals historically used as coolants and lubricants in electrical equipment such as transformers and capacitors.				</li>
				
						<li><strong>Phthalates</strong>: Chemicals used to make plastic flexible, mostly PVC (polyvinyl chloride) products. It is also used in detergents, lubricants, and solvents.</li>
						
						<li><strong>Phenols</strong>: Coal tar was the only source of phenol during World War I. The synthetic form is used in the plywood adhesive, construction, automotive and appliance industries						</li>
				</ul>
				<p>Metals, PAHs, and other contaminants, both organic and synthetic, can be toxic to various plants, animals, and humans, depending on the level of contamination. The origins of sediment contamination can be divided into <u><a href="#point_nonpoint">point and nonpoint sources</a></u> of pollution. Point source pollution comes from a specific, identifiable source such as an industry or smokestack. Nonpoint source pollution is more widespread in nature such as runoff from precipitation and atmospheric deposition that cannot be traced to a specific spot.</p>
				<p>Some of these pollutants, such as the pesticide DDT and PCBs, were released into the environment long ago. Their use was banned in the United States in the 1970s, but these chemicals persist for many years. Other contaminants enter our waters every day as a result of everyday activities such as driving.</p>
				<h3>The Big Cleanup</h3>
						</p>
						<p>Today, it&rsquo;s a very different picture. The Thea Foss Waterway is the centerpiece of bustling marinas, internationally renowned museums, restaurants, grass esplanades, luxury apartments, and a variety of business and industry. </p>
						<p>Under a plan accepted by the EPA, the City cleaned up 80% of the Foss. A group of private companies, including Puget Sound Energy and PacifiCorp, cleaned up the other 20%. From 2002 to 2006, about 425,000 cubic yards of contaminated sediments were dredged from the waterways. Other areas of the Thea Foss Waterway were capped with clean sediments to contain some of the contamination in place. </p>
<div class="floatright"><img src="images/SOS2012_foss2.jpg" width="600" height="445" /></div>
				<h3>On-going Efforts</h3>
						</p>
						<p>The water that leaves our streets, yards, and driveways in Tacoma flows directly to lakes, streams, rivers and Puget Sound. Often referred to as stormwater runoff, this water is not treated at a treatment plant before entering our waterways. That&rsquo;s why our surface water management programs concentrate on preventing pollution before it reaches our waters, and restoring sites already affected by industry and urbanization. </p>
						<p>Even before the <u><a href="#city_of_tacoma">City of Tacoma</a></u> and its partners finished the $105 million remediation of the Thea Foss Waterway in 2006, they knew it was imperative to find ways to protect the quality of the sediment in receiving waters, which are bodies of water such as rivers, streams, lakes, bays, estuaries, and ground water that accepts runoff.</p>
				<p>While the City made significant efforts to reduce or eliminate ongoing sources of contamination to the storm drainage system, technicians found elevated levels of PAHs, PCBs, and mercury remained in sediment and debris collected from Tacoma&rsquo;s 100-year-old storm sewer lines. This legacy pollution was being washed into the Thea Foss by stormwater, threatening to degrade the quality of the newly remediated marine sediment.</p>
						<p>In response, Tacoma launched two new enhanced maintenance programs to prevent new and legacy contaminants from reaching the waterway: storm line cleaning and street sweeping. </p>
						<p><strong>Storm Line Cleaning</strong>: Between 2006 and 2011, the City removed debris and accumulated sediment from storm pipes in four entire drainages and part of a fifth in an effort to remove legacy contaminants from storm lines. </p>
												<div class="floatright"><img src="images/SOS2012_foss3.jpg" width="300" height="218" /></div>
				<p><strong>Street sweeping:</strong> In 2007, the City expanded its aggressive city-wide street sweeping program to remove street contaminants and prevent them from entering the storm system. Streets are the chief collection and removal points for vehicle-related pollution, atmospheric pollutant deposition, construction-related sedimentation, trash, materials from normal street deterioration, and chemicals and sand particles from winter snow and ice management. Sweeping also helps prevent storm lines from becoming clogged with vegetation and sediment, which improves water quality and reduces ongoing habitat deterioration.</p>
				<p>These two enhanced maintenance efforts went above and beyond Tacoma&rsquo;s <u>NPDES</u> permit requirements. These programs were so successful that they are now part of Tacoma&rsquo;s city-wide operating procedures. This enhanced maintenance, in combination with the existing aggressive source control program, resulted in dramatic reductions in contaminant levels.  Monitoring of seven outfalls over the 11 year monitoring record show statistically significant reductions for:</p>
						<ul>
								<li>PAHs - 87-97% reductions in all seven outfalls tested.<br />
								</li>
								<li>Di-(2-Ethylhexyl) phthalate - 58-91% reductions in all seven outfalls.<br />
								</li>
								<li>Total suspended solids - 52-63% reductions in four outfalls.<br />
								</li>
								<li>Lead - 51%-56% reductions in four  outfalls.</li>
				
								<li>Zinc  - 32%-50% reductions in five outfalls.</li>
						</ul>
				
				
						<p>Despite these accomplishments, the work is not over. The City of Tacoma&rsquo;s team of innovative stormwater professionals will continue to use every tool at its disposal—science, investigation, education, enforcement and even intuition—to protect our investment in the Thea Foss Waterway. Their mission is to create an asset for future generations by making sure stormwater discharges do not harm the health of the water and sediments in the Foss.				</p>
				<h3><a name="Thea_foss_waterway">Thea Foss Waterway</a></h3>
						<p>Thea Foss Waterway is an inlet that connects to Puget Sound through Commencement Bay. This narrow estuarine waterway runs roughly north-south just to the east of downtown Tacoma. It is part of the Thea Foss watershed, which is one of nine drainage basins in Tacoma. This basin includes the Wheeler-Osgood Waterway and has 13 municipal outfalls that discharge stormwater to the waterways as well as numerous private outfalls.</p>
				<h3><a name="marine_sediments">Marine Sediments</a></h3>
						</p>
						<p>Sediments are soils, sand, and other matter that wash from land and settle on the bottom of a river, lake, bay, or the ocean floor. These sediments are vulnerable to the effects of pollutants that enter the water bodies since the contaminants can attach to these particles and accumulate. </p>
						<p>Contaminated sediments affect small creatures such as worms, crustaceans, and insect larvae that live on the bottom of a water body, called the benthic environment. Some kinds of toxic sediments kill benthic organisms, reducing the food available to larger animals such as fish.</p>
				<p>Others contaminants are taken up by benthic organisms in a process called bioaccumulation. When larger animals feed on these contaminated organisms, the toxins are taken into their bodies, moving up the food chain in increasing concentrations in a process known as biomagnification. As a result, fish and shellfish, waterfowl, and freshwater and marine mammals, as well as benthic organisms, are affected by contaminated sediments.</p>
						<p><a href="http://www.psp.wa.gov/vitalsigns/marine_sediment_quality.php">Learn more about marine sediments from the Puget Sound Partnership</a>.</p>
				<h3><a name="point_nonpoint">Point and Nonpoint Pollution Sources</a></h3>
<p>The origins of sediment contamination can be divided into point and nonpoint sources of pollution. Point source pollution comes from a specific, identifiable source such as an industry or smokestack. Nonpoint source pollution cannot be traced to a specific spot. Point sources of pollution include municipal sewage treatment plants, stormwater discharges from commercial and industrial facilities, and waste discharges from industry.</p>
<p>Nonpoint sources include stormwater runoff from homes, streets, sidewalks, parking lots, and green spaces that are sources that are widespread and cannot be controlled with a typical source control action. Some contaminants are carried through the air, landing directly in lakes and streams far from the factories and other facilities that produced them.</p>
<h3><a name="npdes">NPDES </a></h3>
						<p>NPDES (National Pollutant Discharge Elimination System) permit requirements are a part of the Clean Water Act passed by the US Congress in 1972. It requires communities in urban areas to implement stormwater management programs or pollution prevention plans that effectively reduce or prevent the discharge of pollutants into receiving waters.</p>
				<h3><a name="city_of_tacoma">City of Tacoma Environmental Services</a></h3>
						<p>Environmental Services regularly inspects, cleans, and maintains more than 500 miles of public stormwater pipe, 22,000-plus catch basins, four pump stations and numerous detention ponds/structures.</p>
				<p>Learn about how the City of Tacoma manages stormwater, prevents flooding, restores Tacoma watersheds and uses science and research to solve problems and prevent future pollution. <a href="http://www.cityoftacoma.org/Page.aspx?nid=190">Tacoma&rsquo;s website is updated as new data and information is available.</a></p>
