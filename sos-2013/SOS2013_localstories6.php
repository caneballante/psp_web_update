						<h3>Storm Team</h3>
			
				<p>A high fecal coliform result during a heavy rain event in 2008 in the Samish watershed, an important commercial shellfish growing area, raised concerns and led to the creation of the Storm Team. Although Samish Bay usually has good water quality, tests showed that during storms large volumes of pollutants wash off the landscape into local streams and rivers and contaminate the bay.</p>
				<p>The Storm Team is a dedicated core of volunteers that head out in the middle of rainstorms as streams and rivers are rising to collect water samples for fecal coliform bacteria testing. Testing during high flow conditions is an important complement to the Stream Team&rsquo;s regular ambient monitoring, and it has been instrumental in identifying priority areas for clean up efforts.</p>
			<p class="floatright">	<img src="images/SOS_2013_skagit2.jpg" width="250" height="188" /></p>
				<p>Initial Storm Team efforts in the Samish watershed helped establish baseline data for the river during storm events for the Washington State Department of Health (DOH) Office of Shellfish &amp; Water Protection, which regulates the commercial shellfish industry. DOH uses fecal coliform loading to determine when to issue a pollution closure.</p>
				<p>As a result of Storm Team and Skagit County sampling, DOH changed the classification of most of Samish Bay from Approved to Conditionally Approved in 2011. Samish Bay commercial shellfish growing areas are now closed automatically when the river reaches 4.7 trillion fecal coliform colonies per day—a level determined to pose a risk for shellfish consumption.</p>	<p class="floatleft">	<img src="images/SOS_2013_skagit1.jpg" width="350" height="404" /></p>
				<p>Storm Team sampling efforts were critical in documenting fecal coliform contamination problems in the Samish watershed. The Clean Samish Initiative (CSI), a partnership of local, state, and federal agencies and organizations, was launched in 2010 by Skagit County with funding from the US Environmental Protection Agency. The CSI effort was put together to identify sources of fecal contamination and to find ways to correct them. With increased County sampling efforts under the CSI, the Skagit Storm Team has been able to redirect efforts over the last two years to the Bay View and No Name Slough drainages in the Padilla Bay watershed.</p>
				<p>More information about the Skagit Stream Team and Storm Team can be found at: <a href="www.skagitcd.org/stream_team">www.skagitcd.org/stream_team</a></p>
				<p>Information about the Clean Samish Initiative can be found at: <a href="www.skagitcounty.net/cleanwater">www.skagitcounty.net/cleanwater</a></p>
