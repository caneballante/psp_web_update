				<div class="SOS_localheader">Rain Gardens to the Rescue:<br />
						Puyallup Gets Disconnected
				</div>
				<p><img src="images/SOS2012_raingarden1.jpg" width="430" height="341" /></p>
<p>Since 2009, the City of Puyallup has educated hundreds of citizens on <u>stormwater pollution</u> prevention through its <u>Rain Garden Program</u>. Since the program began, 62 <u>rain gardens</u>, including seven large rain garden clusters, have been installed in Puyallup. As a result of the rain garden program alone, more than one million gallons of stormwater have been diverted annually from the City’s stormwater system. On a larger scale, Puyallup has cataloged to-date the disconnect of over 13 million annual gallons of stormwater through the installation of rain gardens, permeable pavements, and green roofs in private, commercial, and capital projects. </p>
<p>Improving the management of stormwater helps protect water quality, wildlife habitat, and aquatic resources in the Puget Sound area. Efforts to more effectively manage stormwater are happening throughout the region and involves state, local, and federal agencies; tribes; non-profit organizations; citizen groups; businesses; and many others.</p>
<p>Puyallup is the first City in Washington to implement a rain garden program on a large scale, earning the City the 2011 &ldquo;Going Green&rdquo; Municipal Excellence Award from the Association of Washington Cities.</p>
<p><u>
		<h3>Stormwater Pollution</h3>
</u></p>
<p>Stormwater runoff is rain or snowmelt that flows off developed land and enters nearby streams and rivers directly or through drainage systems before flowing untreated into Puget Sound.</p>
<p>Stormwater runoff poses a high risk to the health of Puget Sound&rsquo;s residents and ecosystems. Stormwater carries a mixture of pollutants—including petroleum products, heavy metals, animal waste, and sediments—from roofs, roads, sidewalks, parking lots, construction sites lawns, and other developed lands or hard surfaces. Stormwater is the leading contributor to water pollution in urban waterways, and has harmed virtually all urban creeks, streams, and rivers in Washington State. Stormwater can also cause flooding and damage property in the wet, winter months, especially during and after major storms. </p>
<p><u>
		<h3>Rain Gardens</h3>
</u></p>
<p>Rain is a fact of life in the Pacific Northwest, and rain gardens are a beautiful way to manage stormwater runoff naturally where it originates, rather than letting it flow into the stormdrains. Rain gardens act like small stormwater facilities that collect, store, and filter water. They help remove pollution such oil, grease, and animal waste by filtering stormwater through layers of soil and plant roots before recharging groundwater supplies. </p>
<p>Rain gardens are relatively simple in design and setup. They can be small or large, and generally consist of native perennial flowers, shrubs, and grasses planted in a sunken, shallow flowerbed (about 18&rdquo; deep) that is strategically placed in your yard to effectively capture rainwater and let it slowly infiltrate into the ground. Rain gardens reduce flooding, help recharge groundwater aquifers, provide beneficial wildlife habitat, and filter pollutants before they can contaminate local waterways.</p>
<p>By connecting rain gardens to your downspouts, you can even capture rainwater from your roof. Other ways to keep runoff out of the stormwater system include harvesting rainwater in a rainbarrel or cistern and installing permeable pavement on your property. Rooftop gardens are also another option.</p>
<p>From the City of Puyallup&rsquo;s website you can download a copy of the <u>Rain Garden Handbook for Western Washington Homeowners</u> developed by Washington State University Extension to learn more about the design, installation, and simple upkeep of rain gardens in the Pacific Northwest climate. [http://www.cityofpuyallup.org/files/library/883cc713be29f80e.pdf]</p>
<p><u>
		<h3>Puyallup&rsquo;s Rain Garden Program</h3>
</u></p>
<p>Puyallup started its Rain Garden Program to meet the water quality education and outreach requirements of the National Pollution Discharge Elimination System (NPDES) permit, which is required by the Clean Water Act. In partnership with other local agencies, Puyallup launched the program in 2009 to educate homeowners about low-impact development techniques by installing rain gardens to treat stormwater runoff at private residences. Three neighborhoods clusters were installed in the City during the program&rsquo;s first two years. </p>
<p>Together with consultant <u>Stewardship Partners</u> and on-going program partner <u>Pierce Conservation District Stream Team</u>, the City of Puyallup completed the planning and installation of the first Rain Garden Clusters through funding from Washington State&rsquo;s Department of Ecology grant programs as well as donations from local businesses and individuals.</p>
<p>The program emphasized community involvement by clustering the installation sites at six to eight homes in a neighborhood. The &ldquo;cluster&rdquo; approach allows one catalyst neighbor to serve as a spokesperson to the other neighbors to share with them the benefits of installing a rain garden.</p>
<p>Puyallup targeted different neighborhoods through direct mailing invitations to rain garden workshops. The first rain garden workshop had over 100 attendees, who were asked to recruit their neighbors and contact the City if they met the criteria for becoming a rain garden neighborhood.</p>
<p>Once a neighborhood was selected, the City worked with its consultant Stewardship Partners to find willing landscapers and retailers to donate equipment, labor, materials, plants, and more. The educational effort extended beyond homeowners to include local businesses and landscape professionals who benefited from learning how to install rain gardens. </p>
<p>The culmination of the rain garden program occurred at each neighborhoods&rsquo; &ldquo;planting day,&rdquo; when the rain gardens were already constructed and ready for planting. Neighborhood volunteers, local school children, and other organizations joined neighbors to plant the gardens and learn more about the City&rsquo;s water quality efforts.</p>
<p>At each planting day event, the City closed the street and hosted an environmental fair where residents could learn more about the City&rsquo;s water quality efforts, additional low-impact development techniques for homeowners, and connect with local conservation groups. These included a demonstration of the City&rsquo;s car wash kits (which help divert polluted water from the stormwater system), radio broadcasts, marking of storm drains (a.k.a. catch basins), and more. The event usually ended with a neighbor hosted barbeque or block party. </p>
<p>By coordinating with homeowners to install grant-funded rain gardens and other Green Stormwater Infrastructure such as rain barrels and permeable pavement at private homes, the City of Puyallup also helped its citizens beautify their yards and neighborhoods and build community relationships. </p>
<p><u>
		<h3>Puyallup Rain Garden Clusters</h3>
</u></p>
<p>The City of Puyallup&rsquo;s first rain garden cluster, which included seven rain garden sites, was installed at the 900 block of 8th Avenue NW in August 2009. Stewardship Partners, City Planning, and stormwater groups organized and funded the installation, while Pierce Conservation District Stream Team organized and coordinated the efforts of community volunteers. Ciscoe Morris, of &ldquo;Gardening with Ciscoe,&rdquo; was brought in to this event to focus media attention and publicity on the great community efforts and environmental demonstration and education project. Ciscoe offers garden advice on his weekly radio show on 97.3 FM KIRO radio and in gardening segments on KING5 TV.</p>
<p>Several local businesses donated materials and staff time to help make the installations a success. One homeowner was inspired to add a rain garden in the back yard after the installation was complete. Neighbors used the opportunity to convince City officials to alleviate local flooding in the street. Construction of a new permeable pavement street with adjacent rain gardens and permeable sidewalks was completed in 2012. Stewardship Partners received funding in recent years to offer rain gardens to the remaining homes on the street as well. </p>
<p>Another seven rain gardens were installed near a school at 5th Avenue SE in June 2010. The Spinning Elementary School neighborhood rain garden cluster was the first to incorporate education of the nearby elementary school students. Two fifth grade classes spent a morning learning more about stormwater issues, marking storm drains, and planting a rain garden as part of this installation.</p>
<p>Puyallup&rsquo;s third official rain garden cluster installation occurred in September 2010. This event installed rain gardens at six homes on 18th Street SW. Ciscoe Morris from Gardening with Ciscoe again broadcasted live from the event. In addition to the planting activities, the City gave a Fish-Friendly car wash demonstration. View a video demonstration online at: <a href="http://www.youtube.com/watch?v=5Y2LoDiwEeQ">http://www.youtube.com/watch?v=5Y2LoDiwEeQ</a>.</p>
<p>Other environmental organizations present at the installation provided demonstrations and information on subjects such as permeable pavement driveways, compost-amending lawns, home energy audits, and information on stormwater and current restoration efforts under way in the Puyallup River Watershed.</p>
<p>Since these first three garden clusters were installed in 2009 and 2010, the City has completed nearly 60 more rain gardens. Learn more about these rain gardens at the City&rsquo;s website: <a href="http://www.cityofpuyallup.org/services/public-works/stormwater-management/programs/puyallup-rain-gardens/">http://www.cityofpuyallup.org/services/public-works/stormwater-management/programs/puyallup-rain-gardens/</a></p>

				
