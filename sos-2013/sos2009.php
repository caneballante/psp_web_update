    <p>The Partnership is required to produce a State of the Sound report every two years. &nbsp;The statutory reporting requirements are to document the current status of the ecosystem, as well as status of implementation and funding. This information can be used to inform decisions about changes to funding, programs, or policies that might accelerate the regional progress towards ecosystem recovery, including more efficient use of resources. <br />
        <br />
      In the 2009 State of the Sound report, the Partnership 1) documents the current status of the ecosystem, 2) explains the performance management system we are putting in place to manage recovery efforts in a systematic way and our progress to date in developing the system, and 3) presents an overview of funding and anticipated results for the 2009-11 biennium, as well as accomplishments in 2007-09 biennium. </p>
