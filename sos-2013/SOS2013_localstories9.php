						<h3>Beach Watchers Keep an Eye on the Eelgrass</h3>
				
				<p class="floatleft"><img src="images/SOS_2013_beach1.jpg" width="430" height="323" /></p>
				<p>Washington State University Extension Island County Beach Watchers – Eelgrass Monitoring Project Lush, subtidal beds of eelgrass provide habitat where snails and fish lay eggs, larvae thrive, crabs and forage fish reside, and young salmon seek shelter. Eelgrass dampens the impact of waves and resists the pressures of erosion. Knowledge about eelgrass in Island County is fueled by the Washington State University (WSU) Extension Island County Beach Watchers&rsquo; Eelgrass Monitoring Project, which was born from a combination of university vision, knowledgeable and resourceful volunteers, a compelling question, and collaboration.</p><p class="floatright"><img src="images/SOS_2013_beach2.jpg" width="400" height="533" /></p>
				<p>In the late 1980s, WSU Extension launched Beach Watchers to provide education, outreach, research, and steward- ship for the marine environment in Puget Sound and the Salish Sea. Since it inception, the program in Island County has trained more than 400 volunteers, and each year it records more than 15,000 volunteer hours and monitors 30 beaches. In 2002, Beach Watchers turned attention to eel- grass in a membership survey. Information and educational materials about eelgrass continued. The combination of increased eelgrass awareness, knowledge of the marine environment, and skillful observation fostered an important observation in 2007 when a Beach Watcher noted some eelgrass beds at Holmes Harbor had disappeared. The idea for the Eelgrass Monitoring Project soon followed. With funding from the Island County Marine Resource Committee, advice and assistance from the University of Washington Friday Harbor Labs and Washington State Department of Natural Resources (DNR), and a pilot study in 2008, the Eelgrass Monitoring Project was up and running at full-scale in 2009.</p>
				<p>The Eelgrass Monitoring Project is conducted annually and includes three components: 1) a boat survey using under- water videography to document presence and absence of eelgrass along DNR-specified transects perpendicular to the shoreline at ten sites, 2) aerial photography during summer low tides to provide a broader look at eelgrass extent over a larger area, and 3) a boots-in-the-muck survey to count eelgrass leaves, measure plant density and water temperature, and gather vegetation samples in Holmes Harbor. Since program inception, more than 50 volunteers have contributed more than 1000 hours to collecting eelgrass data.</p>
				<p>Surveys in 2009 and 2010 confirmed extensive eelgrass beds in Cornet Bay and Holmes Harbor. Damage to eelgrass beds was documented in Cornet Bay with the patterns suggesting possible damage from boating activities. Penn Cove surveys showed relatively few eelgrass beds with an unusual number of green sea urchins. Three years of study in Holmes Harbor point to eelgrass return and relatively stable beds since 2007 and suggest an unusual 2006-2007 winter storm from the north that coincided with an extreme low tide may have influenced the 2007 losses. Data from the eelgrass monitoring project are provided to DNR and are available on the Island County Marine Resource Committee&rsquo;s Sound IQ data system (www.iqmap.org/icSound- IQ/). These data on eelgrass, combined with other data on birds and mammals, intertidal habitats, fish distribution, and more are contributing to the overall understanding of the nearshore ecosystem around Whidbey Island.</p>
