				<div class="SOS_localheader">BRIDGE OVER STILLWATERS<br />
						CARPENTER CREEK ESTUARY RESTORATION</div>
				<p><img src="images/SOS2012_carpenter1.jpg" width="430" height="246" /></p>
<p>Situated at a critical crossroads for migrating salmon, Carpenter Creek estuary is the last significant functioning estuary before leaving Puget Sound. Several small streams, including Carpenter Creek, drain into the estuary and then to the Puget Sound through Appletree Cove near Kingston, Washington.</p>
				<p>Decades ago, the construction of several road embankments on South Kingston and West Kingston Roads fractured this important estuary because the culverts constructed through these road embankments were too small to allow of the natural ebb and flow of the tides. This restricted the flow of water between the salt marsh and estuary. As a result, significant portions of the marsh were filling in with sediment, and freshwater wetland species were encroaching into the upper saltmarsh. The undersized culverts also created barriers for migrating fish that trapped juvenile salmonids at low tide, where they become easy prey.				</p>
				<h3>Importance of Estuaries</h3>
				</p>
				<p>One of the most productive natural habitats in the world, <u><a href="http://www.psp.wa.gov/vitalsigns/estuaries.php">estuaries </a></u>are a cornerstone of the Puget Sound ecosystem. They are vital to the recovery of anadromous fish that migrate from fresh water to salt water and back again to reproduce, including threatened <u><a href="http://www.psp.wa.gov/vitalsigns/salmon.php">Puget Sound Chinook</a></u>, chum, and coho salmon, as well as steelhead and cutthroat trout. Estuaries function as nurseries by providing young fish with shelter, abundant food, and a mix of sea and freshwater that allows them to gradually prepare for the transition from rivers and lakes to the Pacific Ocean. </p>
				<p>Over the last 150 years, Puget Sound has suffered dramatic losses of estuaries and intertidal wetlands. Some estuarine areas, especially those in highly developed areas, have been so heavily modified that restoration may not be feasible. This makes it vitally important to protect and restore estuaries like Carpenter Creek that have not yet been lost.				</p>
				<h3>Restoring Carpenter Creek Estuary</h3>
				<div class="floatleft">
						<p><img src="images/SOS2012_carpenter3.jpg" width="500" height="241" /></p>
				</div>
</p>
				<p>In 2010, the Washington State legislature provided $2.7 million through a state capital budget appropriation to restore Carpenter Creek estuary. Kitsap County used the funds to replace the culvert with a 90-foot single span bridge that restored natural tidal flow to estuary and saltmarsh habitat and allow fish to move into and out of the 30-acre forested estuary habitat. </p>
				<p>Completed in February 2012, the new South Kingston Road Bridge was officially named the Stillwaters Fish Passage in honor of the advocacy work of <u><a href="#stillwaters">Stillwaters Environmental Education Center</a></u>. The restoration of Carpenter Creek estuary would not have happened without the leadership and perseverance of Stillwaters Environmental Education Center staff and volunteers, who have been working nonstop since its founding to make the project a reality despite funding problems and numerous delays.</p>
				<p>Since 2001, Stillwaters Environmental Education Center staff and volunteers have been pushing for the Carpenter Creek Estuary to receive local, state, and federal restoration and protection attention and resources. The first major success was a $600,000 federal feasibility study and environmental assessment on the estuary restoration. Together with Kitsap County and other local restoration advocates, they successfully pursued and were awarded $2.8 million in state legislative funds to replace the restrictive South Kingston Road culvert. </p>
				
						<h3><a name="stillwaters">Stillwaters Environmental Education Center</a></h3>
						<div class="floatright">
								<p><img src="images/SOS2012_carpenter2.jpg" width="250" height="183" /></p></div>
				
				<p>In 1999, a group of local citizens created the Stillwaters Environmental Education Center to educate adults and children about wetlands and water quality and to mobilize citizens and agencies to restore and protect the Carpenter Creek estuary. </p>
				<p>Located in the Carpenter Creek watershed, the center uses its nine acres of wetlands to connect people to the natural environment through hands-on experiences. Today the Center&rsquo;s events have become a North Kitsap tradition. Its programs include nature camps, classes, and a variety of habitat assessment, data collection, and monitoring projects. </p>
				
<h3><a name="projectHistory">Project History</a></h3></h3>
				
				<p>In 2001 Stillwaters staff and volunteers began organizing tours of the Carpenter Creek watershed so people could see its natural wetland habitats for themselves. In addition to local citizens and stewardship organizations, Stillwaters invited staff from Washington Department of Fish and Wildlife, Suquamish Tribe Fisheries, Kitsap County Department of Natural Resources, and the US Army Corps of Engineers.</p>
				<p>That same year, Stillwaters started a letter writing campaign that prompted Kitsap County, Suquamish Tribe, Washington Department of Fish and Wildlife, and private individual landowners to write to the Army Corps of Engineers to request the Corps to use its authority to carry out the culvert removal and bridge construction to restore Carpenter Creek estuary. Based on the strong show of support for the project, the Corps approved funds to begin the Preliminary Restoration Plan process. </p>
				<p>In 2002 the Preliminary Restoration Plan was completed. Kitsap County was awarded matching funds from a Salmon Recovery Funding Board grant to complete non-federal portions of the project. Stillwaters, Washington Department of Fish and Wildlife, and Kitsap County began working on preconstruction monitoring and survey milestones, including collecting historical land use and tidal inundation data. By 2003, the environmental assessment and restoration design was well underway.</p>
				<p>In 2004, progress on the project was delayed when Army Corps of Engineers informed Kitsap County that it could no longer financially support the Carpenter Creek Ecosystem Restoration Project due to federal funding constraints. Stillwaters began another coordinated letter writing campaign to Congressman Norm Dicks and Jay Inslee, Senators Maria Cantwell and Patty Murray, and US Army Corps of Engineers Colonel Debra Lewis. Kitsap County Commissioner Chris Endresen sent letters to Congress requesting it authorize more money for restoration projects nationwide or at least give Corps enough money to keep the project moving forward. The letters and personal contacts resulted in in $150,000 earmarked for Carpenter Creek for 2005.  </p>
				<p>In 2006, the Army Corps of Engineers completed its feasibility study and environmental assessment for Carpenter Creek Estuary, but additional project planning revealed a substantial funding shortfall endangering the project&rsquo;s viability, despite measurable progress in design and permitting. Stillwaters staff emailed, wrote, and phoned senators to request the funds needed to complete the design. </p>
				<p>The Washington State legislature earmarked the Estuary and Salmon Restoration Program to fully fund the remainder of phase 1 of the Carpenter Creek estuary project in 2007. However, in 2008, federal appropriations necessary to fund the Corps of Engineers were insufficient and project planning was suspended.  </p>
				<p>Finally in 2009, the Corps of Engineers received authority to proceed with Carpenter Creek planning and the design work was completed. However, by then the balance of the matching funds received from the Salmon Recovery Funding Board and the Washington Estuary and Salmon Restoration Program had expired. </p>
				<p>In 2010, with the support of the Puget Sound Partnership, Governor Chris Gregoire included funds for Carpenter Creek estuary restoration in a state capital budget appropriation. Construction began in June 2011 and was completed in February 2012. </p>
				<p>Stillwaters staff and volunteers continue to advocate on behalf of Carpenter Creek. Together with Kitsap County they are now seeking a grant to replace the other restrictive culvert located at West Kingston Road, which is blocking exchange between the restored main estuary and the mouth of Carpenter Creek at its intertidal zone. Replacing the second culvert would be the second and final step in completing Carpenter Creek Estuary restoration.</p>
				<p>To fully protect Carpenter Creek Estuary, it is necessary to preserve the properties surrounding it. This project proposes to place the majority of the estuary and its surrounding undeveloped, forested shorelines, salt marshes, freshwater wetlands and riparian habitat under conservation easement or fee title ownership. </p>
