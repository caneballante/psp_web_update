				<div class="SOS_localheader">How Much Is Water Worth?

<br />
The Nisqually and Snohomish Watershed Services Transaction Project</div>
				
				<p class="floatleft">&nbsp;</p>
				<p>Two Washington state watersheds – the Nisqually and Snohomish - have been credited with protecting and restoring the largest amount of habitat in Puget Sound to date.  Now these watersheds have been selected as the most likely candidates for an innovative strategy to keep working forests in the State of Washington from being converted to non-forest uses.  How?  By getting potential buyers, such as utilities, flood districts or tribal nations, to pay forest landowners to undertake specific land management activities that achieve measurable improvements in watershed services and enhance water quality, increase water supplies and improve salmon habitat protection.  </p>
				<p>The Watershed Services Transaction Project was launched in June 2011 by the State Department of Natural Resources in collaboration with the University of Washington Northwest Environmental Forum.  After extensive deliberation during the Forums held in 2010 and 2011, the Snohomish and Nisqually watersheds were identified as the most likely pilot locations for watershed services transactions, primarily because critical organizations presented themselves to lead the projects.  </p>
				<p><img src="images/watertype1.jpg" width="700" height="541" /></p>
				<p><img src="images/watertype2.jpg" width="700" height="541" /></p>
				<p>Forested watersheds provide almost two-thirds of the drinking water in the United States. Many other critical services, such as timber, flood control, habitat for animals and birds, carbon sequestration and recreation, are dependent on forests, but we too often assume that forest landowners  will continue to manage their lands to realize all of these values and that they do not need to be compensated.  </p>
				<p>A few locations around the country are developing comprehensive valuations of the benefits provided by forests, and creating incentives for private landowners to manage their forests for these diverse public values.  &ldquo;Payments for watershed services&rdquo; is an approach that has been implemented successfully in a few communities, and is now being considered in Washington.  </p>
				<p>The Snohomish River Basin pilot project addresses the second largest drainage in Puget Sound.  Seventy four per cent of the drainage is forest land.  The basin is also one of the fastest growing areas in the region, and it&rsquo;s critical to balance the area&rsquo;s growth needs with maintaining a healthy ecosystem.  A recent study valued the potential benefits provided in the watershed to range from a low of $383.1 million to a high of $5.2 billion.  Snohomish County Department of Public Works is leading this demonstration transaction, joined by several key watershed partners, including the Tulalip Tribes, Forterra, King County and Washington DNR.  </p>
				<p>The Nisqually pilot project focuses on the Nisqually watershed, which encompasses 78 miles of habitat, from the Nisqually Glacier on Mount Rainier to the delta in the Nisqually National Wildlife Refuge.  The watershed has a range of land uses, including rural communities; parks, such as Mt Rainier; hydropower projects; military bases and the Nisqually Indian Reservation.  The communities, tribes and organizations in the watershed have worked together to conserve, restore and protect habitat in the basin.   The Nisqually River Council, Nisqually Land Trust and Northwest Natural Resource Group are spearheading the watershed services pilot project.  As in the Snohomish project, their focus will be to provide a demonstration transaction and deliver new sources of income to forest landowners that help them offset the costs of new practices that improve water quality and quantity.  </p>
				<p>The pilot projects are intended to benefit the individual watersheds, and also provide an effective and transferable model for a state or perhaps national watershed services program.   A successful Watershed Services Transaction Project in these two locations can lead the way to address future water supply and water flow needs and create a new financing mechanism for restoration and recovery of the Puget Sound and to sustain Washington&rsquo;s valuable private forest lands.  </p>
