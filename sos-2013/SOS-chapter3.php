						<p>The purpose of this chapter is to report on the status of the ecosystem and progress toward establishing a healthy Puget Sound. To provide a way to measure ecosystem health, a number of indicators were formally adopted by the Puget Sound Partnership (PSP) Leadership Council in 2010 and 2011 as surrogates of the status of the ecosystem. The Leadership Council also adopted qualitative and numerical goals for a healthy Puget Sound, defined by the ecosystem recovery targets to reach by the year 2020. The indicators and the 2020 targets are collectively referred to as Puget Sound Vital Signs and are used to track and communicate progress in recovering the health of Puget Sound.</p>
