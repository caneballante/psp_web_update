		<p>Puget Sound was once home to more populations of Chinook and other salmon with a greater diversity of traits than what exists today. Only 22 of at least 37 historic Chinook populations remain. The remaining Chinook salmon are at only 10% of their historic numbers, with some down lower than 1% of their historic numbers. The decline in salmon is closely associated with the decline in the health of Puget Sound and therefore requires a coordinated, ecosystem-wide restoration effort. </p>
		
<p>
		Salmon recovery is guided by implementation of the Puget Sound Salmon Recovery Plan, adopted by the National Oceanic and Atmospheric Administration (NOAA) in January 2007. This recovery plan was developed by Shared Strategy, a grassroots collaborative effort to protect and restore salmon runs across Puget Sound. Shared Strategy engaged local citizens, tribes, technical experts and policy makers to build a practical, cost-effective recovery plan endorsed by the people living and working in the watersheds of Puget Sound. See the <a href="http://www.sharedsalmonstrategy.org">Shared Strategy website</a> for additional information on the creation and materials of the planning process. </p>
		
 <p>
 		Salmon recovery actions occur at both the watershed and regional scale. Each watershed has a unique set of priorities, strategies, and actions directing recovery, which are updated by their local policy and technical groups. Representative leaders from each of the 14 watershed areas in the Plan also meet as a regional body to provide strategic input for Plan implementation. At the regional scale, there is also an overarching set of priorities, strategies, and actions directing recovery. A group of policy decision-makers, called the Puget Sound Salmon Recovery Council, meets regularly to develop guidance for implementation of the Plan and advises the <a href="LC_about.php">Leadership Council </a>on salmon recovery decisions. This group consists of representatives from each of the 14 watershed areas, the environmental and business community, indian tribes, and state and federal agencies involved in salmon recovery. Additionally, a NOAA-appointed Regional Implementation Technical Team (RITT) works with the regional and local groups to provide technical review and guidance for recovery. </p>
		
<h3>What is the Puget Sound Partnership Doing to Recover Salmon?</h3>
		<p>
				On January 1, 2008, The Puget Sound Partnership Act, Section 49(3), RCW 77.85.090(3) designated the Partnership to serve as the regional salmon recovery organization for Puget Sound salmon species, except Hood Canal Summer Chum. This legislation provided for a timely and thoughtful transition of the regional salmon responsibilities from Shared Strategy, the organization that coordinated development of the salmon recovery plan, to the Partnership as the organization that will help implement the plan.</p>
		
 <p>
 		As the regional salmon recovery entity, the Partnership inherits a community-based, scientifically rigorous, and regionally significant recovery plan that was built through a multi-year stakeholder process. Using the Puget Sound Salmon Recovery Plan as a guide, the Partnership is working with local stakeholders and communities, indian tribes, businesses, and state and federal agencies to identify, sequence, prioritize, and implement projects and programs to recover salmon. Projects and programs include:
		<ul>
				<li>Protecting and restoring habitat,</li>
           		<li>Raising public awareness,</li>
           		<li>Reforming hatchery management,</li>
          		<li>Assuring integration of harvest practices, and</li>
           		<li>Developing a monitoring and adaptive management strategy to help track and assess efforts to recover salmon in Puget Sound.</li> </p>
		</ul>
		<p>
				Salmon recovery and Sound recovery must go hand in hand. Salmon recovery work in the watersheds that make up Puget Sound is the cornerstone of broader Puget Sound recovery efforts. The Partnership is working with watershed groups, which contribute creativity, knowledge, and motivation to implementing lasting solutions to the complex challenges facing salmon and Puget Sound. </p> 
