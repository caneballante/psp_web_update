			<p>In fall 2019, the Puget Sound Partnership (Partnership) conducted an after action review of the 2018-2022 Action Agenda. The goal of the Partnership in conducting the after action review was to seek feedback on the <a href="https://pspwa.app.box.com/s/osxaeqg19fevxu5n3k8xnjytzkwol512">2018-2022 Action Agenda</a> and the process to develop it that will help inform our collective approach to the 2022-2026 Action Agenda. The Partnership has reviewed the responses and is pleased to share <a href="https://pspwa.box.com/s/71kh95t3vx5ltu8931nntd0jdvpqk41k">a report</a> covering the key findings of the after action review. We believe that the feedback we have received provides important guidance in our effort to develop a comprehensive, meaningful, and useful Action Agenda that will help advance Puget Sound recovery. Thank you to all who provided input during the after action review and to all who will continue to contribute to developing, improving, and using the 2022-2026 Action Agenda. </p>
		  <p>Based on the findings of the after action review and other feedback, the Partnership and partners are beginning the effort to develop the 2022-2026 Action Agenda in the coming months. Please look for continued opportunities to engage in the development process by attending <a href="https://psp.wa.gov/partnership-boards-overview.php">board meetings</a>, or by engaging with our <a href="https://psp.wa.gov/LIO-overview.php">local</a>, <a href="https://pugetsoundestuary.wa.gov/about/contact-us/">state</a>, or <a href="https://nwtreatytribes.org/">tribal</a> partners.</p>
