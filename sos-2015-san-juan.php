		<p>The San Juan LIO has made important strides towards protecting Puget Sound and the San Juan Islands&rsquo; unique ecosystem. The San Juan LIO has 13 Near Term Actions (NTAs), which have addressed critical issues ranging from responding to major oil spills, to mitigating stormwater runoff. Major accomplishments are highlighted below.</p>
		  <h3>NTA SJI1—Responding to Major Oil Spills</h3>
		  <p>The San Juan Islands are vulnerable to oil spills and the San Juan LIO is addressing this threat in a number of ways. This past spring, the Islands Oil Spill Association (IOSA) hosted a multi-day oil spill training in Prevost Harbor. Participants in seven boats practiced pulling a boom into position. In case of an actual oil spill, this training will allow responders to direct oil towards shoreline collection points. In addition, LIO members have held meetings with the Islands Trust in British Columbia to update their agreement to include wildlife and natural resource damage assessment and restoration.</p>
  <h3>NTA SJI2—Responses to Increased Vessel Traffic</h3>
		  <p>Large-scale shipping traffic has increased in the San Juan LIO&rsquo;s Action Area, which has compounded the risk of oil spills. To address this issue, the LIO hosted a successful Marine Managers Workshop in Friday Harbor. Sixty-three participants joined this workshop to address the theme of &ldquo;Charting a Course Forward in the Salish Sea: Understanding risks to San Juan County and the Gulf Islands from Increased Vessel Traffic.&rdquo; Participants, including representatives from the United States, tribal, and Canadian governments, nonprofits, and communities, discussed the potential threats of vessel traffic in the Salish Sea.</p>
		  <p>The San Juan LIO has also addressed the risks associated with increased international shipping by completing a feasibility assessment to designate the Salish Sea as a Particularly Sensitive Sea Area (PSSA) under the International Maritime Organization (IMO). A PSSA designation could help the Salish Sea receive extra protection, beyond the scope of the Coast Guard or federal laws. PSSAs have been set up in 14 areas worldwide. The San Juan LIO is looking to develop the PSSA nomination paper with input from stakeholders throughout coastal communities in Washington and British Columbia in 2016-2017. Recently, Friends of the San Juans was awarded a $100,000 grant from the Washington Women&rsquo;s Foundation to continue working on this effort.</p>
  <h3>NTA SJI5—Control and Mitigate Stormwater Runoff</h3>
		  <p>To protect Puget Sound from polluted stormwater runoff, the Town of Friday Harbor hired an engineering firm to construct a waterfront stormwater vault containing cartridge filters that are approved by the Department of Ecology. The Town of Friday Harbor is on plan for completing this vault by the end of 2016.</p>
  <h3>NTA SJI6—Implement the Onsite Sewage System Operation and Maintenance Program Plan</h3>
		  <p>In order to protect groundwater, marine water, and freshwater, property owners in San Juan County are required to have their on-site sewage systems inspected on a periodic basis. San Juan County has worked to ensure that 66 percent of onsite sewage systems in Sensitive Areas have a current inspection. The County is On Plan to complete 100 percent of inspections by the end of the 2014-2016 NTA cycle.</p>
  <h3>NTA SJI7—Stormwater Management Support</h3>
<p>The San Juan LIO is making progress working with residents to improve stormwater management, reduce polluted runoff, and decrease nutrient loading into the marine environment. In 2014, the Surface and Stormwater Assistance Program (SSWAP, which includes San Juan County Public Works and Health Departments, Washington State University, San Juan Islands Conservation District, and the Town of Friday Harbor) published six articles regarding best management practices (BMPs) and funding sources for implementation for San Juan County homeowners. To further educate homeowners about this issue, the San Juan County Department of Community Development is updating a homeowner information packet. The San Juan Islands Conservation District has provided technical and financial assistance to landowners, completing 15 farm management plans and providing cost-share funding to implement 30 BMPs. The Conservation District, in partnership with San Juan County Public Works, has also provided education and outreach to 350 residents through watershed tours, the county fair, and farmer&rsquo;s markets.</p>
