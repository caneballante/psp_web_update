<h2>Process for Setting Targets<a name="process" id="process"></a></h2>
		<p>The Partnership’s target-setting work is supported by Partnership staff, "Indicator Champions" from the different government agencies and tribes, and "interdisciplinary teams" of subject matter experts. For a list of people involved in the technical work to support target-setting, <a href="http://www.mypugetsound.net/index.php?option=com_community&amp;view=groups&amp;task=viewgroup&amp;groupid=57&amp;Itemid=222">click here</a>.</p>
		<p>As a part of target setting, the Partnership seeks guidance from the Leadership Council, the Ecosystem Coordination Board (ECB), and the Science Panel. In addition, the Partnership gathers stakeholder perspectives on possible targets through workshops and other forums.</p>
