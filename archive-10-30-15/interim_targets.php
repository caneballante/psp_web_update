<h2>Role of Science <a name="science" id="science"></a></h2>
<p>In setting targets, one important scientific consideration is the amount of time required for the ecosystem to respond to our actions. For example, the recovery of orca or salmon populations will likely not be realized until well beyond 2020 even under the best possible actions. </p>

<p>Consequently, the Partnership's performance targets will describe the desired status for 2020, which may be different from the ultimate desired status beyond 2020.  </p>
<p>To the extent possible, science advisors will describe relationships among indicators to ensure consideration of how suites of ecosystem targets work together to represent a functioning ecosystem.  </p>
<p>Finally, scientists will consider the uncertainty in the data and information. For example, differences in the availability and quality of information or scientific models means that the targets will be uneven in how certain we are that they represent the desired self-sustaining, healthy state or in the policy consensus about the targets. </p>
