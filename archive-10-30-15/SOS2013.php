						<div class="SOS_localheader">What is the State of the Sound?</div>
						<p>The 2013 State of the Sound is the Puget Sound Partnership’s third report to the Legislature on progress toward the recovery of Puget Sound by 2020.  The 2013 State of the Sound builds upon the information presented in the 2012 State of the Sound, which described this complex ecosystem as a patient in “serious condition.” Our objective for this year's report was to update that assessment, recognizing that with a short, 12-month period between the 2012 and 2013 reports, we can expect relatively little change in recovery trajectory and pace. </p>
<p>
The Partnership works closely with its many partners to coordinate the recovery and long-term protection of the ecosystem. This collaborative effort includes government agencies, tribes, business and private sector interest groups, non-governmental organizations, and citizens, who work together to plan, evaluate, and continually improve efforts to meet our collective vision of a healthy Puget Sound.</p>
