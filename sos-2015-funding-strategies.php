        <p>This table includes the eight overarching recommendations identified in the Ecosystem Coordination Board’s funding strategy report, as well as a summary of the efforts to implement those recommendations as of October 2015.</p>
		<table width="800" border="0" class="table padding-10-all">
				<tbody>
					<tr>

						<td width="400"><h6>Funding recommendations</h6></td>
						<td width="400"><h6>Progress</h6></td>
					</tr>
					<tr>
						<td><p>1. <strong>Support the continuation of federal and state funding sources that currently fund the implementation of the three Strategic Initiatives and the Action Agenda, with particular emphasis on funding the Habitat Initiative.</strong> Federal sources include the Environmental Protection Agency’s Geographic Programs and National Estuary Program, the National Oceanic and Atmospheric Administration’s Pacific Coastal Salmon Recovery Fund. </p>

<p>State funding sources include Puget Sound Acquisition and Restoration, Estuary and Salmon Restoration Program, Floodplains by Design, Salmon Recovery Funding Board grant programs, and Department of Ecology water quality grants and loan programs.</p> 

<p>The recommendations stress the need to fund habitat activities as the gap in that area is larger relative to shellfish and stormwater related actions.  </p> 
							</td>
						<td><p>In the 2015 Legislative Session, Partnership staff, and board members met with individual legislators to personally explain how adequately funding habitat restoration provides multiple benefits to people, as well as to Puget Sound health. Through one-on-one conversations, support was increased for funding habitat recovery programs such as the Puget Sound Acquisition and Restoration fund, Floodplains by Design, and the Estuary and Salmon Restoration Program. (See Appendix 3.) Notwithstanding these efforts, the amounts allocated through the State budget process are not and have not historically been adequate to make enough progress on implementation of the Action Agenda to achieve the Puget Sound recovery goals established by the Legislature.</p></td>
					</tr>
					<tr>
						
						<td><p>2. <strong>Support legislative approval of funding for an appropriate,
integrated water infrastructure package.</strong> Seek adoption of a watershed approach similar to what has been used in the salmon recovery efforts.</p></td>
						<td><p>In the 2015 Legislative Session, the Partnership testified about some of the needs addressed by SB 5628, the Washington Waters Act, which would have provided a comprehensive financing package for stormwater management, floodplain management, and water storage projects. Sponsored by Sen. Honeyford, the bill failed to move out of the Senate Ways & Means Committee. However, a bipartisan House of Representatives task force was created to develop recommendations related to the intent of this bill. The bill or similar legislation may be considered again in the 2016 Legislative Session. </p></td>
					</tr>
					<tr>
						
						<td><p>3. <strong>Support legislative approval of funding for the Department of Health’s septic loan and septic management program initiatives.</strong> Funding of the loan program and the septic management program would address all of the funding needs in the Shellfish Initiative as it is currently scoped.</p></td>
						<td><p>In the 2015 Legislative Session, the Partnership testified in favor of HB 1715, which would have helped to reduce fecal contamination of Puget Sound by providing financing to develop onsite sewage management plans. The bill failed to pass after an amendment removed the revenue source for the proposed program, but it may be taken up again in the 2016 Legislative Session.</p></td>
					</tr>
                    <tr>
						
						<td><p>4. <strong>Seek increased funding for stormwater and other environmental improvements related to the state highway system in a state transportation package,</strong> as well as further alignment between environmental spending for highways and watershed and regional priorities for cleanup and restoration. </p>

<p>Synchronize spending on highway stormwater and environmental needs with watershed planning to ensure that investments are consistent with watershed cleanup and restoration priorities.</p></td>
						<td><p>2ESHB 1299 passed the Legislature on June 11, 2015, to allocate transportation funding. The bill includes investment in Puget Sound recovery infrastructure through two crucial Department of Transportation programs: </p> 
<ul><li>Retrofits to stormwater infrastructure to help reduce pollution from stormwater runoff will receive $2.7 million.</li> 
<li>Retrofits of fish passage barriers, such as culverts, $88.3 million ($75.5 million for Puget Sound).</li></ul></td>
					</tr>
                    <tr>
						
						<td><p>5. <strong>Advocate for additional state funding for stormwater projects</strong> and support funding for high-efficiency street sweeping, removal of legacy sediment loads, and selective highway retrofits as immediate priorities, while continuing work on a long-term strategy for stormwater investments in the Puget Sound basin. </p>

<p>Focus on maintaining a dedicated funding source for stormwater grants of at least $100 million per biennium for grants and specifically identified projects, maintaining flexibility in the types of project funding, and supporting stormwater needs in the development of the water infrastructure package. Pursue state funding for a study to identify a long-term strategy for stormwater improvements in the region, particularly for retrofitting of older developments and infrastructure.</p></td>
						<td><p>The Washington Waters Act described above in relation to recommendation 2 would have provided funding for stormwater management projects.</p>
<p>Ecology’s request for stormwater financial assistance was the top priority among all 2015 agency budget requests ranked by the Partnership for 2015-17. Compared to the Governor’s request of $74 million, the final enacted budget was $53 million, including almost $32 million for Puget Sound.</p></td>
					</tr>
                    <tr>
						
						<td><p>6. <strong>Advocate for strategic prioritization of federal and state 
infrastructure funding</strong> based on economies of scale, advancement of the science, equity and social justice, agriculture and resource land protection, and workforce development.</p></td>
						<td><p>The Partnership has been working with federal and state partners to advocate for coordinated investment to accelerate the pace and scale of ecosystem recovery. The coordinated investment model uses a collaborative, multiple-benefits approach to floodplain investments that delivers substantive flood risk reduction in conjunction with habitat restoration, water quality gains, and other community improvements. The 2015-17 biennial budget awarded $35.5 million to the Floodplains by Design coordinated investment program.  Also, the Partnership is working closely with the state’s congressional delegation on a bill that would help align federal investments with the Action Agenda’s goals and priorities.</p></td>
					</tr>
                    <tr>
						
						<td><p>7. <strong>Consider options for collection and distribution of funds across jurisdictional boundaries</strong> at a watershed, multi-watershed, or Sound-wide scale to address differences in funding capacity among local governments in the region. </p>

<p>Consider reviving the concept of a regional funding district (initially proposed by Bill Ruckelshaus in 2008). Seek authority in the state Legislature to establish multi-jurisdictional and multi-county districts with wide-ranging revenue options to address water and habitat needs consistent with Action Agenda priorities. </p></td>
						<td><p>Partnership staff participated in a workshop hosted by Sen. McCoy to consider a concept for legislation that would provide for these kinds of funding districts.  The concept is still under consideration, but has not been advanced as a bill.  </p></td>
					</tr>
                    <tr>
						
						<td><p>8. <strong>Review and revise the Partnership’s funding strategy</strong> during the biannual updates of the Action Agenda.</p></td>
						<td><p>The Partnership is working to develop an approach to updating the funding strategy for the 2016 Action Agenda.</p></td>
					</tr>
