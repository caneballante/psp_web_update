			<p>Five years ago today I joined an organization that was struggling against a herculean challenge—to chart a course for the recovery of the nation&rsquo;s largest estuary and the state&rsquo;s cherished jewel—the Puget Sound. Since then, we have accomplished great things together. It is with gratitude for the opportunity provided to me by Governor Inslee, intense pride about our accomplishments, and optimism for the future, that I share with you that I have tendered my resignation, effective May 3.</p>
				<p>The achievements of our agency and our partners during my tenure have been remarkable. A galvanized public has brought critical support to the fight against the extinction of our state&rsquo;s official marine mammal, the <a href="http://www.psp.wa.gov/psdoth2018.php">Southern Resident orca</a>. Projects restoring thousands of acres of habitat are now coming to fruition across the Sound. The <a href="http://www.psp.wa.gov/psdoth2018.php">Puget Sound Day on The Hill</a> trip has strengthened our collective voice with decision-makers in Washington, D.C. Regionally, our relationships are stronger than ever with our federal partners, with whom we worked closely on the creation of the Puget Sound Federal Task Force. By focusing on aligning federal, tribal, and state priorities, this effort increases the impact of investments by all these partners. Within weeks, a revised <a href="https://dennyheck.house.gov/media-center/in-the-news/editorial-puget-sos-uniting-efforts-to-recover-puget-sound">PUGET SOS</a> (Promoting United Government Efforts to Save Our Sound) bill may be introduced in Congress, aiming to bring new resources to support our work. Puget Sound recovery is now the national priority it must be. </p>
				<p>All of this work has been done with the engagement and input of hundreds of partners—tribes, elected officials, our boards, government leaders, non-profits, businesses, scientists, academic institutions, our sister state agencies, agriculture, aquaculture and other industries, as well as interested individuals—ensuring that the necessary course forward, as described in the <a href="http://psp.wa.gov/action_agenda_center.php">Puget Sound Action Agenda</a>, is not solely the voice of the Partnership, but rather the collective will of all who make the decision to be a partner.  </p>
				<p>The organization itself has evolved significantly over the last five years, from an agency with an uncertain future, to one sharply focused on adding value to the recovery system. We concentrate our limited resources on functions that create better outcomes than the sum of the parts. We align, catalyze, and advocate to ensure the success of our partners. The role of science in the decision-making and planning processes has been fortified and focused on outcomes and continuous learning. The Partnership&rsquo;s internal operations are now systematic, transparent, and informed by best management practices. These professional management systems have allowed us to achieve two &ldquo;clean&rdquo; financial audits. The last <a href="http://psp.wa.gov/sos.php">State of the Sound</a> report was nominated for a national award for public communication. From across the country and the world, other recovery efforts are using our learning to inform their practices.</p>
				<p>Looking forward, the work of recovery and resiliency still faces formidable challenges, including the pressures from increasing human populations and climate change. Even though we are now well positioned for success, having built a thoughtful, science-driven recovery management system, the magnitude of results will still be commensurate with the magnitude of investment. Only 30 percent of the specific actions proposed in the last Action Agenda were implemented in the last cycle. The single greatest barrier to implementation was the lack of funding. To reverse the degradation of the natural systems on which we all depend, we must increase investment for implementing the Near Term Actions in the Action Agenda. The path forward must also include a strong focus on supporting innovation focused on solving our water-related challenges. This includes incentivizing and supporting the private sector to work with our public institutions for research, development, and deployment of innovative solutions to solve not only public challenges such as stormwater pollution, but also to help transition critical Washington businesses and sectors, such as agriculture, maritime, and aerospace, to profitable and sustainable practices.</p>
			<p>On a more personal note, it has been an honor to work alongside so many people who engage unfailingly in the silent and consequential battles to protect our region. The relentless dedication of the Puget Sound tribes to pass to their children a better world, one that is enriched by their languages, cultures, and traditional knowledge, sets an example for us all. Last, but also most present in my mind at this moment, is how much I will miss working with my colleagues at the Partnership. This small but mighty group personifies the best in public service: a resilient, intellectually curious, diverse, and courageous group who direct their passions into improving their communities. Because of them, our organization is now strong, with excellent leaders, and staff whose commitment and promise is second to none. I can&rsquo;t wait to see all they will accomplish.</p>
