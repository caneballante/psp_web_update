			<p class="padding-10-top">The environmental justice law (SB 5141) known as the <a href="https://app.leg.wa.gov/RCW/default.aspx?cite=70A.02">Healthy Environment  for All Act</a> (HEAL), passed in 2021. Chapter 70A.02 RCW provides a  roadmap for integrating environmental justice into state agencies. The law  requires seven state agencies  <a href="https://app.leg.wa.gov/RCW/default.aspx?cite=70A.02.050">to create and adopt  community engagement plans</a> by July 1, 2022. For this and other parts  of HEAL, agencies work closely with the <a href="https://waportal.org/partners/home/environmental-justice-council">Environmental  Justice Council</a> (EJ Council), which consists of members  appointed by the Governor. HEAL states that the EJ Council will  provide guidance on community engagement plans as agencies create and update  them. HEAL also requires that each covered agency consider the guidance  developed by the EJ Council.  See the <a href="https://waportal.org/partners/home/environmental-justice-council">Environmental  Justice Council website</a> for more details on the role of the EJ  Council.</p>
			<p>HEAL directed the Department of Health to convene the first  meeting of the EJ Council by January 1, 2022. However, the EJ Council was not  fully appointed and seated until March 2022. Because of those delays, April 4, 2022,  was the earliest practical date the EJ Council could meet. HEAL also created an  Interagency Workgroup to coordinate work among the seven HEAL agencies and  others that decide to follow its guidelines. The Interagency Workgroup began  drafting the guide for community engagement plans in January 2022 while  awaiting seating of the EJ Council. </p>
			<p>The EJ Council has not reviewed the current versions of the  interagency DRAFT community engagement guide and DRAFT agency-specific  community engagement plans.</p>
			<p>The Puget Sound Partnership is committed to a strong  partnership with the EJ Council as we integrate environmental justice into  agency activities. This work is led by the Equity and Environmental Justice  Team and implemented agency-wide. The Partnership&rsquo;s Executive Director Laura Bradstreet is committed to her role as an Ex Officio member of the EJ Council.  We are mindful of our duty to the legislature and people of Washington to adopt  a community engagement plan by July 1, 2022. </p>
			<p>In balancing these interests and obligations, our agency is  adopting the interagency DRAFT community engagement guide as our provisional  community engagement plan. Our future work to tailor this plan to  agency-specific activities and implement it will be dependent on coordination  with the EJ Council and will incorporate guidance from the EJ Council and  communities across Washington state, and will include tribal consultation.  The Partnership will actively seek to engage and  consult with tribal nations as we develop our complementary Tribal Engagement  plan.<u> </u></p>
			<p>Following adoption of the provisional community engagement  plan, our agency and the other agencies named in HEAL will incorporate guidance  from the EJ Council.  Our agency will  collaborate with the Interagency Workgroup and the EJ Council to hold community  listening and feedback sessions to co-author needed changes to the provisional  community engagement plans. Agencies will also conduct tribal consultation. Our  agency will regularly partner with the EJ Council, community, and tribes over  the coming years to update our community engagement plan. The community  engagement plan is a living document that will evolve as our agency builds a relationship  with the EJ Council and Washingtonians as environmental justice is implemented  across state agencies.</p>
			
