          <li>Developing an implementation strategy for the newly adopted Shoreline Master Plan compliance, including streamlining permitting to be consistent with protection and recovery targets.</li>
          <li>Restoring tidal inundation in valuable salmon rearing habitat.</li>
          <li>Removing shoreline armoring (bulkhead) to improve nearshore habitat for salmon and forage fish.</li>
          <li>Developing water quality improvement projects.</li>
          <li>Managing invasive species in the nearshore and inland. </li>
