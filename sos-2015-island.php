		<p>The Island Local Integrating Organization (ILIO) FY2014 NTA development process increased collaboration and alignment on shared priorities in the watershed. This proved to be a successful approach to local Action Agenda planning and implementation by enabling integration of local watershed recovery and protection plans (e.g. the Shoreline Master Plan) and fostering effective, cross-jurisdictional partnerships to apply grant dollars to the highest-priority recovery projects in the watershed. Through this process, ILIO partners developed a refined list of priority actions to address ecosystem protection and recovery gaps in existing watershed projects and programs. Twelve NTAs were included in the 2014 Action Agenda update, and sixteen projects were implemented to address these priority local ecosystem recovery efforts and in support of the three Action Agenda strategic initiatives. Of the 16 projects implemented, seven were linked directly to strategic initiative sub-strategies. </p>
