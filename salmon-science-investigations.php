					<tbody>
					<tr>
						<td><p class="psemp-mar-font">Modeling population-level impacts from Contaminants of Emerging Concern exposures to endangered salmonids</p></td>
						<td><p class="psemp-mar-font">Contaminants of Emerging Concern (CECs) are a group of anthropogenic chemicals that occur in the environment, have the potential to cause harm to exposed organisms, and are poorly regulated. The objective of this proposed project is to develop and apply a modeling framework that will evaluate the population-level impacts of CEC exposures on Chinook salmon.  A population matrix model will be developed to analyze how exposures to CECs under different scenarios affects population outcomes and stable age distributions. The models will be applied to a highly impacted system, the Puyallup River, and a lightly impacted system, the Nisqually. </p></td>
						<td><p class="psemp-mar-font">Andy James, University of Washington and Ruth Sofield, Western Washington University </p></td>
						
					</tr>
					<tr>
						<td><p class="psemp-mar-font">Impacts of spring storms on juvenile salmon health in an urbanized watershed</p></td>
						<td><p class="psemp-mar-font">Adult coho salmon spawning in urban-impacted streams have been documented to die prematurely due to inputs of untreated roadway runoff and exposures to 6PPD-quinone - a product found in tires. However, the mortality of juvenile coho from exposures to runoff in streams has not been studied. This project includes field exposures, water quality sampling, and laboratory exposure studies to better understand the risks of runoff for juvenile coho, using Miller Creek as a representative system. The results will provide a first assessment of the acute risks of degraded water quality to juvenile coho in runoff-impacted streams and an understanding of the relative role of various tire- derived contaminants.</p></td>
						<td><p class="psemp-mar-font">Jen McIntyre, Washington State University; John Hansen, U.S. Geological Survey; and Ed Kolodziej, University of Washington</p></td>
						
					</tr>
					<tr>
						<td><p class="psemp-mar-font">Evaluation of experimental hatchery rearing strategies to benefit Chinook salmon in Puget Sound</p></td>
						<td><p class="psemp-mar-font">In Washington State, approximately 80% of returning adult Chinook are of hatchery origin. As a result, the near-term health of salmon-dependent ecosystems, fisheries, and Southern Resident Killer Whales rely on Puget Sound hatcheries. Building from Salish Sea Marine Survival Project findings, seven hatcheries in Puget Sound launched experiments to evaluate the impact of variable rearing and release strategies on size-at-age, age-at-maturity, and marine survival of Chinook salmon. The project is collecting and analyzing data from these ongoing experiments to lay the groundwork for a final synthesis report in 2027 that will include an evaluation of the various release strategies and guidance on optimizing hatchery management to increase Chinook abundance in Puget Sound.</p></td>
						<td><p class="psemp-mar-font">Liz Duffy, Long Live the Kings</p></td>
						
					</tr>
					<tr>
						<td><p class="psemp-mar-font">Testing cumulative effects of restoration for multiple salmon populations in the Whidbey Basin using causal analysis</p></td>
						<td><p class="psemp-mar-font">This project will use a causal analysis within a Cumulative Effects Evaluation framework to test the hypotheses that (i) cumulative restoration is increasing the availability and structure of intertidal habitats and (ii) juvenile Chinook salmon distributions from multiple river systems benefit from these actions. Multiple lines of evidence will be developed to evaluate the hypotheses including 1) qualitative findings of published literature, 2) quantitative analysis of published studies, 3) spatial analysis, 4) habitat change analysis, 5) statistical analysis of fish monitoring data, and 6) simulation modeling. This study will result in a deeper understanding of the benefits of tidal delta and nearshore restoration for juvenile salmon, where restoration will best benefit multiple populations, and how restoration efforts benefit population and ecosystem recovery in the Whidbey Basin.</p></td>
						<td><p class="psemp-mar-font">Mike LeMoine, Skagit River System Cooperative</p></td>
						
					</tr>
					<tr>
						<td><p class="psemp-mar-font">Using bioenergetics and landscape connectivity to plan effective tidal delta restoration projects for Chinook salmon under a changing climate</p></td>
						<td><p class="psemp-mar-font">This project will develop an analytical framework using hydraulic, landscape connectivity-abundance, and bioenergetic models to compare the abundance and growth potential of juvenile Chinook salmon across a suite of restoration and climate scenarios. The framework addresses two hypotheses: 1) Well placed restoration projects support increased juvenile Chinook salmon use beyond the intended project area by increasing connectivity among sites or creating areas with high growth potential; and 2) Warming water temperatures counteract restoration efforts by increasing the metabolic demand of juvenile Chinook salmon in estuarine deltas, which may be further impacted by poor project placement. Design plans, abundance estimates, and growth potential results will be used to evaluate which recovery actions provide cumulative, broad-scale restoration benefits and remain resilient to future climate change. </p></td>
						<td><p class="psemp-mar-font">Emily Howe, The Nature Conservancy</p></td>
						
					</tr>
					<tr>
						<td><p class="psemp-mar-font">Investigation of sediment contaminant impacts on the growth and survival of juvenile Puget Sound Chinook salmon</p></td>
						<td><p class="psemp-mar-font">To undergo the migration from freshwater to the ocean, salmon must successfully complete the stressful process of smoltification. However, juvenile salmon undergoing smoltification are faced with multiple stressors including exposure to legacy contaminants, contaminants of emerging concern, and altered prey availability. This work will first evaluate how Duwamish River sediment impacts the survival of field-collected polychaetes and amphipods, invertebrate prey species for juvenile Chinook. The invertebrate work will inform laboratory studies addressing how juvenile Chinook health is impacted by three key factors – contaminated prey, reduced food availability, and the physiological stress of smolting. The study results will inform the potential for environmental stressors and chemical exposures to limit the survival of outmigrant Chinook salmon.</p></td>
						<td><p class="psemp-mar-font">John Stark, Washington State University</p></td>
						
					</tr>
