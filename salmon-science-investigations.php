					<tr>
						<td><p class="psemp-mar-font">Evaluating early marine growth and size/condition as an indicator of overall survival in Puget Sound Chinook salmon</p></td>
						<td><p class="psemp-mar-font">Documenting and quantifying relationships between early life history conditions, individual performance, and adult return rates would be advantageous for predicting how residence of juvenile salmon in Puget Sound’s changing ecosystem affects recovery trajectories and fisheries management. The project goal is to determine whether the link between early marine performance and marine survival is best indexed by individual growth or size, independently, or by assessing, size, growth, and the interactions between them to inform survival assessments. It will also evaluate the appropriate spatial and temporal resolution for potential marine survival indicators and ecosystem indicators related to growth.</p></td>
						<td><p class="psemp-mar-font">Josh Chamberlin and Correigh Greene, National Oceanic and Atmospheric Administration</p></td>
						
					</tr>
						<tr>
						<td><p class="psemp-mar-font">Evaluating the effects of experimental hatchery management on hatchery Chinook salmon</p></td>
						<td><p class="psemp-mar-font">The overarching goal of this project is to evaluate the effects of experimental hatchery rearing and release strategies on survival and size- and age-at-return of hatchery Chinook salmon. This research will include sampling, data analysis, experiment coordination efforts, and active communication of findings with regional resource managers through an initial synthesis report. This project is part of a larger, multi-year, collaborative study across tribal, state, and LLTK hatcheries. </p></td>
						<td><p class="psemp-mar-font">Liz Duffy, Long Live the Kings</p></td>
						
					</tr>
					<tr>
						<td><p class="psemp-mar-font">Yearling Chinook salmon habitat use in the Skagit River</p></td>
						<td><p class="psemp-mar-font">This project is analyzing six field seasons of observational data collected on the Skagit River to better understand the specific habitat use for parr and yearling migrant juvenile Chinook salmon. The field data collected for this study will provide insight and quantification of the relationships between stream type juvenile Chinook and habitat preferences during different seasons. This project will provide practical information about habitat use for yearling migrant Chinook salmon to improve and enhance restoration efforts in the Skagit River, and other watersheds with the stream type life history pattern.</p></td>
						<td><p class="psemp-mar-font">Jen O’Neal, Natural Systems Design</p></td>
						
					</tr>
					<tr>
						<td><p class="psemp-mar-font">Investigation of sediment contaminant impacts on the growth and survival of juvenile Puget Sound Chinook salmon</p></td>
						<td><p class="psemp-mar-font">The goal of this project is to conduct toxicity tests to determine if food restriction and exposure to a Duwamish Waterway sediment toxic chemical mixture that overlaps with smoltification will have a compounding stress impact on juvenile chinook salmon, reducing their overall health and possibly contributing to increased mortality. This research on the contribution of PCBs, PBDEs, and complex sediment chemical mixtures to estuarine-reared-fry growth, swimming performance, and health status will allow WRIA 9 to better evaluate the potential for contaminants to be limiting survival of outmigrant Chinook.</p></td>
						<td><p class="psemp-mar-font">John Stark, Washington State University</p></td>					
						
