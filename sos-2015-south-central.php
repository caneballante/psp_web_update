		<h3>Building Cities in the Rain </h3>
        <p>A South Central LIO NTA started this innovative project, which addresses challenges of ensuring compact growth in urban areas while still managing stormwater needs. The Department of Commerce leads a broad-based committee developing recommendations for a stormwater control transfer program designed to help restore priority basins while addressing stormwater. <a href="https://www.ezview.wa.gov/site/alias__1780/34828/default.aspx" target="_blank">New draft guidance is planned for release in summer 2015.</a></p>
        <h3>Implementation of Salmon Habitat Plans</h3>
        <p>The South Central LIO is home to three salmon recovery watersheds: WRIA 8, WRIA 9, and WRIA 10/12.  All three have been working hard with numerous stakeholders to implement their recovery plans.</p>
          <div class="no-icon"><a href="http://www.govlink.org/watersheds/8/" target="_blank">
          <h6>WRIA 8:</h6></a></div>
          <p>Since 2005, we have completed 49 projects from<strong> </strong>the plan and initiated an additional 51, resulting in:</p>
        <ul>
          <li>2,625 acres protected through acquisition or easement</li>
          <li>More than 450 acres of riparian area planted/treated</li>
          <li>Nearly 1 mile of levees removed or set back</li>
          <li>More than 73 acres of floodplain reconnected</li>
          <li>More than 8,000 linear feet of lakeshore restored</li>
        </ul>
        <div class="no-icon"><a href="http://www.govlink.org/watersheds/9/" target="_blank"><h6>WRIA 9:</h6></a></div>
        <p>Since 1998 more than 80 projects and 14 programs have been completed or are in progress, resulting in:</p>
        <ul>
          <li>918 acres protected through acquisition or easement</li>
          <li>More than 61 acres of riparian area planted</li>
          <li>6,200 feet of levees removed or set back</li>
          <li>More than 57 acres of floodplain reconnected</li>
          <li>3,000 linear feet of marine shoreline restored</li>
        </ul>
        <div class="no-icon"><a href="http://www.co.pierce.wa.us/index.aspx?nid=2873" target="_blank">
        <h6>WRIA 10/12:</h6></a></div>
          Since 1999, more than 56 projects have been completed with 21 projects either underway or proposed for 2015, resulting in the following in the Puyallup Watershed:</p>
        <ul>
          <li>More than 150 acres of riparian area planted</li>
          <li>More than 584 acres of floodplain reconnected</li>
          <li>More than 21,820 linear feet of levee setback or removed</li>
          <li>More than 1,257 acres of floodplain property acquired</li>
        </ul>
        <h3>Information for Improved Decision-Making</h3>
        <p>South Central LIO partners have collected data and tested strategies to help improve recovery actions, including completion of several large NEP-funded research grants:  </p>
        <ul>
          <li>Estimating the <strong>costs of stormwater infrastructure</strong> needed to restore water quality and flow in WRIA 9.</li>
          <li>Enhancing and standardizing <strong>benthic macro-invertebrate monitoring</strong> (BIBI).</li>
          <li>Evaluating <strong>effectiveness of rural land use regulations</strong> in protecting water quality.</li>
          <li>Identifying methods and metrics to monitor aquatic habitat in small wade-able streams, fostering <strong>adaptive management</strong>.</li>
          <li>Modeling toxic contamination reduction and source control needs to <strong>eliminate fish consumption advisories</strong> in Lake Washington.</li>
          <li>Assessing how human activities affect nitrogen loading and dissolved oxygen levels in Quartermaster Harbor (a shallow marine estuary) so <strong>water quality can be improved</strong>.</li>
          <li>Implementing a new approach to encouraging <strong>riparian stewardship among landowners</strong> along 16 miles of the lower Cedar River.</li>
          <li>Creating a Green Shores for Homes rating system to encourage <strong>shore-friendly development</strong>; </li>
          <li>Developing and evaluating strategies for <strong>model flow control measures for creeks</strong>.</li>
          <li>Advancing the Don&rsquo;t Drip and Drive social marketing program so that it is ready to be a major regional effort to <strong>improve vehicle owners&rsquo; awareness of leaks and motivate them to fix their leaks</strong>.</li>
          <li>Demonstrating <strong>significant improvements in water quality outfalls in Tacoma</strong>, including 89 to 98 percent reduction in three PAHs at all seven outfalls. Tacoma attributes this success to its aggressive source control and enhanced maintenance programs.</li>
        </ul>
