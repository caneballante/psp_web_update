		<p>The <a href="https://psp.wa.gov/evaluating-vital-signs.php">Puget Sound Vital Signs</a> and their indicators are measures of ecosystem health that describe our vision  for the future. Vital Signs also articulate the statutory goals for Puget Sound  recovery and describe how we will know whether statutory goals are achieved. The  2022-2026 Action Agenda sets targets for six Vital Sign indicators. These targets represent iconic and valued components of the Puget  Sound ecosystem, and they are strongly linked to the work proposed in this  Action Agenda. </p>
		<img class="img-responsive"  src="images/Action-agenda-example-target.jpg" width="946" height="259" alt=""/>
<h2>Funding recovery </h2>
		<p>The Partnership works closely with partners to  develop a dependable and diverse funding strategy that will support Puget Sound  recovery now and in the future. Federal, state, local, and tribal governments  currently provide much of the funding for Puget Sound recovery actions.  Non-governmental agencies, private foundations, businesses, and individuals  also provide funding.</p>
		<h2>Puget Sound funding strategy </h2>
		<p>An effective funding strategy for Puget Sound  recovery aims to: </p>
		<ul class="bullet-size-fix">
			<li>define the full range of funding needs for Puget Sound recovery; </li>
			<li>maintain and efficiently use existing funding; </li>
			<li>engage local voices to determine how and where to best spend funding  with clarity and impact; and </li>
			<li>secure additional funding to fully implement Action Agenda strategies  and actions. </li>
		</ul>
		<p>Funding approaches must also  integrate principles and practices of honoring tribal  nations&rsquo; treaty and sovereign rights, equity, and environmental justice  to address the  harmful effects effects felt by some communities. <br>
			The funding strategy for Puget Sound recovery is  detailed in the 2022-2026 Action Agenda  and includes five  key components:</p>
		<ol class="bullet-size-fix">
			<li>Establish  a clear picture of the size and nature of the funding need for Puget Sound  recovery</li>
			<li>Maintain  and increase funding from existing Puget Sound recovery sources, including  developing a major new source of state funding</li>
			<li>Increase  the effectiveness of investment decisions for existing sources of funding</li>
			<li>Build  a portfolio of new private funding sources</li>
			<li>Enhance  capacity for rapid funding response </li>
		</ol>
		
