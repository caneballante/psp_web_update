		<h2>How is the Action Agenda developed? </h2>
		<p>The Partnership  provides leadership through the collective development of updating the Action  Agenda, progress measurements, and funding strategy. </p>
		<img class="img-responsive" src="images/Action-Agenda-process.jpg" width="911" height="798" alt=""/>
		<p>The Partnership&rsquo;s three statutory boards  direct the Partnership in its charge of accelerating the science-informed  effort to recover Puget Sound. Two advisory councils advise the Leadership  Council on matters related to Puget Sound recovery. These boards and advisory  councils are key to the Partnership&rsquo;s role and coordinate their priorities for  maximum effectiveness via shared goals. </p>
		<p>Community  members and residents can also support the recovery effort through their own  actions. For example, by voting for programs and candidates that support the  stewardship of the Puget Sound and by considering sustainability and the  environment in their purchasing decisions.Â  </p>
