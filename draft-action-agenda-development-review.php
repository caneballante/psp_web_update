			<h2>WHAT IS THE 2022—2026 ACTION AGENDA?</h2>
			<p>For the purposes of the 2022-2026 Action Agenda update, an action describes the activities that will be a shared focus for implementing <a href="https://pspwa.box.com/s/ghhp8c3dbm0uegyq1o8c1c3cidwf0j1m" target="_blank">each strategy</a> from 2022-2026. This could include restoration and acquisition; program development, improvement, or implementation; education; outreach; research; legislative or policy improvements; or other types of activities. Actions are intended to guide partner implementation and innovation and will inform the focus of public and private funding and implementation support by the boards and regional partners. The 2022-2026 Action Agenda update will not include a solicitation for or list of Near-Term Actions (NTA) like in past Action Agendas.</p>
			
			<h2>How will Action Agenda actions be identified?  </h2>
			<p>The phase 4 process is designed to promote co-generation of Action Agenda actions that will implement strategies. Actions will be identified through public workshops and will utilize <a href="https://psp.wa.gov/implementation-strategies.php">Implementation Strategies</a> and other existing plans<sup>1</sup> – as well as approved desired outcomes and approved strategies – as a starting point for discussion.</p>
 
<p>Public workshops will be held virtually in early November and early December. Participation in the workshops is highly encouraged and will provide an opportunity for partners from across the recovery come together to co-generate actions to advance Puget Sound recovery.</p> 
 
<p>This webpage will continue to be updated with background materials in advance of each workshop. Following the public workshops, results will be synthesized into proposed actions for board review and adoption in early 2022. </p>
			
			<div class="highlightbox margin-0-top margin-0-bottom"><p><sup>1</sup><a href="https://psp.wa.gov/LIO-overview.php">Local Integrating Organization (LIO) plans</a>, <a href="https://nwtreatytribes.org/habitatstrategy/" target="_blank">tribal priorities</a>, <a href="https://psp.wa.gov/salmon-recovery-overview.php">salmon recovery plans</a>, and <a href="https://www.governor.wa.gov/issues/issues/energy-environment/southern-resident-orca-recovery/task-force" target="_blank">the Orca Task Force recommendations</a> also provide important activities and policy recommendations for the Action Agenda and reveal areas of shared focus where actions will advance multiple aspects of recovery. These plans are all created through science-informed and collaborative processes that capture the expertise and most recent thinking of individuals from around the region. Through the process of reviewing and providing feedback on the initial draft list of actions via public workshops, partner and board meetings, recovery partners will have the opportunity to identify additional actions that are needed implement strategies. </p></div><br>
			
