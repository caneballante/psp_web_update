<p>Specifically, the Grant Writer and Strategist can provide the  Tribes, LIOs, LEs, and local partners with grant writing technical assistance  and the capacity to prepare their grant applications for submission. They can  help develop grant proposals to federal, state, and private funding agencies. The  overall goal is to increase the number of successful submissions for local and  regional recovery projects. This technical assistance is free for partners and  the partnership does not take any portion of the award if successful. </p>
<h2>Specific Grant writing Technical Assistance that we can  provide:</h2>
<ul class="bullet-size-fix">
	<li>Writing  and Writing Review</li>
	<ul>
		<li>Review drafts of applications or in collaboration with the project sponsor, create drafts on their behalf.Â  </li>
	</ul>
	<li>Opportunity or Notice of Funding Review</li>
	<ul>
		<li>Review a funding opportunity and break it down into manageable parts.</li>
	</ul>
	<li>Grant Application Management</li>
	<ul>
		<li>Provide management of the application process. Managing the progress and completion of sections in collaboration with the project sponsor. </li>
	</ul>
	<li>Grant Strategy Development</li>
	<ul>
		<li>If you are interested in brainstorming a large project or opportunity or have a planned project that you cannot match funding to. </li>
		</ul>
			</ul>
	<p>Some things we cannot provide: We cannot write a grant  application in a silo, it will take collaboration and work from the partner. We cannot physically submit an application for a partner, and in general Puget Sound Partnership cannot be a lead applicant or a fiscal agent on any grant we are assisting on. We also cannot directly grant write on an HSIL or regional EPA opportunities, nor provide post award assistance. <br>
		The Grant Writer and Strategist is one full-time position on  the Strategic Funding Team. Therefore, Puget Sound Partnership is being  proactive in thinking about their capacity and have created a decision matrix  to able to equitably distribute their time. The decision matrix will consider Puget Sound ecosystem and salmon recovery priorities, as well as equity and  environmental justice considerations.</p>
	<h2>Grant Writing </h2>
<p>For direct grant writing to be successful for there are some things on the partner side that can make this technical  assistance smooth as possible. Before reaching out for assistance please make  sure you have the following prepared. The Strategic Funding Team can only  support projects that align with the Puget Sound Partnership&rsquo;s collective effort to restore and protect the Puget Sound,  therefore requests must support the Action Agenda, Salmon Recovery  Plan, and Science Work Plan.</p>
	<p><u>Project/Program Information</u></p>
	<ul type="disc">
		<li>Identified project concept, problem statement, project sponsor, collaborators, and project scope. </li>
		<li>Technical requirements of the funding opportunity have either been completed or have a planned completion. This may include design, engineering, modeling, etc. The Strategic Funding Team cannot complete these technical requirements. </li>
	</ul>
	<p><u>Funding  Opportunity</u></p>
	<ul type="disc">
		<li>A grant       opportunity or notice of funding has been identified, reviewed, and it&rsquo;s       been determined that the entity and project are eligible. </li>
	</ul>
	<p>**<em>If  you have a project or concept that you have been unable to identify a funding  opportunity that fits, or if you have a project and/or funding need and want to  strategize local partnerships, please reach out to our Strategic Funding Coordinator </em><em>who can properly assist. It does not mean you cannot receive  assistance; it just means you are not at the writing stage yet!</em></p>
	<p><u>Capacity  &amp; Readiness</u></p>
	<ul type="disc">
		<li>Assessed       internal capacity and if awarded a grant you have the capacity to not only execute the project, but capacity to complete financial management, compliance, reporting, and manage your relationship with your grant       management officer and/or program officer. <em>Puget Sound Partnership will  not be able to provide any post award support through this team. </em></li>
		<li>Considered and       assured there are no barriers that would prevent successful execution of the project if the funding the Strategic Funding Team assisted on, was awarded. <em>If a barrier(s) exist please reach out to our Funding       Barriers Strategist </em><em>for support in how to address it prior to pursuing funding. </em></li>
		<li>Partners       should ensure that they are eligible for the funding they are seeking  assistance for. The eligibility could include a current SAM account and UEI for federal opportunities. </li>
	</ul>
	<p><u>Financial  Information</u></p>
	<ul type="disc">
		<li>Draft or complete budget prepared. <em>The Strategic Funding Team may be able to give general feedback and advice on formatting, but specific numbers been to come from the project sponsor.</em></li>
		<li>Partners should have their organization&rsquo;s financial documents in order and ready to include in any application. </li>
	</ul>
	<p>After partners have considered this list and are ready to  request assistance, please do so by completing this brief intake form: <strong>LINK </strong><em>If you are  unsure about some aspects to this list, please still complete the intake form  and describe your uncertainty and the Strategic Funding Team will assist.</em><strong> </strong></p>
	<h2>Grant Strategy</h2>
	<ul>
		<ul>
			<li>Grant Matching â Opportunity Driven</li>
			<ul>
				<li>When an opportunity opens that aligns with known projects or  partner funding needs and interests, the Strategic Funding Coordinator or the  Grant Writer and Strategist will share out the opportunity with the partner and  suggest that it may be an option for them. </li>
			</ul>
			<li>Grant Strategy â Project Driven</li>
			<ul>
				<li>A deeper dive into a large project where a partner seeks a  grant strategy and potential timelines for funding. </li>
			</ul>
		</ul>
	</ul>
	<p>Grant strategy thinks about the bigger picture of your  project and outcomes. The project you are seeking a strategy for should be  vetted through community process, have partners identified, have activities and  outcomes already determined. A request for grant strategy will begin with an  in-depth consultation so we may learn about your project, its history and  rationale, and begin to review any materials that have been created. We will  focus in on the overall goal of the project and intention and strategize how  those lines up with known funding sources. This may mean piecing together  different funding sources for phases and looking out over multiple years. We  will look at what could be potential successful funding pathways for your  project. We will look at what has historically been successful for the  opportunities we identify, to gauge how your project aligns with past  successful applicants as well as the specific funding opportunity scoring  criteria. <br>
		Of course, we are not the funders. And while we might create  a grant strategy with options that seem promising, we have no guarantee that  you will be awarded those specific funding opportunities. We will of course do  our best to assist you to be in the best possible shape for your applications  with our suite of technical assistance. </p>
