			<h2 class="margin-0-top">Scope of 2016 Action Agenda Update</h2>
			<p>The 2016 Action Agenda will focus Puget Sound recovery work for the next 2 years on actions associated with the three Strategic Initiatives of Stormwater, Habitat, and Shellfish. The Strategic Initiatives were first incorporated into the Action Agenda in 2012 as a means of focusing and prioritizing actions that address the most significant problems and create meaningful improvements for Puget Sound.</p>
			<p>In July 2015, the Puget Sound Partnership&rsquo;s Leadership Council adopted an updated list of sub-strategies associated with each Strategic Initiative (Resolution No. 2015-01). The sub-strategies will serve as the scope and focus for development of the Near Term Actions (NTAs) for the 2016 Action Agenda 2-Year Implementation Plan. In addition, a number of crosscutting sub-strategies were identified and included as important to all Strategic Initiatives. These include addressing climate change, supporting enforcement, maintaining visibility, and identifying and filling science gaps. </p>
			<p><strong>Strategic Initiative Workgroups convene to develop update recommendations</strong><br>
				The Partnership convened three interdisciplinary workgroups in May 2015 to review the sub-strategies, develop criteria, and recommend the sub-strategies that should be included in each Strategic Initiative. The workgroups&rsquo; recommendations included guidance for the Strategic Initiative Transition Teams. The recommendations were available to the public for comment and were also transmitted to the Science Panel, Ecosystem Coordinating Board, and Salmon Recovery Council for consideration. The Leadership Council considered all recommendations and public comments before approving the updated Strategic Initiatives. More information regarding the workgroup process can be found in the Workgroup Recommendations, June 11, 2015.</p>
			<p><strong>Strategic Initiative Transition Teams use updated Strategic Initiatives to guide work</strong> <br>
				The Strategic Initiative Transition Teams formed in July 2015, to undertake the following tasks:</p>
			<ul>
				<li>Identify regional priorities for the updated Strategic Initiatives</li>
				<li>Develop criteria for review of Near Term Actions</li>
				<li>Solicit, review, and recommend NTAs for the 2016 Action Agenda</li>
			</ul>
			<p>The adopted 2016 Strategic Initiatives and sub-strategies guide the work of the Strategic Initiative Transition Teams in establishing the regional priorities and developing the solicitation for Near Term Actions. Follow these links for more information: </p>
