			<p><strong>UPDATE: FALL 2015 </strong><br>
				Oil transportation by rail and other modes has changed markedly in Washington State over the past 6 years—especially in inland areas and along the Columbia River and Grays Harbor. Given the statewide nature of these changes and the Partnership&rsquo;s evolving focus on Puget Sound, the Oil Spill Work Group disbanded in fall 2015 after a 2-year hiatus. Nevertheless, the products of the group&rsquo;s efforts (like the Puget Sound Vessel Traffic Risk Assessment, described below) will continue to inform ongoing policy and management conversations.</p>
			<p>Oil spill prevention and planning is critical in our region. Going forward, the Puget Sound Partnership will continue, as we are able, to participate in regional oil-spill planning and prevention forums (like the Northwest Area Committee, Pacific States/British Columbia Oil Spill Task Force, and the Puget Sound Harbor Safety Committee).</p>
			<h2>Puget Sound Partnership involvement in oil-spill planning and prevention</h2>
			<p>The 2009 Washington State Legislature directed the Puget Sound Partnership to provide independent advice to and assessment of Washington State&rsquo;s oil spill programs and to make recommendations for any needed improvements. In late summer 2010, the Partnership assembled a Cross-Partnership Oil Spill Work Group to help the Leadership Council carry out this responsibility.<br>
				The organizations represented in the Work Group broadly represented issues, concerns, and recommendations relevant to Puget Sound, the Washington coast, and the lower Columbia River.</p>
			<p>The Work Group's first meetings occurred in late fall 2010. Attendees of the work group&rsquo;s October 29 meeting unanimously agreed on four recommendations. On November 23, 2010, the Puget Sound Partnership Leadership Council passed Resolution 2010-4 to support these recommendations.</p>
			<p>The Partnership summarized the Work Group&rsquo;s recommendations to the House&rsquo;s Ecology and Parks Committee on December 10, 2010. Video on TVW.</p>
			<p>The 2012 Puget Sound Action Agenda outlined the priorities identified by the Work Group in May 2011. Since then, the Partnership, through Action Agenda, has focused on activities related to:</p>
			<ul>
				<li>Risk assessments of vessel traffic</li>
				<li>Reduction of abandoned and derelict vessels</li>
				<li>Involvement of local organizations in oil spill practice drills and assessments of natural resources</li>
			</ul>
			<h2> Formation of Vessel Traffic Risk Assessment Steering Committee</h2>
			<p> In October 2012, the Puget Sound Partnership Oil Spill Work Group and Puget Sound Harbor Safety Committee formed a joint Vessel Traffic Risk Assessment (VTRA) Steering Committee, co-chaired by Todd Hass (from the Partnership) and John Veentjer (from the Harbor Safety Committee), composed of 12 representatives from several maritime industry sectors, the Makah Nation, Washington Association of Counties, environmental non-governmental organizations, the Washington State Department of Ecology, and the U.S. Coast Guard.</p>
			<p>In November 2012, the Puget Sound Partnership awarded a grant to George Washington University to update the VTRA for north Puget Sound. Background and latest supporting materials for the analysis are hosted at Professor Rene van Dorp&rsquo;s George Washington University website. The VTRA Steering Committee met almost monthly between Dec. 2012 and February 2014.</p>
			<p>The VTRA Steering Committee and other interested parties reviewed a series of draft VTRA 2010 reports between late January and mid-February 2014. The <a href="http://www.seas.gwu.edu/~dorpjr/tab4/publications_VTRA_Update_Reports.html">final report</a> (PDF128 Mb download) was released on March 31, 2014.</p>
			<h2>Formation of the VTRA Work Group</h2>
			<p>The work of the VTRA 2010 Steering Committee is complete, but in early April 2014 most of the participants tentatively agreed to form a vessel traffic work group through the Puget Sound Harbor Safety Committee (facilitated by the same co-chairs as previously). The goals of the work group were to:</p>
			<ul>
				<li>Understand how the VTRA 2010 results can be compared to other risk assessments</li>
				<li>Promote coordination and interaction with peers in Canada concerning same</li>
			</ul>
