			<h2 class="margin-20-top">What is in the Action Agenda?</h2>
			<p>The Action Agenda for Puget Sound is the shared roadmap to recovering our nation's largest estuary– it identifies the goals and strategies for recovery and describes how the work of many partner organizations contributes to improving the health of Puget Sound.</p>
			<p>The 2016 update is expected to result in a more focused Action Agenda designed to accelerate Puget Sound recovery. The Action Agenda comprises two components: the <em>Comprehensive Plan</em> and the <em>Implementation Plan</em>.</p>
			<ul>
				<li> The <strong>Comprehensive Plan</strong> provides the roadmap for long-term Puget Sound recovery by outlining overarching strategies for successful protection and restoration, identifying the full scope of actions and funding necessary for recovery, and introducing the approaches by which issues and activities are prioritized, progress is evaluated, and strategies and actions are adapted over time. </li>
				<li>The<strong> Implementation Plan</strong> is the<em> action</em> component of the Action Agenda. It represents the collective effort focused on Puget Sound recovery for the next two years. Based on the fundamental framework and broad strategies described in the <em>Comprehensive Plan</em>, the <em>Implementation Plan</em> defines the suite of Near Term Actions and ongoing programs that will ensure the progress necessary over the next two years to achieve the 2020 recovery targets for Puget Sound Vital Signs. </li>
			</ul>
			<p> All actions are informed by science and are designed to contribute toward achieving our recovery goals to improve Puget Sound.   Access the <a href="http://pugetsoundactionagendaupdate.publicmeeting.info/">full Draft 2016 Action Agenda for Puget Sound</a> or the <a href="http://pugetsoundactionagendaupdate.publicmeeting.info/">Executive Summary</a>. </p>
			<p>The Action Agenda's Implementation Plan focuses implementation into three <a href="2016_AA_strategic-initiatives-update.php">Strategic Initiatives</a>:</p>
			<ul>
				<li>Prevent pollution from stormwater runoff</li>
				<li>Protect and restore habitat</li>
				<li> Protect and recovery shellfish beds</li>
			</ul>
			<p> These initiatives represent priorities for Puget Sound, balancing ecological, economic and human wellbeing factors to focus on actions that make the greatest progress toward ecosystem recovery targets for the time and resources spent. The Strategic Initiatives help direct spending and resources, and guide the Partnership’s work with partners to increase funding, seek policy changes, report successes and challenges, and educate and engage people in the recovery effort.</p>
			<p>The Implementation Plan includes a suite of priority <strong>Near Term Actions</strong> to ensure recovery progress within the Strategic Initiatives over the next two years. Near Term Actions are new, expanded, or enhanced programs, specific projects, or scientific investigations. The Implementation Plan also includes ongoing programs that are part of existing Puget Sound recovery efforts and are aligned within the Strategic Initiatives. </p>
			<h2>HOW DO ACTIONS GET INCLUDED IN THE ACTION AGENDA?</h2>
			<p>Tribes, Local Integrating Organizations, regional organizations and groups, research and academic institutions, and state agencies, among others, may sponsor NTAs and propose them for inclusion in the Action Agenda. Where proposed actions align with one of the three Strategic Initiatives and meet other threshold criteria, they may be ranked for inclusion in the Action Agenda. </p>
			<h2>WHAT WAS THE PROCESS FOR UPDATING THE 2016 ACTION AGENDA?</h2>
			<p>The process for updating Near Term Actions for the 2016 Action Agenda began in spring 2015 with the formation of three Strategic Initiative Update Workgroups. Each workgroup reviewed and discussed the scope for addressing the issues associated with their assigned Strategic Initiative. In June 2015, the workgroups summarized their findings in a <a href="https://pspwa.app.box.com/s/gabtcrbzo9i5yybkeyi6lx6cez0bh10o/1/6900322854/56265146454/1">white paper</a> submitted to the Science Panel for consideration and to partners and the public for a 30-day comment period. After the Leadership Council reviewed and approved the scope for the Strategic Initiatives, the process of selecting Near Term Actions for the 2016 Action Agenda began. The figure below shows the process and timeline for developing the 2016 Action Agenda.</p>
			<p>In September 2015, local, regional, and tribal partners around Puget Sound were asked to <a href="https://pspwa.app.box.com/s/43heo2w6kb28197m3s3wsw69kkg90oud">submit proposals</a> for near-term recovery actions that can be completed over the next 2 to 4 years to support the three Strategic Initiatives.  Local, regional, and tribal partners submitted a total of 364 final Near Term Action (NTA) proposals. These final proposals were reviewed, scored, and ranked by three interdisciplinary teams of experts (known as the <a href="2016_AA_SITT.php">Strategic Initiative Transition Teams</a>) using a set of <a href="https://pspwa.box.com/s/43heo2w6kb28197m3s3wsw69kkg90oud">published criteria</a>. </p>
			<img class="img-responsive" src="images/actionagenda_timeline.png" width="975" height="438" alt=""/>
			<h2>HOW DO WE KNOW WHETHER THE ACTION AGENDA IS EFFECTIVE?</h2>
			<p><a href="http://www.psp.wa.gov/vitalsigns/">Puget Sound Vital Signs</a> and <a href="action_agenda_targets.php">recovery targets</a> measure how well the Action Agenda is working to recover our ecosystem. If data on the ground show that we’ve made progress toward a recovery target, we will know that the Action Agenda is working. We report on our progress in the biennial <a href="os.php">State of the Sound report</a>. Effectiveness monitoring tools and publications include:</p>
			<ul>
				<li><a href="https://sites.google.com/a/psemp.org/psemp/news-and-announcements/secondpsempquarterlynewsletterissued">Puget Sound Ecosystem Monitoring Program</a></li>
				<li> <a href="https://sites.google.com/site/pressureassessment/home">Puget Sound Pressure Assessment</a></li>
				<li><a href="sos.php"> State of the Sound</a></li>
				<li><a href="http://www.psp.wa.gov/vitalsigns/">Vital Signs</a></li>
				<li><a href="http://psp.wa.gov/gis/ReportCard/">Action Agenda Report Card</a></li>
			</ul>
