			<h2 class="margin-20-top">What is in the Action Agenda?</h2>
			<p> Several years of <strong>research and effort</strong> have shown three areas of focus that deliver the most progress toward recovery for dollars invested. Termed &ldquo;Strategic Initiatives&rdquo; these <strong>three priority areas</strong> form the basis of the Action Agenda for 2016.</p>
			<p>The Action Agenda addresses each <strong>Strategic Initiative</strong> by setting targets for recovery that are based on scientific understandings of the ecosystem. The <strong>recovery targets</strong> are used to <strong>guide revisions</strong> to the Action Agenda, <strong>focus actions</strong> to be accomplished right away (<strong>Near Term Actions</strong>), <strong>recommend allocations</strong> of funding and other resources to specific strategies and actions, and <strong>evaluate</strong> Action Agenda implementation.</p>
			<p>The recovery targets are aimed at achieving recovery by the year 2020, although thorough research and careful monitoring of Puget Sound conditions show that the realistic timeframe for recovery should be greatly extended beyond 2020.</p>
			<h2>How do actions get included in the Action Agenda?</h2>
			<p>Two types of actions are included in the Action Agenda: Near Term Actions, which can be begun or completed in 2 years; and longer term actions. Local Integrating Organizations, regional organizations and groups, and state agencies may sponsor Near Term Actions (NTAs) and propose them for inclusion in the Action Agenda. If the proposed actions align with one of the three Strategic Initiatives, they may be considered for ranking in the Action Agenda. Once an action becomes designated as an NTA, it becomes eligible for state and federal funding.</p>
			<h2>What is the process for updating the Action Agenda?</h2>
			<p>The process for updating Near Term Actions for the 2016 Action Agenda began in spring 2015 with the formation of three Strategic Initiative Update Workgroups. Each workgroup reviewed and discussed the scope for addressing the issues associated with their assigned Strategic Initiative. In June 2015, the workgroups summarized their findings in a white paper submitted to the Science Panel for consideration and to partners and the public for a 30-day comment period. After the Leadership Council reviews and approves the scope for the Strategic Initiatives, the process of selecting Near Term Actions for the 2016 Action Agenda can begin. The figure below shows the process for developing the 2016 Action Agenda.</p>
			<ul>
				<li>Local Integrating Organizations work within their communities to      develop local plans of action for recovering Puget Sound, preferably      aligning with the Action Agenda.</li>
				<li>Partners propose Near Term Actions associated with the Strategic      Initiatives.</li>
				<li>Interdisciplinary teams of experts evaluate and rank the Near Term      Actions based in part on the importance of the NTAs in addressing the      Strategic Initiatives and for Sound-wide recovery Sound-wide.</li>
				<li>The Science Panel and Ecosystem Coordination Board review and rank      Near Term Actions.</li>
				<li>The Leadership Council approves the Near Term Actions with      adoption of the entire 2016 Action Agenda.</li>
				<li>The Leadership Council submits the 2016 Action Agenda to the U.S.      Environmental Protection Agency, for approval as the Comprehensive Conservation      Management Plan for Puget Sound under the National Estuary Program.</li>
			</ul>
