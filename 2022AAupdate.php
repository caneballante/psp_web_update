			<H2>How can you participate?</H2>
			<p>The Partnership and partners are currently developing a stakeholder engagement plan and establishing mechanisms for collaboration and participation in co-developing the objectives (phase 2), strategies (phase 3), and actions (phase 4) for the Action Agenda. Throughout 2020 and 2021, staff leading the development of the Action Agenda will provide presentations at the regularly scheduled meetings of existing stakeholder groups, host workshops where stakeholders can convene around particular topic areas, and provide online mechanisms for providing feedback as your schedule enables.</p>
			
			<p>Opportunities to engage in phase 2 are described generally--and specifics will be added as they become available--in the <a href="https://pspwa.box.com/s/2ebvje65mkmeveqmgt3l9407jxib0a7z" target="_blank" rel="noreferrer nofollow" data-attrib-id="MTU5MzYyNzI1NTE0My1odHRwczovL3BzcHdhLmJveC5jb20vcy8yZWJ2amU2NW1rbWV2ZXFtZ3QzbDk0MDdqeGliMGE3eg==">phase 2 information boxnote</a>.</p>
		
			<p>Stay tuned for more information on participation opportunities as the process is developed.</p>
			
			<p><strong>Ask for a briefing or a conversation</strong></p>
			<p>We would be happy to talk with you or your organization or group about the 2022-2026 Action Agenda development process and get your ideas. Please contact Don Gourlie (don.gourlie@psp.wa.gov) or your other Partnership staff connections to request a briefing.</p>
		
