		<p>The <a href="https://psp.wa.gov/action_agenda_center.php">Action Agenda for Puget Sound </a>charts  the course for recovery of Puget Sound&nbsp;—it complements and incorporates the work of  many partners from around Puget Sound to prioritize and describe strategies and  actions needed to recover Puget Sound. These strategies and actions provide opportunities  for federal, state, local, tribal, nonprofit, and private entities to invest  resources and coordinate actions. We, the Puget Sound Partnership, are responsible for coordinating regular updates  of the Action Agenda based on new and improved information about what works  best for recovery. We are currently beginning the process to update the Action  Agenda for 2022-2026. </p>

		<h2>The leadership council approves a concept for the 2022-2026 Action Agenda</h2>
		<p>In June 2020, the Leadership Council—the governing body of the  Partnership—approved <a href="https://pspwa.box.com/s/gsf2xgrqaxbmnq7zdummw5dft9otjbhn" target="_blank">a concept </a>for the  2022-2026 Action Agenda.</p>
		<p>Key elements of the concept:</p>
		<ol>
		  <li><strong>Update our ecosystem recovery plan to improve the  health of the Puget Sound ecosystem and the people that inhabit the region.</strong></li>
          <li><strong>Clearly describe the ecosystem status and the  pressures affecting the ecosystem. </strong></li>
          <li><strong>Focus the next four years on a set of  science-informed, shared recovery outcomes for reducing pressures and barriers. </strong></li>
          <li><strong>Collaboratively identify strategies and  actions to achieve those outcomes.</strong></li>
          <ol>
            <li>Identify strategies that emphasize multi-benefit and high-impact  actions.</li>
            <li>Identify targeted actions for recovery through direct  collaboration between partners rather than a passive solicitation for near-term  actions from partners.</li>
            <li>Provide better guidance for recovery strategies and actions by outlining  connections to ongoing programs (regulatory programs, for example), funding  opportunities, scientific information, and more.&nbsp; </li>
            <li>Describe the role of the Management Conference and other  audiences (the State Legislature, for example ) in implementing the Action Agenda.&nbsp;</li>
          </ol>
          <li><strong>Incorporate  new and improved tools and resources in the approach to recovery</strong>.<br>
          Includes the progress measures framework (Vital Signs and intermediate and  activity progress measures), strategies from the mobilizing funding  project,&nbsp;alterative future scenarios information, climate adaptation and  resilience strategies, and strategic communications.&nbsp;</li>
		</ol>
