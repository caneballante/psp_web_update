			<h2 class="margin-0-top"><strong>What is the 2022-2026 Action Agenda</strong></h2>
		<p>The Action Agenda for Puget Sound charts the course to recovery of our nation's largest estuary–it complements and incorporates the work of many partners from around Puget Sound to describe regional strategies and specific actions needed to recover Puget Sound. These strategies and actions provide opportunities for federal, state, local, tribal, and private entities to better invest resources and coordinate actions. </p>

		<p>The 2018-2022 Action Agenda was adopted in Dec 2018 and implementation is progressing steadily. Implementation and funding status of actions from the 2018-2022 Action Agenda is available in the <a href="https://actionagenda.pugetsoundinfo.wa.gov/Results/FundingStatus" target="_blank" rel="noreferrer nofollow" data-attrib-id="MTU5MjQ5Njc2MzY5OC1odHRwczovL2FjdGlvbmFnZW5kYS5wdWdldHNvdW5kaW5mby53YS5nb3YvUmVzdWx0cy9GdW5kaW5nU3RhdHVz">Action Agenda Tracker </a>of the Puget Sound Info website.</p>

		<p>The Partnership is committed to adopting a new Action Agenda with updated strategies and actions in the summer of 2022. The new Action Agenda will chart the course for recovery action for the years 2022-2026.</p>

		<h2>What has been decided so far?</h2>
		<p>In June 2020, the Partnership's Leadership Council approved a <a href="https://pspwa.box.com/s/fq4pfjqzdp26ujf00r5kkoghnveq5412" target="_blank" rel="noreferrer nofollow" data-attrib-id="MTU5MzQ2ODY5MTQ2OS1odHRwczovL3BzcHdhLmJveC5jb20vcy9mcTRwZmpxemRwMjZ1amYwMHI1a2tvZ2hudmVxNTQxMg==">proposed concept </a>for the 2022-2026 Action Agenda.</p>
				
		<p>Key elements of the proposal for the 2022-2026 Action Agenda:</p>
		<ol>
          <li>Develop a comprehensive ecosystem recovery plan that, once implemented, will result in improvements to the health of the Puget Sound ecosystem and the people that inhabit the region.</li>
          <li>Outline a shared problem statement and approach to recovery that is consistent with the Leadership Council&rsquo;s Beyond 2020 Vision. Clearly describe the ecosystem status and pressures, as well as objectives/outcomes for reducing pressures.</li>
          <li>Incorporate new and improved tools and resources in the approach to recovery, including the progress measures framework (Vital Signs and intermediate and activity progress measures), strategies from the mobilizing funding project,&nbsp; emerging scenarios information, climate adaptation strategies, and strategic communications.&nbsp;</li>
          <li>Focus the next four years of action on a set of science-informed, shared recovery objectives and collaboratively identified strategies and activities to achieve those objectives.</li>
          <ol>
            <li>Identify a shared set of common and priority pressures to reduce, as well as strategies to address those pressures that emphasize multi-benefit and high-impact activities.</li>
            <li>Identify targeted activities for recovery through direct collaboration between partners rather than a passive solicitation for near-term actions from individuals and organizations.</li>
            <li>Provide more complete guidance for advancing recovery strategies and activities by articulating connections to ongoing programs (regulatory programs, for example), funding opportunities, scientific information, and more.&nbsp;</li>
          </ol>
          <li>Describe and guide the work needed from the Management Conference and provide recommendations and guidance for implementation targeted to other audiences (the State Legislature, for example).&nbsp;</li>
        </ol>
