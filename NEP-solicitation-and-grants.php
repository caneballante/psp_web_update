			<p>Under the National Estuary Program, the U.S. Environmental Protection Agency (EPA) provides funding to Washington state agencies as Strategic Initiative Leads and to the Northwest Indian Fisheries Commission to implement the Puget Sound Action Agenda. Strategic Initiative Leads develop and implement six-year strategies for four categories of ecosystem protection and restoration. EPA funding to the Strategic Initiative Leads and the Fisheries Commission totals about $115 million since 2010. EPA can provide additional funding for the next two years of the Strategic Initiative Lead six year strategies, depending on congressional appropriations. Strategic Initiative Leads investments implement the 2012 Puget Sound Action Agenda and the priorities set through the strategic initiative process. The three strategic initiatives for 2012/2013 include:</p>
