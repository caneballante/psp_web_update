				<p><span class="new">NEW!</span> EPA releases RFP for Strategic Initiative implementation leads for FY2016
On September 22, EPA released a Request for Proposals titled “Puget Sound Action Agenda – Implementation Strategies and Actions for Vital Sign Recovery Assistance Program.” These funds will support the three Strategic Initiative Teams and their work with the EPA Puget Sound National Estuary Program and Puget Sound Partnership’s 2016 Action Agenda. Grant recipients will provide technical advice and manage subawards for implementing priority Action Agenda strategies. Proposals are due November 30, 2015. EPA Region 10 will host a solicitation webinar to address questions about the RFP on October 13, 2015. For more details on the RFP, visit EPA’s <a href="http://www2.epa.gov/puget-sound/funding-and-grants-puget-sound">Funding and Grants for Puget Sound website</a>.</p></div><br /><br />

		<p>Under the National Estuary Program, the U.S. Environmental Protection Agency (EPA) provides funding to Washington state agencies as Lead Organizations and to the Northwest Indian Fisheries Commission to implement the Puget Sound Action Agenda. Lead Organizations develop and implement six-year strategies for four categories of ecosystem protection and restoration. EPA funding to the Lead Organizations and the Fisheries Commission totals about $115 million since 2010. EPA can provide additional funding for the next two yearsof the Lead Organization six year strategies, depending on congressional appropriations. Lead Organizations   investments implement the 2012 Puget Sound Action Agenda and the priorities set through the strategic initiative process. The three strategic initiatives for 2012/2013 include:</p>
<ul>
		<li>Prevent pollution from urban stormwater runoff</li>
		  <li>Protect and restore habitat</li>
		  <li>Recover shellfish beds<br />
	      </li>
  </ul>
<p>Lead Organizations use competitive processes, and in some cases direct awards, to fund work identified in their six-year strategies. The <a href="http://blogs.nwifc.org/psp/about/">Northwest Indian Fisheries Commission </a> awards grants to the 19 federally recognized tribes in Puget Sound and any authorized consortium of these tribes to implement high priority projects identified in the Action Agenda.</p>
<p>The Puget Sound Partnership also has cooperative agreements with EPA focused on regional engagement, stewardship and managing the implementation of the Action Agenda.  </p>
<p>EPA funding is a small part of the overall funding needed to  protect and restore Puget Sound. The 2013 State of the Sound report estimates a $390 million shortfall over a three year period to implement near-term actions identified in the 2012 Action Agenda. </p>
<p>For more information on other grant and loan funding for protection and restoration projects in Puget Sound visit:</p>
