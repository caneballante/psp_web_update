			<h2>Background on the Action Agenda</h2>
			<p>For background on the Action Agenda, <a href="2018_AA_update.php">click here</a>.
			<p>
			<h2>How is this 2018-2022 Action Agenda different?</h2>
			<p>This 2018-2022 Action Agenda builds on past Action Agendas and reflects several new developments that focus and prioritize actions and investments. Improvements include:</p>
			<ul>
				<li><strong>Increased reliance on Vital Sign Implementation Strategies</strong>, which result in more targeted and specific strategies to achieve Vital Sign indicator targets built on current scientific knowledge and analyses of existing recovery work.</li>
				<li>A complete and direct <strong>discussion of the threats facing Puget Sound and barriers</strong> that continue to impede the recovery effort.</li>
				<li><strong>Re-organized content</strong> in the Comprehensive Plan to more clearly communicate the process for building, implementing, and improving the Action Agenda and the Puget Sound recovery effort.</li>
				<li><strong>Updated descriptions of important issues and strategies </strong>that apply to all Puget Sound recovery efforts, such as mobilizing funding, integrating science into our framework, climate change, education, and behavior change. And further clarifying how these concepts inform recovery planning.</li>
				<li><strong>A long term vision for Puget Sound resilience and recovery</strong>. This vision was developed by the recovery community and adopted by the Leadership Council in the summer of 2018.</li>
				<li><strong>An updated list of priority recovery actions</strong> to accelerate Puget Sound recovery progress and guide funding and implementation over the next four years.</li>
			</ul>
			<h2>What comments are helpful?</h2>
			<p>The Partnership welcomes comments on the role of the Partnership and Action Agenda, the methods for identifying needed priorities and actions, the content of the Action Agenda, needed actions to recover Puget Sound, and more. <strong>Certain components of the Action Agenda are not subject to change</strong>—the Regional Priorities and Near Term Actions for the 2018-2022 Action Agenda (Chapters 3 and 4 of the Implementation Plan) have been previously vetted by the recovery community through robust development and review processes.</p>
<a id="supp-materials"></a>
			<h2>What happens next?</h2>
			<p>Post October 15, the Partnership will release a detailed matrix of comments received and summary of responses, and make appropriate revisions to the Action Agenda. In the late fall and early winter, the Partnership will present a revised draft to the Management Conference, culminating with the Leadership Council considering the Action Agenda for adoption in December.  			</p>
