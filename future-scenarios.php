			<p><br>
			  On June 18, 2024, the  Future Scenarios project hosted a <a href="https://pspwa.box.com/s/qzcsdqj5vdxnb1j58fc1i1meepr5jms2" target="new">morning workshop</a> to: <br>
              <strong> - Learn about a new alternative scenario that incorporates bold land  use ideas</strong>. Leaders in Puget Sound recovery joined together to consider a future  that encompasses five of the bold ideas you helped generate in the November  2023 Bold Land Use Planning in Puget Sound workshop. <br>
              <strong>- Explore each bold land use idea.</strong> A discussion was held to consider the assumptions that  underpin the bold ideas and their associated tradeoffs to identify specific  implications, risks, and opportunities, as well as ways to navigate and offset  them. <br>
              <strong>- Contribute to a working list of concrete next steps.</strong> Using  insights from the scenario analysis, we helped to identify actions that could advance  some or all the bold ideas in both the near and long terms.<br>
              <br>
              This workshop  built on the work of the<a href="https://gcc02.safelinks.protection.outlook.com/?url=https%3A%2F%2Fpspwa.box.com%2Fs%2F21soyjonl5xcpbg07xvtvg6ztjs8do7f&data=05%7C02%7Ckatherine.wyatt%40psp.wa.gov%7Cb751ac52be7342f566cc08dc83ecc79e%7C11d0e217264e400a8ba057dcc127d72d%7C0%7C0%7C638530300562853263%7CUnknown%7CTWFpbGZsb3d8eyJWIjoiMC4wLjAwMDAiLCJQIjoiV2luMzIiLCJBTiI6Ik1haWwiLCJXVCI6Mn0%3D%7C0%7C%7C%7C&sdata=xuukOQenh5kQNVsdyKeX67WTLtZrJk1fr3MoQ5ePKFw%3D&reserved=0">November  2023 workshop</a>, which generated bold ideas that  address various development, human wellbeing, equity, and ecosystem restoration  implications derived from three alternative scenarios. Since then, the Future  Scenarios Project Team has worked closely with Strategic Initiative Leads and  other partners to create another alternative scenario that illustrates a subset  of bold land use ideas.  The workshop  explored a new hybrid scenario that included five bold ideas: </p>
		  <ol class="bullet-size-fix">
			  <li><strong>Integrate  growth management.</strong> Adopt  growth management frameworks that encourage cohesion and planning to achieve  multiple benefits at the watershed scale working across jurisdictions.&nbsp;&nbsp;</li>
			  <li><strong>Concentrate  growth in already developed areas.</strong> Adopt  policies and incentives that funnel growth into already developed urban areas  and emphasize redevelopment; limit rural growth and ensure rural growth that  does occur is near urban boundaries.&nbsp;&nbsp;</li>
			  <li><strong>Redefine  critical areas.</strong> Protect  remaining salmon habitat through enhanced and more consistent critical area  definitions and implementation across Puget Sound and invest in restoration of  habitat important to salmon including barrier removal, riparian habitat,  floodplains, and estuaries. Use climate impacts as a catalyst to expand  restoration efforts in impacted areas.</li>
			  <li><strong>Protect  and expand working lands.</strong> Adopt  policies that increase the viability of working agriculture and working  forestry. Fully value working lands and their services to incentivize farm and  forest land preservation.</li>
			  <li><strong>Emphasize  innovative stormwater management</strong><strong>.</strong> Implement a variety of stormwater policies that emphasize  multiple benefits at the watershed scale such as use of regional facilities,  stormwater parks, and stormwater transfers.</li>
		  </ol>
