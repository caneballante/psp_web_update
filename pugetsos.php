					<P class="margin-0-top">Testimony of Laura L. Blackmore, Executive Director of the Puget Sound Partnership, before the House Committee on Transportation and Infrastructure Subcommittee on Water Resources and Environment hearing on Protecting and Restoring America’s Iconic Waters, Tuesday, June 25, 2019
					</p>
					<hr>
					<p>Chair Napolitano, Ranking Member Westerman, and distinguished members of the subcommittee, thank you for inviting me here today. On behalf of the Puget Sound Partnership and our hundreds of partners, I want to thank you for convening this important hearing today.</p>
					<h3>Puget Sound—an Economic Engine, a Scenic Treasure, a National Draw</h3>
					<p>Puget Sound is a deep fjord estuary that lies within the broader Salish Sea. Considered the largest estuary by volume in the United States, Puget Sound is a complex ecosystem encompassing mountains, farmlands, cities, rivers, forests, and wetlands. Sixteen major rivers flow to Puget Sound and 20 treaty tribes call the region home.</p>
					<p>Four and a half million people live in the Puget Sound area with another 1.3 million expected to live there by 2040. Last month the Seattle Times reportedi that Seattle was the second fastest growing city in the nation in 2018, and the fastest in 2017. We are a region of innovators and entrepreneurs: eleven Fortune 500 companies are headquartered in the Puget Sound area, many of which have shaped 21st century life. Our economy is roaring and our natural beauty and recreation opportunities help businesses and companies attract top talent.</p>
					<p>On the surface, Puget Sound looks beautiful, but the fact is Puget Sound is slowly dying. Southern Resident orcas, Chinook salmon, and steelhead are all listed under the Endangered Species Act. Toxic chemicals and pharmaceuticals continue to pollute our waterways, and shellfish beds are routinely closed to commercial and recreational harvest. Despite a significant investment of energy and resources from federal, tribal, state, and local governments, habitat degradation outpaces restoration. While this</p>
					<p>situation at times seems impossibly gloomy, the hundreds of passionate people who are devoted to seeing the return of a healthy and resilient Puget Sound give us hope.</p>
					<h3>About the Puget Sound Partnership</h3>
					<p>The Puget Sound Partnership grew out of a groundswell of support from citizens concerned about the health of Puget Sound, its many culturally and ecologically significant species, and the well-being of the humans who also call this region home. Based on the recommendation of a task force headed by former EPA Administrator Bill Ruckelshaus, the Washington State Legislature formed the Partnership in 2007.</p>
					<p>On behalf of the people of Washington State, the Legislature charged us with recovering Puget Sound and achieving six goals:</p>
					<ul id="l1">
						<li>
							<p>Healthy human population</p>
						</li>
						<li>
							<p>Vibrant quality of life</p>
						</li>
						<li>
							<p>Thriving species and food web</p>
						</li>
						<li>
							<p>Protected and restored habitat</p>
						</li>
						<li>
							<p>Abundant water quantity</p>
						</li>
						<li>
							<p>Healthy water quality</p>
						</li>
					</ul>
					<p>Congress designated Puget Sound as an Estuary of National Significance in 1988. The Puget Sound Partnership participates in the EPA&rsquo;s National Estuary Program (NEP), created by Congress in 1987. This highly effective program, which incorporates 28 estuaries from every coast, charges us with developing and implementing a collaborative, non-regulatory blueprint for restoring and protecting this iconic water body.</p>
					<h3>We fulfill these responsibilities in three primary ways:</h3>
					<p>Chart the course – Action Agenda and Puget Sound Salmon Recovery Plan</p>
					<p>The 2018-2022 Action Agenda for Puget Sound, which serves as the Sound&rsquo;s Comprehensive Conservation &amp; Management Plan as authorized by the NEP, charts the course to achieving a resilient Puget Sound. It outlines regional strategies and specific actions required to make progress toward recovery. The actions proposed for funding in the Action Agenda offer the promise of effective investment in Puget Sound protection and restoration. As required under the NEP, the Partnership convenes a Management Conference composed of federal, tribal, state and local government agencies, businesses, the environmental community, the agricultural and timber industries, academic institutions, fishermen, shellfish growers, and other partners to develop and manage the implementation of the Action Agenda.</p>
					<p>The Partnership&rsquo;s Leadership Council also oversees the implementation of the Puget Sound Salmon Recovery Plan, approved by NOAA in 2007 as the region&rsquo;s recovery plan for Chinook salmon under the Endangered Species Act. The Salmon Recovery Plan includes strategies for recovering Chinook salmon populations in each watershed of Puget Sound. With federal and state funding, the Partnership supports local councils that manage each of these watershed-scale strategies.</p>
					<p>Promote shared measures – State of the Sound report</p>
					<p>The biennial State of the Sound report improves understanding across the Management Conference and among decision-makers about how well the recovery effort is going. The State of the Sound answers the following questions:</p>
					<ul>
						<li>
							<p>How is the ecosystem doing?</p>
						</li>
						<li>
							<p>What are the outstanding examples of recovery projects?</p>
						</li>
						<li>
							<p>How is management of recovery going?</p>
						</li>
						<li>
							<p>Who funds Puget Sound recovery?</p>
						</li>
						<li>
							<p>What is needed to see more progress in Puget Sound recovery?</p>
						</li>
					</ul>
					<h3>Support partners – mobilize funding, communicate effectively, remove barriers</h3>
					<p>The Partnership supports the collective effort of our partners by advocating for enhanced and diversified funding sources, funding science and monitoring work to answer pressing questions, evaluating the effectiveness of recovery actions, convening forums to confront difficult issues, and ensuring effective communication throughout our partner network.</p>
					<h3>Funding Shortfalls Threaten Puget Sound Recovery</h3>
					<p>Nothing tells the story of Puget Sound more profoundly than last summer&rsquo;s tragic loss of the newborn calf of Tahlequah, a member of the endangered Southern Resident orca J pod. She grieved over the body of her dead calf for 17 days, and her pod accompanied her as she swam 1,000 miles through Canadian and U.S. waters of the Salish Sea with the body of that calf. The world watched Tahlequah suffer, and now the world watches us.</p>
					<p>This year, Washington State legislators listened to their constituents and to Governor Inslee, to the pleas of the Governor&rsquo;s Southern Resident Orca Task Force, and to our Leadership Council and the multitude of Management Conference members. They passed significant policy and budget bills aimed at orca recovery. Because of the bold actions on the part of our state elected officials, we have hope that we will stave off extinction for the Southern Residents. But state resources alone are not enough. Federal funding is crucial. Here&rsquo;s why:</p>
					<p>Scientists say that we can still recover Puget Sound, but only if we act boldly now. We know what we need to do. The primary barrier between us and more food for orcas, clean and sufficient water for people and fish, sustainable working lands, and harvestable shellfish is funding. We cannot wait any longer to fully fund the Action Agenda and the Puget Sound Salmon Recovery Plan.</p>
					<p>The primary source of funding to implement the Action Agenda is the Puget Sound Geographic Program. Over the past several fiscal years, Congress has appropriated $28 million annually into this fund, managed by the EPA. National Estuary Programs nationwide leverage $19 for every $1 in federal funding, and we are no exception. While this funding is significant and appreciated, estimates of the actual need to fully implement the Action Agenda show that the funding received falls far short of the need: the funding gap for the 2014-2015 Action Agenda was 68 percent, and for the 2016-2018 Action Agenda it was 73 percentiii. The funding gap for salmon recovery is about 84 percentiv. Our monitoring shows that at these funding levels, we are barely holding our ground against further degradation, if not managing decline of the ecosystem.</p>
					<p>The single greatest step we could take to ensure a durable, systematic, and science-based effort for Puget Sound recovery is to fully fund the implementation of the Action Agenda and Salmon Recovery Plan on an on-going basis.</p>
					<p>The Promoting United Government Efforts To Save Our Sound (PUGET SOS) Act (H.R. 2247), introduced by Congressmen Heck and Kilmer this year, would authorize up to $50 million in funding for Puget Sound recovery, a significant and very welcome jump from the $28 million per year that Congress has appropriated for the last several fiscal years.</p>
					<h3>Why Passage of the PUGET SOS Act is Critical to Puget Sound Recovery</h3>
					<p>Puget Sound is a national treasure, as long as it is healthy. A dying Puget Sound is a national disgrace. Our Governor, state Legislature, local elected officials, Tribes, and network of organizations and individuals have proven their commitment to recovering Puget Sound. We need commensurate investment at the federal level. Passage of the PUGET SOS Act would demonstrate that federal commitment. Here&rsquo;s why this bill would be such a boon to Puget Sound:</p>
					<ul>
						<li>PUGET SOS aligns federal agency brainpower and resources. These are tremendous assets. Ensuring they are coordinated, setting goals, and holding each other accountable will help increase their effectiveness and provide yet another boost to Puget Sound recovery. Establishing the Puget Sound Program Office at EPA and requiring a Federal Task Force promises that these goals will be met.</li>
						<li>PUGET SOS protects and sustains a cherished resource and a cherished way of life. The investment of up to $50 million authorized in the PUGET SOS bill will enable us and our partners to more effectively plan and implement the projects that will recover Puget Sound.</li>
						<li>PUGET SOS demonstrates to the nation that Puget Sound is vital to the economic, cultural, and environmental security of the United States. By investing significantly in the health and well-being of Puget Sound, on par with other great waters like the Great Lakes and Chesapeake Bay, federal decision- makers demonstrate that Puget Sound is worth saving. They affirm that it is of critical importance to national well-being, and that they too are concerned for the future of their children and grandchildren. They demonstrate that recovering an ecosystem is more than a one-time effort, that our fates are interlinked with the environment we live in, and that we must stay ever vigilant and ever active in protecting and restoring our home.</li>
					</ul>
					<p>Thank you again for the opportunity to appear before you today, and I look forward to your questions.</p>
					<p>Laura Blackmore<br>
						Executive Director, Puget Sound Partnership
