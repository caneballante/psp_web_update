			<h3>Challenges to advancing recovery and opportunities going forward</h3>
			<p>It  is an ambitious goal to undo 150 years of environmental degradation,  particularly in the context of continuing demands on the ecosystem from population growth and the challenges of remaining competitive in an  every changing global economy. By their very nature, efforts to  strengthen regulations, modify how our communities look and function and  even ask individuals to change their daily habits will face roadblocks along the way. We described a number of our barriers to  implementing the Action Agenda in the 2013 State of the Sound. While we  continue to make progress in addressing these, our experience suggests  that helping our decision-makers and our citizens make &ldquo;Sound Choices&rdquo; in the years ahead will be the key to our success.</p>
			<h3>REGULATORY LOOPHOLES AND PERMIT BARRIERS</h3>
			<p><strong><u>Challenge:</u></strong> Despite listing of Chinook salmon under the Endangered Species Act more  than a decade ago, and absolute clarity about the negative impacts of certain land use  practices and handling of pollutants on Puget Sound, a number of  policies and regulations still include exceptions that impede our  recovery. These include exemptions for &ldquo;small&rdquo; projects, caps on costs for individuals or certain businesses, and phasing in of  requirements over long timeframes for the public to adjust to proposed  changes. In addition, conflicting regulatory requirements and  overlapping jurisdictional responsibilities can make compliance difficult and cumbersome, even slowing down projects that are designed  to restore the environment.</p>
			<p><strong><u>Recommendation:</u></strong> We need to provide stronger support for our decision-makers at all government levels to make unpopular decisions, and develop options for them to consider that help balance competing demands. </p>
			<h3>PUBLIC MISPERCEPTION OF THE PROBLEM.</h3>
			<p><strong><u>Challenge:</u></strong> The sparkling waters of Puget Sound suggest to the public that Puget  Sound is fine. Recent surveys indicate that even among those residents who recognize that  grave problems exist, they do not understand the urgency and have  &ldquo;recovery fatigue.&rdquo; They have heard the message for a long time and have  either lost hope, motivation or the desire to engage since they do not see progress. Others do not feel personal  responsibility for taking action. </p>
			<p><strong><u>Recommendation: </u></strong>Long-term  success will require new strategies to change public perception and  engage our decision-makers and members of the public in solutions. If we rely  on the public health analogy, it has been 65 years since the Surgeon  General warned us about the dangers of smoking. Puget Sound cannot wait  that long for its citizens to heed the warning and act.</p>
			<h3>DIFFICULTY TACKLING THE SIGNIFICANT ROLE THAT PRIVATE INTERESTS HAVE IN RECOVERY EFFORTS</h3>
			<p><strong><u>Challenge:</u></strong> Differences persist with Puget Sound communities over how to balance  the benefits of measures that are necessary to protect our natural resources and reduce  contamination to our groundwater and surface water against the cost of  changing business and land development practices. These conflicts are  especially apparent in our rural agricultural communities and in communities that do not want to restrict growth or limit the use  of private property.Â  Many of these actions entail significant  sacrifices or seem too costly to one segment of the population, even  when these actions may benefit the whole. Balancing ecosystem recovery needs with competing demands for services such as health,  transportation, education and social welfare will require making  unpopular decisions.</p>
			<p><strong><u>Recommendation:</u></strong><u> </u>We  need a comprehensive strategy that tips the scale in favor of bearing the costs for recovery to benefit future  generations including new creative approaches to attracting investment  in Puget Sound recovery. We need to work with landowner groups and major  economic interest groups to find &ldquo;win-win&rdquo; solutions that will enable them to prosper in conducting their businesses while  changing practices that contribute to the ongoing degradation of Puget  Sound.</p>
