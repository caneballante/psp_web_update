			<h2 class="margin-0-top">Strategic Initiatives represent priorities</h2>
						<p><a href="downloads/2014_action_agenda/Addendum_StrategicInitiatives_final_20141015.pdf">ADDENDUM: STRATEGIC INITIATIVES <br />
								The 2014/2015 Action Agenda for Puget Sound
						</a>
						<p>Three region-wide priorities are emphasized in the 2014/2015 Action Agenda:</p>
						<ul>
								<li><strong>Prevent pollution from urban stormwater runoff</strong>. Polluted runoff from roads, roofs, parking lots, and other paved areas is the biggest threat to Puget Sound&rsquo;s water quality. Although we have many tools and technologies for reducing stormwater pollution, we need to use them much more widely. </li>
								<li><strong>Protect and restore habitat</strong>. Restoring damaged shorelines and protecting salmon habitat along the many rivers and streams that flow into Puget Sound is necessary to save salmon and honor tribal treaty rights. We must stop harming habitat, protect what remains, and restore the critical habitats we have damaged. </li>
								<li><strong>Restore and re-open shellfish beds</strong>. Shellfish harvesting is a major Puget Sound industry, as well as a tribal treaty right.&nbsp;But harvests are threatened by pollution that has closed more than 7,000 acres of Puget Sound beaches. Shellfish health begins on land, through reduction of pollution from contaminated runoff and maintenance and repair of failing septic tanks. </li>
						</ul>
						<p>The initiatives represent priorities for Puget Sound, balancing ecological, economic and human wellbeing factors to focus on actions that make the greatest progress toward ecosystem recovery targets for the time and resources spent. The Strategic Initiatives help direct spending and resources, and guide the Partnership&rsquo;s work with partners to increase funding, seek policy changes, report successes and challenges, and educate and engage people in the recovery effort.&nbsp; </p>
						<p>For more information on the strategic Initiatives please review <a href="http://www.psp.wa.gov/downloads/2014_action_agenda/Final 2014 action agenda update/Section2_StratInits_20140617.pdf">Section 2 of the 2014/2015 Action Agenda</a>.<br />
						<br />
						<h2>Strategic Initiatives Incorporate 2014 Action Agenda Near-term Actions</h2>
						<p>At its meeting in May 2014, the Leadership Council adopted the updated 2014 Action Agenda and its associated Near Term Actions (NTAs). At its September 2014 meeting, the Leadership Council designated <br />
								the 2014 NTAs that support the Strategic Initiatives to prioritize resource allocation for accomplishing the NTAs.</p>
								<strong>2014 Strategic Initiatives </strong><br />
								<a href="downloads/2014_action_agenda/strategic initiatives/Stormwater.xlsx">Stormwater</a><br />
								<a href="downloads/2014_action_agenda/strategic initiatives/Habitat.xlsx">Habitat</a><br />
								<a href="downloads/2014_action_agenda/strategic initiatives/ShellFish.xlsx">Shellfish </a>
<p>To accomplish the update to the Strategic Initiatives, an ECB subcommittee was formed consisting of stakeholder representative and the subcommittee reviewed sub-strategies and NTAs, developed criteria for evaluating NTAs, and formed recommendations for what 2014 NTAs should be included in the three Strategic Initiatives. The subcommittee recommendation was considered at the August 2014 meeting of the Ecosystem Coordination Board, and the Board forwarded their recommendation to the Leadership Council for adoption</p>
