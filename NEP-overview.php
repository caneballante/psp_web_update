			<p>Congress created the <a href="https://www.epa.gov/nep/overview-national-estuary-program">National Estuary Program</a> in 1987, and the next year, designated Puget Sound as an Estuary of National Significance because it is critical to the environmental and economic well-being of the nation. In 2007, Washington passed legislation creating the Puget Sound Partnership, a state agency dedicated to protecting and restoring Puget Sound.</p>
			<p>The Puget Sound National Estuary Program is a non-regulatory initiative and a forum for engaging and aligning diverse organizations in partnership to create, implement and monitor approaches to achieve economic and environmental health of the Puget Sound. These organizations include federal agencies, tribal governments and organizations, state agencies, regional entities, local governments, not-for-profit organizations, higher education, the private sector, and people from around the Puget Sound region. </p>
			<p>Collectively, the governments, organizations, businesses, and individuals engaged in Puget Sound recovery are called the <a href="http://www.psp.wa.gov/downloads/2014_action_agenda/Final%202014%20action%20agenda%20update/AppA_Mngmnt-Conference.pdf">Management Conference</a>. Using a collaborative, consensus-building approach, the Management Conference engages in developing and implementing the<a href="https://www.psp.wa.gov/2022AAupdate.php"> Puget Sound Action Agenda</a>. The Action Agenda serves as the Puget Sound Comprehensive Conservation and Management Plan. It aligns and integrates regional conditions, builds upon local input and supports both local and regional priorities for protection and recovery of Puget Sound. </p>
			<p>In 2016, <a href="https://www.epa.gov/puget-sound">Region X of the Environmental Protection Agency (EPA)</a> implemented a funding model intended to accelerate recovery and protection of Puget Sound. Under this model, the Puget Sound Partnership acts as a convener of the Puget Sound Management Conference and curates the <a href="https://www.psp.wa.gov/2022AAupdate.php">Puget Sound Action Agenda</a>. The funding model supports the Partnership, the <a href="https://nwifc.org/">Northwest Indian Fisheries Commission</a>, and three strategic initiatives â€“ habitat, shellfish, and stormwater managed by state agencies, the <a href="http://www.psp.wa.gov/strategic-initiatives-leads.php">Strategic Initiative leads</a>. Those aligned under the EPA funding model work to maximize and leverage National Estuary Program dollars and seek tangible on-the-ground results.</p>
