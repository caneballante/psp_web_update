			<h3>ocean acidification</h3>
			<p>The term “ocean acidification” refers to a long-term change in seawater pH toward the acidic end of the pH scale. Ocean acidification is caused as carbon dioxide (CO<sub>2</sub>) from the atmosphere dissolves into seawater. Over time, the addition of CO<sub>2</sub> causes changes in seawater chemistry, lowering the pH and reducing the carbonate ion concentrations of the seawater. Ocean acidification will increase as fossil fuel combustion and deforestation continue to add CO<sub>2</sub> to the atmosphere.</p>
			<p>The acidity of the global ocean has increased by about 26 percent since 1750. The acidity is projected to further increase roughly 100 to 150 percent by 2100 relative to pre-industrial levels. Ocean waters on the outer coast of Washington and the Puget Sound have seen an acidity increase of about 10 to 40 percent since 1800 (decline in pH of 0.05 to 0.15).</p>
			<h6>Ocean acidification is a reality in the Pacific Northwest</h6>
			<p>Significant effects of rising atmospheric CO<sub>2</sub> from human sources are detectable in Pacific Northwest waters now, and these effects will continue to grow as CO<sub>2</sub> continues to increase. The Puget Sound and adjacent Pacific Northwest marine waters are particularly vulnerable to ocean acidification due to the combination of several factors that affect the dissolved CO<sub>2</sub> concentration, pH, and aragonite saturation state of seawater. These can include the amount of global CO<sub>2</sub> in the atmosphere, the emission of other acidic gases besides CO<sub>2</sub>, the high rates of plankton growth that ultimately drive oxygen down and release CO<sub>2</sub> via respiration in the water column, runoff of nutrients that fuel plankton growth, and upwelling off the Washington coast that brings nutrient-rich and low pH waters up to the surface. Through ocean circulation, these waters eventually can make their way into Puget Sound.</p>
			<p>Oceanic waters at depths of 150 to 300 meters on the coast of Washington naturally have higher dissolved CO<sub>2</sub> concentrations than surface waters, due to respiration. Upwelling causes this water to rise upward closer to the surface where it mixes with the increased dissolved CO<sub>2</sub> from the atmosphere. This results in waters that are more corrosive than during pre-industrial times. Current atmospheric CO<sub>2</sub> concentrations cause an increase in coastal surface water corrosive conditions from 11 to 33 percent of the time. </p>
			<p>Puget Sound waters meet federal and state water quality standards in terms of pH. However, southern Hood Canal and the Whidbey Basin are of particular concern because pH levels there are among the lowest in Puget Sound, partially because the high productivity leads to high biological respiration rates producing more CO<sub>2</sub>, which adds to the atmospheric CO<sub>2</sub> signal.</p>
			<h6>Impacts on species</h6>
			<p>Direct changes to marine water quality will occur as seawater pH declines and corrosiveness increases. These changes will alter biological communities in Puget Sound. Species that build shells or other internal structures from calcium, such as molluscs, crustaceans, and echinoderms are affected by corrosive conditions, with negative consequences on shell formation, survivorship, or reproduction. Of the species likely to be affected, molluscs have so far received the most attention. Laboratory tests have shown impacts on Olympia oysters, pteropods (also known as sea angels and sea butterflies), red urchins, and northern abalone. Combining two or more stressors—for example, high temperature and low pH or aragonite saturation state—can cause more harm than either stressor alone. While most marine organisms can tolerate a range of environmental conditions, at some point their tolerance fails. Evidence of conditions in Puget Sound that exceed the tolerance of some native species—pteropods, for instance—have been detected.</p>
			<p>Corrosive conditions are particularly of concern to the shellfish industry in Puget Sound, which depends on good water quality to grow oysters, clams, and mussels. Already, this industry has had to make changes to its culture practices to adapt to lower pH water.</p>
			<p>Ecosystem-based models suggest that changes to crustacean abundance—especially copepods, a kind of zooplankton—will have a strong impact on overall food web structure. Not only would Vital Sign indicators such as the Marine Water Condition Index and harvestable shellfish beds be among those impacted by ocean acidification, but also planktivorous forage fish and those species higher up in the food web, such as salmon and marine birds that depend on forage fish for food. </p>
