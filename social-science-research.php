			<h2 class="margin-0-top">SOCIAL SCIENCE RESEARCH</h2>
			<p>Social science research provides useful information to inform the design of programs, evaluates the progress of recovery actions, informs policy decisions, and further explores the relationship between people and their environment. </p>
			<h3>HUMAN WELLBEING</h3>
			<p>The Human Wellbeing Vital Signs address the recovery goals for a healthy human population and vibrant human quality of life, particularly as they relate to people&rsquo;s engagement with the natural environment of Puget Sound. The Vital Signs include familiar aspects of human health that contribute to our wellbeing, such as physical and psychological health, as well as social, cultural, and economic wellbeing and governance, or the way that people participate in decision-making. <a href="evaluating-human-wellbeing.php">LEARN MORE</a></p>
			<h3>SOCIAL SCIENCE ADVISORY COMMITTEE</h3>
			<p>The Partnership&rsquo;s Science Panel established the Puget Sound Social Science Advisory Committee (SSAC) in 2010. The SSAC engages social scientists from many disciplines to inform and support Partnership goals and advise the Science Panel on matters related to the social sciences, salmon, and ecosystem recovery. Committee members include social scientists, both academics and professionals, from the Puget Sound region. The SSAC meets regularly to discuss advancements in social science research and monitoring, guide additional Puget Sound priority social science, review Partnership materials, and provide a liaison to the Science Panel. The SSAC also informs the work of the Puget Sound Institute (PSI) in support of recovery efforts.</p>
			<h3>SOCIAL SCIENCE RESEARCH NEEDS FOR PUGET SOUND RECOVERY</h3>
			<p>In 2013, the Puget Sound Institute conducted a workshop for regional social scientists to determine research and monitoring needs or gaps related to Puget Sound recovery. Through the workshop social science research and monitoring needs for Puget Sound recovery were identified and used to inform the 2016 Biennial Science Work Plan. The social science research agenda will be revisited and revised in 2018.			</p>
			<h3>NETWORK ANALYSIS</h3>
			<p>In 2013 the Puget Sound Partnership commissioned researchers at the University of Washington to investigate the social connections influencing Puget Sound recovery, because a key part of the agency&rsquo;s mission is to build working partnerships. <a href="https://pspwa.box.com/s/knxdmd0st71w4f3z3ggk7nfk76oqbx6d">LEARN MORE</a></p>
			<h3>MUNICIPAL BARRIERS TO GREEN INFRASTRUCTURE</h3>
			<p>To better understand the barriers local governments face to installing green infrastructure, like improved stormwater management or low-impact development, the Puget Sound Partnership in 2015 commissioned the LEAF program at Edmonds Community College to conduct research. Researchers identified such barriers as lack of communication across local government divisions and uncertainties in performance and cost. Solutions also emerged, such as providing financial assistance to update infrastructure and train staff. </p>
