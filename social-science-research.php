					<p>Surveys to measure behavior related to yard care, car maintenance, home maintenance, pet/animal waste, onsite septic systems, small farms, and recreational boating and fishing were conducted in 2012, 2013, and 2015. In addition, the first survey established the baseline and included a segment asking about social capital, or the value of social connectedness.  The Sound Behavior Index is part of the <a href="http://www.psp.wa.gov/evaluating-vital-signs.php">Sound Stewardship Vital Sign</a> and will continue to be monitored. Sound Behavior Index reports are available <a href="https://pspwa.box.com/s/45tqlfh4w6qidabkzlr21jy3y82xv0po">here</a>. </p>
				
				</li>
