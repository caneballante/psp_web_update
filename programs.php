		<h2 class="margin-0-top">Executive Management Team</h2>
			<p>The Executive Management Team is responsible for the strategic leadership and management oversight of the Puget Sound Partnership. The Executive Management Team includes the Executive Director, Deputy Director, and Director of Administrative Services. </p>
			<h2 class="margin-0-top">EXTERNAL OPERATIONS</h2>
			<p>The External Operations Division consists of the Boards, Communications, Integrated Planning, and Science and Evaluation programs. </p>
		
			<h2 class="padding-20-left">ADAPTIVE SYSTEMS AND ACCOUNTABILITY PROGRAM</h2>
		<p class="padding-20-left">The Adaptive Systems and Accountability Program leads the development, management, and implementation of adaptive management processes and information management systems that support the recovery and long-term protection of the Puget Sound ecosystem. Using the  <a href="http://cmp-openstandards.org/">Open Standards for the Practice of Conservation</a> as a guiding framework, the team works closely with Partnership staff and partners to ensure decisions about Puget Sound recovery are informed by the best available science, consider the diverse perspectives from around the region, and incorporate learning from past and current efforts.</p>
<h2 class="padding-20-left">BOARDS</h2>
			<p class="padding-20-left">The Boards Program is responsible for supporting the Leadership Council, Ecosystem Coordination Board, and Science Panel, which in turn guide the agency in its charge to accelerate Puget Sound recovery. The Boards Program also supports the Puget Sound Salmon Recovery Council, which advises the Leadership Council on matters related to salmon recovery. This program coordinates and focuses the efforts of recovery partners on each of the agency’s boards to align actions, remove barriers, build partnerships, mobilize funding, and drive innovation to advance the recovery effort.    </p>
			<h2 class="padding-20-left">COMMUNICATIONS</h2>
			<p class="padding-20-left">The Communications Team fosters the networks and relationships through the which the Puget Sound recovery community can come together to create and deliver effective, decision-critical communications.
The Communications Team engages partners in Puget Sound recovery to build trust, reinforce relationships, and foster commitments, enabling key decision-makers to have the tools, information, and support needed to make policy and funding decisions that support Puget Sound recovery.</p>
			<h2 class="padding-20-left">INTEGRATED PLANNING</h2>
		<p class="padding-20-left">The Integrated Planning Program facilitates broad collaboration and communication to develop a shared vision and plan to accelerate Puget Sound recovery. This program builds strategic partnerships, identifies barriers and priorities, and seeks to empower partners with the tools, funding, and resources they need to implement local and regional recovery plans. </p>
		<h2 class="padding-50-left">PLANNING</h2>
			<p class="padding-50-left">The Planning Team leads the planning activities of the agency, particularly the collaborative development, adoption, and implementation of the <a href="http://www.psp.wa.gov/202AAupdate.php">Action Agenda</a>, as well as the development and execution of <a href="http://www.psp.wa.gov/implementation-strategies.php">Implementation Strategies</a>.  The Planning Team works collaboratively to incorporate the work of many partners to identify and describe regional strategies and specific actions needed to protect and restore Puget Sound. The Team also works to ensure that the Action Agenda brings efficiency and coordination to a complex system, guides effective investment in Puget Sound recovery, and meets both our federal National Estuary Program and state-mandated requirements.   </p>
			<h2 class="padding-50-left">ECOSYSTEM RECOVERY</h2>
			<p class="padding-50-left">The Ecosystem Recovery Team works closely with the region’s Local Integrating Organizations and salmon recovery Lead Entities to align actions, remove barriers, and direct resources to advance the most important actions embedded in recovery plans. This team empowers partners and decision-makers to be well-informed by providing resources, offering technical assistance, and helping to craft regional solutions to overcome barriers. The Salmon Recovery Program resides within the Ecosystem Recovery Team and focuses on managing and implementing the Puget Sound Salmon Recovery Plan and the Puget Sound Acquisition and Restoration (PSAR) fund to support projects and programs that protect and recover salmon habitat in Puget Sound.   </p>
			<h2 class="padding-20-left">SCIENCE AND EVALUATION</h2>
		<p class="padding-20-left">The Science & Evaluation Program is responsible for coordinating and implementing a shared, science-based system of measurement and monitoring that allows assessment of collective actions. This team’s work informs updates to the Puget Sound Action Agenda by tracking the status of Near Term Actions and ecosystem recovery indicators and assessing the effectiveness of recovery efforts. Science & Evaluation staff report ecosystem recovery progress through the Puget Sound Report Card and Vital Signs websites, as well as the biennial State of the Sound report. The Science & Evaluation Program also provides support to the Science Panel, including development of the Quadrennial Science Work Plan, and provides support for the Puget Sound Ecosystem Monitoring Program (PSEMP). </p>
			<h2>INTERNAL OPERATIONS</h2>
			<p> The Internal Operations Division supports the core functions of the agency by providing Fiscal, Human Resources, Administrative, and IT services. Additionally, it is the central point for all Environmental Protection Agency (EPA) program management. </p>
			<h2 class="padding-20-left">FISCAL</h2>
			<p class="padding-20-left">The Fiscal Services Program provides leadership and support for agency priorities and programs through quality budgeting, contracting, grant management, purchasing, and accounting services. The program performs agency-wide financial activities, and ensures financial compliance with state and federal laws/regulations and reporting requirements, as well as ensures operational effectiveness and financial stability of the agency.  Fiscal Services monitors the day-to-day fiscal operations of the agency, ensuring funds are effectively requested and used in alignment with the agency's mission and goals.  </p>
			<h2 class="padding-20-left">EPA</h2>
			<p class="padding-20-left">The Puget Sound Partnership is part of the National Estuary Program (NEP) administered by the U.S. Environmental Protection Agency (EPA). The Partnership and EPA have cooperative agreements that support our base responsibilities related to the Puget Sound NEP and implementation of the Puget Sound Action Agenda. The Partnership’s EPA Program Manager is responsible for compliance with the requirements in the cooperative agreements. The Program Manager is the lead for developing cooperative agreements, negotiating amendments to the agreements, complying with EPA reporting requirements, coordinating activities with other NEPs in EPA Region 10 and nationally as needed, as well as providing subcontract oversight, coordinating with other state agencies and entities that receive NEP funds, and other related activities. </p>
