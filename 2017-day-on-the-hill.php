			<p class="padding-20-top">More than 60 people concerned about the future of Puget Sound met in Washington, D.C., May 1-3, 2017, to participate in Puget Sound Day on the Hill. The trip helped to raise the awareness of our nation’s decision-makers, ensuring them the opportunity to learn about the successes, opportunities, challenges and importance of the ongoing work for a clean, productive and resilient Puget Sound.</p>
			<p>Participants represented a broad range of interests: state and local governments, non-profit organizations, Puget Sound treaty tribes, and agricultural as well as small- and industrial-scale businesses.</p>
			<p>More than 150 individuals and organizations have also signed a letter supporting the awareness-raising purpose of the trip and acknowledging the energy shared by so many in our region for protecting the health of Puget Sound. The ever-growing list of signatories reflects the great diversity of support for federal funding for Puget Sound recovery.</p>
			<p>Participants met with Washington state senators and representatives, as well as with staff from congressional offices and committees and agency representatives. Topics of conversation included the importance of a healthy Puget Sound to the state’s economy and stories of innovation, community commitment, and project success in recovering Puget Sound.</p><br>
 
