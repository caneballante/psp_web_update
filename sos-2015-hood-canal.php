		<p>The Hood Canal LIO, administered by the Hood Canal Coordinating Council (HCCC), established the <a href="https://hcccwagov.app.box.com/s/l24h4xm3yub01523dpfp" target="_blank">Integrated Watershed Plan (IWP)</a>, a comprehensive strategic framework for advancing a shared regional vision by integrating existing and future natural resource management efforts across Hood Canal jurisdictions (including Mason, Jefferson, and Kitsap counties, the Skokomish Tribe, and Port Gamble S&rsquo;Klallam Tribe).</p>
		  <p>As a council of governments with a regional focus, HCCC can advance strategic regional priorities in a way that individual jurisdictions cannot.</p>
  <h3>The Integrated Watershed Plan for Hood Canal</h3>
		  <p>The IWP identifies key pressures facing the Hood Canal environment and the wellbeing of its communities, and focus areas with measurable indicators to monitor the success of our Strategic Priorities at addressing those pressures. </p>
		  <p>HCCC launched a new reporting tool to share this information with its partners and public, at <a href="http://www.ourhoodcanal.org/" target="_blank">OurHoodCanal.org</a>, where people can stay up-to-date on our efforts to keep Hood Canal a unique and special place for generations to come. </p>
          <img class="img-responsive" src="images/hood-canal.png" />
          <p class="caption">OurHoodCanal.org reports on Hood Canal ecosystem indicators and HCCC's progress toward achieving the Integrated Watershed Plan goals.</p>
  <h3>Highlights of IWP Accomplishments: </h3>
  <p>Strategy B1 is to implement the <a href="http://hccc.wa.gov/AquaticRehabilitation/Regional+PIC/default.aspx" target="_blank">Hood Canal Regional Pollution Identification and Correction Program (HCRPIC)</a>. The HCRPIC Program coordinates shoreline water quality surveys among Hood Canal&rsquo;s three Counties (Kitsap, Jefferson, Mason), Conservation Districts, and the Skokomish and Port Gamble S&rsquo;Klallam tribal governments â€“ to systematically survey priority Hood Canal shoreline areas for septic system leaks and investigate and correct pollution hotspots. By 2017, the HCRPIC Program will survey 250 parcels, investigate 80 pollution hotspots, update GIS maps of all septic systems in Hood Canal, conduct a Pilot Nutrient Study to further PIC work in the region, and develop a regional public outreach plan.</p>
		  <p>One of the Activities of Strategy B2 is to develop technical guidance for <a href="https://hcccwagov.app.box.com/s/ddf3x1fzf20zpfnjnpw7vh1hsd3ru563" target="_blank">salmon recovery prioritization in Hood Canal</a>. This guidance serves as a &ldquo;diagnosis,&rdquo; synthesizing the issues adversely affecting each salmonid stock, and lists and ranks the actions necessary to recovery Hood Canal Summer Chum salmon, and Skokomish and Mid-Hood Canal Chinook salmon. This prioritization assists the annual Salmon Recovery Funding Board funding process in Hood Canal, as well as long term salmon recovery planning. </p>
		  <p>IWP Strategy B3 is to implement and manage the Hood Canal <a href="http://hccc.wa.gov/In+Lieu+Fee+Mitigation+Program/default.aspx" target="_blank">In-Lieu Fee Mitigation Program</a>, which conducted two credit-sales and in 2014 completed its first mitigation project, collaborating with the Great Peninsula Conservancy to acquire and restore 14 acres of important wetlands at Irene Pond, near Belfair. </p>
  <h3>Looking forward in Hood Canal</h3>
		  <p>The IWP will be adaptively managed as progress is made and new priorities emerge. For 2016, the IWP will be updated to better reflect HCCC&rsquo;s role in the region and provide specific guidance to inform our planning processes, including Near Term Actions prioritization for the Puget Sound Action Agenda.</p>
		  <p>HCCC is committed to continuing its research and assessment of Hood Canal&rsquo;s ecosystem indicators, including exploring how <a href="http://ourhoodcanal.org/content/human-wellbeing" target="_blank">human wellbeing</a> relates to the natural environment. </p>
		  <p>Hood Canal&rsquo;s unique geology and natural resource industries make the ever-changing climate a particular concern for its communities. HCCC will continue to develop plans for a regional climate adaptation strategy that can be integrated into existing planning processes.</p>
