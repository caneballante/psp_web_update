						<td><h3>NTA IMPLEMENTATION</h3></td>
						<td><ul>
								<li>Lack of funding and resources is the most commonly cited barrier to progress.</li>
								<li>Factors observed or cited for enabling success:</li>
								<ul>
									<li>Increased attention for the NTA due to its inclusion in the Action Agenda</li>
									<li>Development of systems to make information easily accessible </li>
									<li>Establishing a manageable scope</li>
									<li>Presence of collaborative partners</li>
									<li>Alignment of diverse groups to share knowledge</li>
									<li>Finding mutually beneficial solutions</li>
									<li>Support from elected officials and boards</li>
									<li>Being willing and able to pursue opportunities as they arise</li>
									<li>Presence of individual champions for each project</li>
									<li>Use of neutral convener(s)</li>
								</ul>
								<li>Presence of a dedicated team to implement and support actions</li>
								<li>NTAs categorized as Strategic Initiatives were more often categorized as On Plan but were not completed at a higher rate.</li>
								<li>Some local NTAs involved partners working at different speeds and levels of funding, making it difficult to fully implement the NTA</li>
								.
								<li>The need for broad consultation in decisionmaking slowed implementation of NTAs.</li>
							</ul></td>
						<td><ul>
								<li>The larger role of the Partnership is designed to support partners by creating the conditions associated with their success, including the following:</li>
								<ul>
									<li>Procuring and aligning resources</li>
									<li>Bringing attention to their high-priority needs</li>
									<li>Defining mutually beneficial solutions with other partners</li>
									<li>Getting support from elected officials and boards</li>
									<li>Mobilizing collaborative partners and workgroups and being a neutral convener</li>
									<li>Helping partners be nimble and responsive as conditions for success arise</li>
								</ul>
							</ul></td>
					</tr>
					<tr>
						<td><h3>NTA EVALUATION</h3></td>
						<td><ul>
								<li>Existing NTA performance measures were not always adequate for measuring progress.</li>
								<li>Levels of detail on quarterly reporting of NTA status data were not consistent across different owners. </li>
								<li>Scope and direction of NTAs often evolved from how they were originally written.</li>
							</ul></td>
						<td><ul>
								<li>Continue to improve performance measures that provide meaningful and actionable information.</li>
								<li>Enable rigorous oversight of reporting through more focused NTAs and reduce the probability of significant divergence from the original scope.</li>
							</ul></td>
