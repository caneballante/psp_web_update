						<td><h3>NTA Development</h3></td>
						<td><ul>
								<li>NTAs need to be narrowly tailored and well-defined in scope to clearly be able to be accomplished in a 2-year implementation period.</li>
								<li>Efforts to increase engagement with local communities succeeded, resulting in a 28 percent increase in NTAs between the 2012 and 2014 Action Agendas. This has increased the need for more planning, tracking, accountability, and management resources to properly support the system. </li>
							</ul></td>
						<td><ul>
								<li>Produce better-designed, discrete, and achievable actions that satisfy specific criteria for NTA adoption.</li>
								<ul>
									<li>Encourage identifying phases for activities or projects expected to take longer than 2 years. </li>
									<li>Request a provisional cost estimate during the action development phase to aid evaluation as to whether an NTA will be achievable in the 2-year timeframe. This also provides a preliminary indication of the cost of the Action Agenda, which helps with the alignment of resources. </li>
									<li>Anticipate the capacity of partners and consultation needs when planning NTAs that can be realistically accomplished within 2 years. </li>
								</ul>
								<li>Ensure NTA reporting and evaluation resources are commensurate with the number of NTAs. </li>
								<li>Focus the Action Agenda more closely around the Strategic Initiatives and better align funding with them.</li>
								<li>Support NTA owners in using models and best practices from other successful projects, to the extent possible. </li>
							</ul></td>
					</tr>
