		<p>Since the first Action Agenda in 2008, the local jurisdictions within the Strait Action Area have focused, in part, on improving the management of and correcting targeted problems associated with stormwater runoff. </p>
		 <p>Located in the rainshadow of the Olympic Mountains, where it rains a mere 16 inches per year, on average, the City of Sequim considers water (and stormwater) as a most valuable natural resource.  With little rain, even less flooding, and no major water quality problems it can be a challenge to convey the need for additional stormwater management. To prepare for growth and changes in climate however, Sequim has drafted its first ever Storm and Surface Water Management Plan (see NTA STRT 28). In the face of this year&rsquo;s drought and the need for water conservation, a compelling case can be made to comprehensively manage water resources with a proactive plan. The Sequim City Council is now considering the draft plan and will soon determine funding strategies to pay for its implementation.  Adoption of the plan by the Sequim City Council is anticipated in early 2016. </p>
		  <p>A short distance to the west, near the edge of the rainshadow, the City of Port Angeles is working, like Sequim and the other three local jurisdictions within the Strait Action Area (i.e., Clallam and Jefferson counties and City of Port Townsend; see NTA STRT 27 and 31 through 34), to improve stormwater management. As the only city or county with a required National Pollutant Discharge Elimination System (NPDES) permit for stormwater, Port Angeles is continuing to implement their program (see NTA STRT 30) and correct existing problems.</p>
		  <p>Because of the vintage of the City&rsquo;s infrastructure, much of the wastewater system is combined with stormwater. In the 1960s, wastewater treatment and force mains were constructed so that most of the combined sewage no longer flowed directly to Port Angeles harbor or the Strait of Juan de Fuca. The system was designed such that combined sewer overflows (CSOs) occur during large rain events, when peak flows overwhelm the pipe capacity.  On average, about 26.3 million gallons of combined sewage and stormwater flows into Port Angeles Harbor each year during those events, for a total of about 74 separate CSO discharges per year. To correct the CSO problems, Port Angeles embarked on a suite of major projects, in two phases (NTA STRT 29), including:</p>
        <ul>
          <li>Building bigger wastewater force mains and a new larger pump station.</li>
          <li>Constructing two new gravity sewer mains.</li>
          <li>Retrofitting a 5 million gallon tank for temporary storage of peak flows.</li>
          <li>Building an influent diversion structure and improvements inside the wastewater treatment plant.</li>
          <li>Retrofitting an industrial marine outfall to become the Port Angeles primary outfall further offshore.  </li>
        </ul>
        <p>Port Angeles has borrowed $35.35 million dollars to accomplish the CSO Reduction program at a total cost of approximately $46 million dollars. When all work is completed, sometime around July 2016, CSOs will have been reduced to less than three per year.</p>
