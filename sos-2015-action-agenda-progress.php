			<p><a name="aaa"></a>While partners report that the majority of Near Term Actions are making favorable progress, many priority actions continue to face serious constraints. Lack of funding is our partners’ leading barrier to implementing Puget Sound recovery actions. </p>
			<h3>About the Action Agenda</h3>
			<p>The Action Agenda is a prioritized set of actions and programs for directing resources for Puget Sound recovery in an efficient and effective way. Hundreds of partners come together to plan, prioritize, and implement the actions needed to recover and sustain Puget Sound.</p>
              <p>
              The biennial State of the Sound report looks at the progress partners are making to implement the Action Agenda as well as the funding needed to advance the three Strategic Initiatives:</p>
              <ul><li><a name="progress"></a>Preventing pollution from urban stormwater runoff</li>
              <li>Protecting and restoring habitat</li>
            <li>Recovering shellfish beds</li></ul>
			<h3>PROGRESS</h3>
			<p>Near Term Actions comprise the core of the Action Agenda and help bring together a wide range of regional partners to find and advance critical solutions to some of the major factors affecting Puget Sound recovery. Although NTAs alone will not deliver recovery, they complement ongoing work by focusing efforts within a framework of agreed-upon priorities. Assessment of NTA implementation, as reported by parties responsible for the actions, is one way the Partnership monitors Action Agenda progress. </p>
			<h6>Status of 2012 Action Agenda NTAs</h6>
			<p>This table shows 2012 Action Agenda NTAs and their implementation status, sorted by all NTAs, Strategic Initiatives, and NTAs not related to Strategic Initiatives.</p>
