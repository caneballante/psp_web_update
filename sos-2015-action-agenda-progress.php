<h6>Linking Strategies and Actions to the Partnership’s Goals for Recovery of Puget Sound</h6>
<p>This project is the first step in evaluating the relationships between the recovery effort—NTA implementation and financial investments—and progress toward ecosystem recovery goals, by connecting those pieces of information that were previously evaluated separately. <a href="sos-2015-linking-strategies-actions.php">Learn more …</a> </p>
			<h6>Report Card forums</h6>
			<p><a name="ll"></a>As a science-based organization, the process of inquiry, learning, and adaptively managing to ensure better results is infused throughout the Partnership’s practices. One such practice involves the Report Card forums held periodically at Leadership Council meetings. These forums are designed with an emphasis on the oversight roles envisioned in RCW 90.71.350(2), focusing on providing the support for any necessary course adjustments rather than assessments of noncompliance. <a href="sos-2015-report-card-forum.php" title="Report Card Forums">Learn more …</a></p>
			<h3>LESSONS LEARNED & RECOMMENDATIONS</h3>
			<p><a name="aas"></a>By analyzing progress in implementing Near Term Actions, the Partnership is able to identify what worked, what didn’t, and make improvements based on these lessons learned. Partnership staff use these observations to improve the strategic planning of recovery efforts, especially in the development of the 2016 revision of the Action Agenda. <a href="sos-2015-lessons-learned.php" title="Lessons Learned">Learn more …</a></p>
			<h3><a name="aas"></a>ACTION AGENDA SUCCESSES</h3>
			<p>A wide range of partners across Puget Sound help to implement the Action Agenda. The 2015 State of the Sound: Report to the Governor and Legislature features three examples of success in state funded programs that are critical to Puget Sound recovery:</p>
	<ul><li>Puget Sound Acquisition and Restoration (PSAR) helps to implement the most important, science-based salmon habitat protection and restoration priorities for Puget Sound.</li>
		<li>Floodplains by Design is a public-private partnership is working to reduce flood risks and restore habitat along Washington’s major rivers.</li>
		<li>An Oil Transport Study provided data and analysis of statewide risks and gaps, identifying options for increasing public safety, and improving spill prevention and response readiness.</li></ul>
			<p>In addition, there have been considerable successes in a range of different projects funded by the federal EPA National Estuary Program, as well as many specific Near Term Actions implemented by Action Agenda partners at the local level. The following pages provide more detail on the latter examples.</p>
			<h6>National Estuary Program achievements</h6>
			<p>Designated state, local, and tribal governments in Puget Sound (Lead Organizations) received federal funding from the EPA to administer the Puget Sound National Estuary Program (NEP) that helps advance Puget Sound recovery. The following pages describe some of the accomplishments of the Lead Organizations for shellfish recovery, watershed protection and restoration, toxics prevention, reduction and control, Marine and nearshore protection and restoration, and tribal priorities. <a href="#" title="National Estuary Program Overview">Learn more ...</a></p>
			<h6>Local Integrating Organization achievements</h6>
			<p><a name="em"></a>Local Integrating Organizations bolster consensus and provide momentum around locally relevant Puget Sound recovery actions, from a range of different local entities. These actors include, but are not limited to, local governments, tribes, private sector entities, watershed planning units, watershed councils, shellfish protection districts, conservation districts, regional fishery enhancement groups, marine resource committees (including those working with the Northwest Straits Commission) nearshore groups, and watershed lead entities, all working to implement the Action Agenda. <a href="sos-2015-lio.php">Learn about Local Integrating Organization accomplishments …</a></p>
<h3>EFFECTIVENESS assessment</h3>
			<p>Assessment of effectiveness of specific action or projects is another way the Partnership is able to measure Action Agenda implementation. This work answers a deeper question about how effective actions are and what the successes, challenges, or barriers are rather than simply whether they have been implemented or not. To date, the Partnership has evaluated several restoration projects for each of the three Strategic Initiatives: Shellfish Restoration in the Samish Basin; Estuary Restoration in the Nisqually, Snohomish, Skagit, and Skokomish river deltas; and Stormwater Pollution Reduction in the City of Seattle. <a href="sos-2015-effectiveness-monitoring.php" title="Effectiveness Monitoring">Learn more …</a></p>
