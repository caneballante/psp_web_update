			<h6>IN PROGRESS: Status of 2014 Action Agenda NTAs</h6>
			<p>While the 2012 Action Agenda NTAs reflect a complete cycle, the 2014 Action Agenda NTAs are still in progress. Completion rates for these actions are expected to increase by the end of the 2-year cycle. The 2012 and 2014 results cannot be directly compared. </p>
          <a name="analyses"></a><img class="img-responsive" src="images/nta2.png" />
