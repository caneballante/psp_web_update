			<h2>No Discharge Zone</h2>
			<p>Last updated: June 2, 2017</p>
			<img  class="floatright margin-20-left" src="images/marina-288x300.jpg" width="175" height="182" alt=""/>
			<h3>Overview of the issue:</h3>
			<p>On July 21, 2016, the Washington State Department of Ecology issued a petition to the U.S. Environmental Protection Agency (EPA) to establish a No Discharge Zone (NDZ) in Puget Sound area waters. The petition had long been supported by the Puget Sound Partnership and other state agencies. An NDZ is a designated body of water where the discharge of sewage from vessels is prohibited, whether treated or not. An NDZ is the only way for the state to gain authority to control this potential source of pollution. The decision to petition EPA came after more than four years of evaluation, extensive stakeholder outreach and a 2014 draft petition that went out for public comment. After review of the petition and extensive opportunity for public comment, in December 2016 the EPA determined that there are adequate facilities in the Puget Sound for vessels to pump out their sewage and Washington state may therefore move forward with an NDZ designation. With EPA’s approval in place, Ecology may now begin initiating rulemaking to make Puget Sound off limits to vessel sewage. The maritime industry – notably the tug and small cruise ship sectors – has expressed concerns about the cost of compliance. While EPA’s December determination was considered final, private interests are now petitioning EPA Administrator Scott Pruitt to rescind.</p>
 
			<h3>In the news:</h3>
<p>KING 5 news: <a href="http://www.king5.com/tech/science/environment/presidents-epa-counsel-calls-meeting-over-puget-sound/444319695">President's EPA counsel calls meeting over boat discharge in Puget Sound (June 2017)</a></p>
<p>WorkBoat Magazine: <a href="https://www.workboat.com/news/government/operators-fear-cost-puget-sound-no-discharge-zone/">Operators fear cost implications of Puget Sound no discharge zone (March 2017)</a></p>
<p>KNKX: <a href="http://knkx.org/post/epa-moving-ahead-no-discharge-zone-puget-sound">EPA Moving Ahead With No-Discharge Zone For Puget Sound (November 2016)</a></p>
 
 
			<h3>What our partners are saying:</h3>
			<p><a href="http://wagreen.p2a.co/lyrHluC">Washington Environmental Council</a></p> <p><a href="http://www.ecy.wa.gov/programs/wq/nonpoint/CleanBoating/ndzstatus.html">Department of Ecology</a></p>  
 
			<h3>Puget Sound Partnership statement</h3>
<p>We all want the same thing: a healthy, productive ecosystem. But the discharge of human waste into the waters in which we swim, fish and otherwise recreate is harmful for people and the environment.  The Puget Sound Partnership and our partners work on many fronts to minimize this type of pollution. Making Puget Sound a no-discharge zone for vessel sewage connects a missing piece in our strategy to clean up and restore the Sound. Years of study have gone into the concept of an NDZ. The research supports the need for an NDZ and also ensures there are adequate pump-out facilities in place (and more are planned) to make the transition as smooth as possible. The NDZ will help to protect our valuable shellfish beds, swimming beaches, and sensitive areas vulnerable to human pathogen pollution from vessel waste. There is broad support for establishing an NDZ, including that of the Puget Sound Partnership’s Ecosystem Coordination Board and the Leadership Council. Washington state has made large investments in sewage treatment, stormwater management, and in the control and prevention of industrial pollution and agricultural runoff. Banning sewage discharges from all sources supports the Puget Sound Action Agenda and Washington Shellfish Initiative.</p> 
