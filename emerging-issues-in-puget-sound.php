			<h2>THE HIRST DECISION</h2>
			<p>last update: January 29, 2018</p>
		
			<h3>Overview of the issue:</h3>
 	<img  class="floatright margin-20-left" src="images/olympia.jpg" width="175" height="182" alt=""/>
			<p>The Hirst fix refers to legislation intended to solve problems resulting from the Hirst decision, a State Supreme Court ruling that has been preventing local governments from issuing building permits for dwellings that rely on wells for water where the local government cannot certify the legal availability of that water. During the 2017 legislative session, the Legislature was unable to pass a Capital Budget due to the insistence by Republican legislators on passage of a so-called &ldquo;Hirst fix&rdquo; before a Capital Budget vote would be considered. This became a critical situation for Puget Sound recovery as the funds for many recovery projects and programs was included in the Capital Budget. Finally, in late January 2018, both houses of the Legislature passed Engrossed Substitute <strong>SB 6091, a compromise bill addressing the Hirst decision</strong>. The bill is detailed and complex—a summary is included in the <a href="http://lawfilesext.leg.wa.gov/biennium/2017-18/Pdf/Bill%20Reports/House/6091-S.E%20HBR%20APH%2018.pdf">House Bill Report</a>, available on the state&rsquo;s Legislative Information website, <a href="http://www.leg.wa.gov/">leg.wa.gov</a>. In addition, the Washington Department of Ecology <a href="https://ecology.wa.gov/Water-Shorelines/Water-supply/Water-rights/Case-law/Hirst-decision">offers information</a> on their website about the legislation and how it will be implemented. One important feature of the bill is an expression of the Legislature&rsquo;s intent to appropriate $300 million for projects to achieve the goals of the act until June 30, 2033.</p>
		
 

