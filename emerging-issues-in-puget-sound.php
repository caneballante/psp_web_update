			<h2>JLARC Audit Findings</h2>
			<p>last update: June 6, 2017</p>
			<img  class="floatright margin-20-left" src="images/olympia.jpg" width="175" height="182" alt=""/>
			<h3>Overview of the issue:</h3>
 
			<p>The Puget Sound Partnership’s enabling legislation, Engrossed Substitute Senate Bill 5372, directed the Joint Legislative Audit and Review Committee (JLARC) to conduct two performance audits of the Partnership, the first in 2011 and the second in 2016. JLARC staff completed the 2011 performance audit, as well as a follow-up report in 2013. The report of the 2016 audit was released in May 2017.</p>
 
			<p>Recognizing the first JLARC reviews were likely too early to measure progress in meeting the Legislature’s goals and objectives, the 2011 audit and 2013 follow-up report focused on key accountability directives assigned to the Partnership: would the Partnership be able to determine which restoration efforts were getting results? The Partnership agreed with the audit’s conclusion that much work was still needed to create and implement measures to understand what was and was not working in efforts to meet statutory goals and objectives.</p>
 
			<p>The 2016 audit was designed to answer the following questions:</p>
			<ul> 
       			<li>What progress has been made in restoring Puget Sound by 2020?</li>
				<li>Are restoration efforts and expenditures across the Puget Sound tracking with the priorities and strategic initiatives set by the Partnership to meet legislative goals?</li>
				<li>How is the Partnership meeting key oversight and accountability requirements directed by statute? Specifically, how has it improved linking actions to progress on restoration goals, prioritizing actions, and monitoring effectiveness?</li>
				<li>Has the Partnership identified any barriers to implementing the Action Agenda? If there are barriers, have solutions been identified and communicated to the Governor and/or Legislature?
				<li>How is the Partnership utilizing monitoring and adaptive management in updating the Action Agenda?</li>
 			</ul>
			<p>The audit included a review of other national ecosystem restoration efforts to inform these questions.</p>
 
			<p>In their final report, the JLARC auditors noted that the Partnership’s ability to report data has been improved by adopting recovery targets that are linked to five of the six recovery goals identified by the Legislature; prioritizing near-term actions in the Puget Sound recovery plan (Action Agenda); and more clearly reporting on the status toward recovery targets. Areas that still need to be addressed encompass the following:</p>

			<ul>
				<li> Meeting the statutory requirements for identifying partner organizations whose actions are noncompliant with the Action Agenda or those that are making outstanding progress toward recovery goals</li>

				<li>Fully integrating salmon recovery within overall recovery efforts</li>
				<p>The report identifies three factors that hinder Puget Sound recovery efforts:</p>
			</ul>
			<ul>
				<li>Short planning timeframes</li>
				<li>An incomplete inventory and analysis of actions and funding</li>
				<li>Unclear structure and execution of the monitoring program</li>
			</ul>


		    <p>JLARC’s recommendations encompass the following:</p>

			<ul>
				<li>Submit a plan to the Legislature that identifies and addresses needed revisions to the planning and recovery timeframes.</li>
				<li>In collaboration with the Office of Financial Management, submit a plan to the Legislature that details how the Partnership will create a more complete inventory of recovery actions and funding.</li>
				<li>Submit a plan to the Legislature that details how the Partnership will address the deficiencies in its ability to meet the essential requirements for a monitoring program, as identified by JLARC staff. The plan will also address how the Partnership will improve and clarify links between monitoring and planning.</li>
			</ul>
			 <p><a href="http://leg.wa.gov/jlarc/reports/2017/PSPartnership2016/pf/default.htm">Read the JLARC report</a></p>

 

