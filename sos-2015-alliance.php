			<h3>Oakland Bay and Johns Creek Restoration</h3>
			<p>The Alliance for a Healthy South Sound (AHSS) identified restoration and protection of Oakland Bay and Johns Creek as a Near Term Action under the Puget Sound Partnership's Shellfish and Habitat Strategic Initiatives for the 2014 Action Agenda for Puget Sound.</p>
			<h6>What was the action?</h6>
			<p>Work is underway to restore the former Bayshore golf course in Oakland Bay (near Shelton). Multiple NGOs and government agencies contributed to the acquisition of the site and associated preparation for restoration activities. Capitol Land Trust secured multiple grants from funding agencies and project partners, including $1.8 million through the Washington State Recreation and Conservation Office, $1.25 million from the U.S. Fish and Wildlife Service and U.S. Environmental Protection Agency through the state Department of Ecology, as well as cash and/or in-kind match from the Squaxin Island Tribe, Mason Conservation District, Taylor Shellfish Farms, South Puget Sound Salmon Enhancement Group, and Trust for Public Land.</p>
			<h6>Why was it important?</h6>
			<p> A state Department of Fish and Wildlife assessment noted that the marine shoreline in Oakland Bay is among the highest habitat values in the South Puget Sound. With sufficient water flows, Johns Creek is capable of supporting coho along with fall and summer chum. The area is of high cultural value to the Squaxin Island Tribe; the mouth of Johns Creek is the site of what was once one of the largest longhouses and Squaxin villages. The project&rsquo;s value also derives from prevention of development in and around Oakland Bay, which could result in further habitat degradation and water quality issues.</p>
			<p>The project plays a key role in a larger, strategic effort by Capitol Land Trust and the Squaxin Island Tribe to conserve key marine nearshore and freshwater habitats and ecosystem function in the Oakland Bay watershed. This emphasis on the importance of small estuary restoration action is reflected in the Oakland Bay and Hammersley Inlet Nearshore Habitat Assessment, in collaboration with the Oakland Bay Action Plan and in South Puget Sound salmon recovery plans.</p>
			<h6>What is next?</h6>
			<p>As of summer 2015, construction is under way at the former Bayshore golf course in Oakland Bay to restore the Johns Creek estuary and associated marine shoreline. When construction is complete, the golf course fairways will be replanted with native vegetation, a 1,400-foot dike will be removed to provide migrating salmon with additional acres of saltwater marsh. Re-vegetation and installed habitat features along Johns Creek will provide cool water and in-stream structural complexity, both important components of high quality salmon habitat.</p>
			<h6>Key Project Accomplishments</h6>
			<ul>
				<li>Conserved 74 acres along Oakland Bay.</li>
				<li>Preserved 4,000 feet of marine shoreline.</li>
				<li>Preserved 27 acres of intact, saltmarsh habitat.</li>
				<li>Removed a 1,400-foot tidal dike.</li>
				<li>Retired approximately 250 acre feet per year of surface water rights that were used for irrigating the golf course. Those water rights were returned for instream flows.</li>
			</ul>
