			<p>The Puget Sound Starts Here (PSSH) campaign seeks to increase awareness of the magnitude of Puget Soundâ€™s pollution problem, and to motivate residents to adopt new behaviors that will decrease the amount of pollution entering the Sound through stormwater runoff and other sources of pollution. The tools developed to achieve these goals include a recognizable brand, television advertisements, a Web site designed to foster behavior change, and public outreach tools for use by PSSH partners at a local, grassroots level.</p>
      		<p><span style="font-weight: bold">We encourage ECO Net members and other PSSH partners to use the campaign photos and artwork to tie into their environmental education, communication and outreach efforts. &nbsp;The photos and artwork provided should NOT be used to promote efforts or messaging that is inconsistent with the purpose and intentions of the Puget Sound Starts Here campaign.</span><br />
      				<br />
      				In an effort to increase creative, effective and successful use of the PSSH campaign, please share your ideas and ways you have used the campaign for your efforts with the Partnership so they can be shared with other education, communication and outreach providers. &nbsp;<span style="font-weight: bold">The Partnership is happy to provide any support possible for the development of campaign materials and efforts. &nbsp;</span>Please contact  <a href="mailto:catherine.cochrane@psp.wa.gov">Catherine Cochrane</a>For assistance with files and formats please contact <a href="erika.lari@psp.wa.gov">erika.lari@psp.wa.gov</a> </p>
