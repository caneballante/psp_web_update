			<h2>About the Action Agenda</h2>
			<p>The Action Agenda charts the course for recovering Puget Sound. The Action Agenda complements and incorporates the work of many partners from around Puget Sound and describes regional strategies and specific actions needed to protect and restore Puget Sound. These strategies and actions are based on scientific information and stakeholder input, and provide opportunities for federal, state, local, tribal, and private entities to better invest resources and coordinate actions. The Action Agenda comprises two components: the Comprehensive Plan and the Implementation Plan. The Comprehensive Plan charts the course for long-term Puget Sound recovery by introducing the approaches by which issues and activities are prioritized, progress is evaluated, and strategies and actions are adapted over time. The Implementation Plan is the action component of the Action Agenda. Based on the fundamental framework and broad strategies described in the Comprehensive Plan, the Implementation Plan defines the suite of Near Term Actions that will advance recovery over the next 4 years. The Implementation Plan also includes ongoing programs that are part of existing Puget Sound recovery efforts.</p>
			<h2>The 2018-2022 Action Agenda builds on lessons learned</h2>
			<p>This 2018-2022 Action Agenda builds on past Action Agendas and reflects several new developments that focus and prioritize actions and investments. Improvements include:</p>
			<ul>
				<li>Increased reliance on Vital Sign Implementation Strategies, which result in more targeted and specific strategies to achieve Vital Sign indicator targets built on current scientific knowledge and analyses of existing recovery work.</li>
				<li>A complete and direct discussion of the threats facing Puget Sound and barriers that continue to impede the recovery effort.</li>
				<li>Re-organized content in the Comprehensive Plan to more clearly communicate the process for building, implementing, and improving the Action Agenda and the Puget Sound recovery effort.</li>
				<li>Updated descriptions of important issues and strategies that apply to all Puget Sound recovery efforts, such as mobilizing funding, integrating science into our framework, climate change, education, and behavior change. And further clarifying how these concepts inform recovery planning.</li>
				<li>A vision for Puget Sound resilience and recovery beyond the Washington Legislatures intended completion date of 2020.</li>
				<li>An updated list of priority recovery actions to accelerate Puget Sound recovery progress and guide funding and implementation over the next four years.</li>
			</ul>
			<a id="supp-materials"></a>
			<h2>What happens next?</h2>
			<p>Post October 15, the Partnership will release a detailed matrix of comments received and summary of responses, and make appropriate revisions to the Action Agenda. In the late fall and early winter, the Partnership will present the draft to the Management Conference, culminating with the Leadership Council considering the Action Agenda for adoption in December.  </p>
