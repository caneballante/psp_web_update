		<p>The Whatcom LIO has 14 Near Term Actions (NTAs) addressing issues ranging from Chinook recovery and shellfish harvest closures to mitigating stormwater runoff. While progress is being made on all fourteen of the NTAs, the NTAs highlighted below have progressed at a faster pace because funding was available to support the implementation of milestones.  </p>
        <h3>Implement Chinook Restoration Projects in the WRIA 1 Salmonid Recovery Plan 3-Year Plan (NTA-WH1)</h3>
          <p>Restoration projects are designed to deliver immediate benefit to North Fork/Middle Fork Nooksack and South Fork Nooksack early Chinook salmon, while restoring habitat-forming processes to the extent feasible. In 2014, five projects were completed involving construction of 83 log jams over 2.4 miles of mainstem and tributary Chinook habitat to form pools, create temperature refuges, provide complex woody cover, improve upstream passage, increase floodplain connectivity and/or restore stable side channels for spawning.&nbsp;Designs were completed or funded for restoration of an additional four priority reaches, which will queue up projects benefitting Chinook and other salmonids for construction through 2017.</p>
  <h3>Implement Chuckanut Bay Pollution Identification and Correction Program (NTA WH9) </h3>
          <p>The Whatcom County Marine Resources Committee initiated a bi-weekly sampling program in 2015 in the fresh and marine water to help identify pollution sources to Chuckanut Bay. The results of the sampling are shared with Whatcom County Health Department and Washington State Department of Health.  As part of this NTA, the Whatcom County Health Department has been compiling and evaluating onsite sewage system data for the Chuckanut Bay drainage with the goal of evaluating 75 percent of systems and repairing 100 percent of identified failing systems by December 2015. The outreach strategy to engage the community in water quality issues and improvement efforts include workshops, one of which was conducted in fall of 2014 and another that is planned for fall of 2015.  </p>
        <h3>Implement Whatcom County Pollution Identification and Correction Program (NTA WH10)</h3>
          <p>The Whatcom County Pollution Identification and Correction (PIC) Program is a component of and coordinated with the Whatcom Clean Water Program (NTA C7.1). It involves multiple partners working throughout Whatcom County to identify and correct sources of fecal coliform that have resulted in closure of shellfish harvesting.   Whatcom County Departments of Public Works and Health, Whatcom Conservation District, Washington Departments of Ecology, Health, and Agriculture, Natural Resources Conservation Service, Lummi Nation, and Nooksack Indian Tribe are coordinating monitoring efforts in fresh and marine water throughout the Nooksack River basin and coastal drainages.  A volunteer landowner monitoring program was also recently initiated by the Whatcom Conservation District with funding support from the Whatcom LIO.  </p>
        <p>One goal of the Whatcom PIC Program is to provide technical and financial assistance to 50 agricultural operations per year.  In the first half of 2015, Whatcom County Public Works and Whatcom Conservation District contacted 57 landowners with potential non-dairy livestock in the Drayton Harbor watershed. There are currently 25 landowners participating in the Pollution Identification and Correction technical assistance program.  </p>
        <p>Another goal of the Whatcom County PIC Program is to evaluate 75 percent of onsite sewage systems in focus areas per year and repair 100 percent of identified failures. Of the 3,094 systems in the Drayton Harbor watershed, more than 90 percent have been evaluated to date. Of that 3 percent were identified as 'failing.'</p>
        <ul>
          <li>All of the &lsquo;failing&rsquo; systems have been abated with 65 percent installing new systems and 12 percent making repairs to non-treatment components so the system is no longer failing.  </li>
          <li>13 percent of the 3 percent identified as failing had follow-up inspections that showed the system was not failing. </li>
          <li>The remaining 10 percent of the &lsquo;failing&rsquo; systems are vacant and not generating sewage. </li>
